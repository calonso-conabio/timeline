<p>La escuela se fundó en los terrenos de la Hacienda de Santo Tomás de los Tepetates y desde entonces se conoce como "El casco de Santo Tomás", bajo el mandato del presidente Lázaro Cárdenas (1934-1940). El primer director del Instituto Politécnico fue Juan de Dios Bátiz Paredes en 1936.</p>
<p>En 1938 se inció la impartición de las licenciaturas de Botánico, Zoólogo, Antropólogo Hidrobiólogo, Entomólogo, Químico Bacteriólogo y Parasitólogo, Médico Rural, Químico Zimólogo e Higienista Dietólogo.</p>
<p>La Escuela se benefició grandemente de la llegada de los exilados españoles que con motivo de la Guerra Civil Española fueron recibidos en México. Muchos profesionales de las ciencias biológicas llegaron en 1939 y principios de los 40s e ingresaron como profesores a la Escuela Nacional de Ciencias Biológicas. Entre ellos estaban Ramón Álvarez Buylla, Modesto Bargalló Ardévol, Adela Barnés, <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXIX/candidoBolivar.php">Cándido Bolivar Pieltain,</a> Federico Bonet Marco, Pedro Carrasco, Manuel Castañeda Agulló, Gómez Vinuenza, Carlos Velo, Bibiano Fernández Osorio-Tafall, Enriqueta Ortega Feliú, Enrique Rioja, Faustino Miranda, Juan De Oyarzabal, Serafina Palma, Dionisio Peláez y María Teresa Toral, entre muchos otros.</p>
