												<h2>Instituto de Biología, UNAM</h2>
												<h3>1930</h3>
											</header>
<p>El Instituto de Biología fue fundado el 9 de noviembre de 1929 en las instalaciones de "La Casa del Lago" del bosque de Chapultepec. La Casa del Lago fue construida durante la intensa remodelación de la que fue objeto con motivo de la conmemoración del Centenario de la Independencia, construcción de estilo francés inaugurada por José Ives Limantour (1854-1935), secretario de Hacienda con el General oaxaqueño Porfirio Díaz (1830-1915). Esta hermosa casa utilizada primero como el Club de Automóvil se convirtió en breve tiempo (1929) en la sede de la Dirección de Estudios Biológicos de la Secretaría de Agricultura y fue uno de los crisoles más importantes de naturalistas en el país. El primer director fue el naturalista y maestro <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfonsoLHerrera.php">Alfonso Luis Herrera </a> (1868-1942), exdirector del Museo de Historia Natural, quien recientemente había creado y dirigido el Zoológico y el Jardín Botánico de Chapultepec.

<p>  Un par de años más tarde (1929), el recinto se convierte en el Instituto de Biología de la Universidad Nacional Autónoma de México y así funciona por tres décadas con el biólogo poblano <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/IsaacOchoterena.php">Jacobo Isaac Ochoterena Mendieta </a> (1885-1950) como primer director seleccionado de una terna: el médico tapatío Eliseo Ramírez Ulloa (1888-1942), el médico michoacano Ignacio González Guzmán y él. Ochoterena promovió un enfoque descriptivo y práctico de la biología con énfasis en problemas médicos, a diferencia del enfoque de Alfonso Herrera, que se centraba en la explicación del fenómeno de la vida, su origen y evolución. </p> 

<p>Entre los naturalistas que pasaron por la Casa del Lago (1929-1959) sobresalen la cactóloga <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/HeliaBravo.php">Helia Bravo Hollis, </a>(1901-2001), entomólogo alemán <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/CarlosCHoffmann.php">Carlos Cristian Hoffman </a>(1876-1942), <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/EnriqueRioja.php">Enrique Rioja Lo Bianco </a>(1895-1963), el botánico <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/MaximinoM.php">Maximino Martínez y Martínez </a>(1888-1964), el mastozoólogo guerrerense <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/BernardoVilla.php">Bernardo Villa </a>(1911-2006), el herpetólogo y ornitólogo <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/RafaelMCampo.php">Rafael Martín del Campo </a>(1910-1987), los micólogos <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/TeofiloHerrera.php">Teófilo Herrera </a>(1926- ) y <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/GastonGuzman.php">Gastón Guzmán </a>(1932-2016), el botánico <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/ManuelRuiz.php">Manuel Ruiz Oronoz </a>(1909-1978), el botánico español <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/FaustinoMiranda.php">Faustino Miranda </a>(1905-1964) y el bioquímico zacatecano Roberto Llamas Flores (1909-1988). La fructuosa influencia de esta generación de naturalistas y científicos mexicanos continúa dando frutos a la fecha. </p>
<p>En 1959 la Casa del Lago inicia sus actividades como Centro Cultural de la Universidad Autónoma de México con el escritor tapatío Juan José Arreola (1918-2001) como primer director.  Después de 23 años en el Bosque de Chapultepec, el Instituto de mudó a Ciudad Universitaria</p>
<p>En el <a href="http://www.ib.unam.mx/">Instituto de Biología </a>se encuentran once Colecciones Biológicas Nacionales: el Herbario Nacional de México (MEXU) con más de 1,300,000 ejemplares. Además en el área de botánica están la Colección de Algas, de Hongos, de Líquenes, de Briofitas, de Plantas Vasculares, la Colección Etnobotánica, la de Frutos y Semillas, la Palinoteca y la Colección de Maderas. Además del Herbario Nacional, las otras colecciones nacionales son la de Helmintos, Insectos, Moluscos, Peces, Ácaros, Anfibios y Reptiles, Arácnidos, Aves, Crustáceos y Mamíferos.</p>


