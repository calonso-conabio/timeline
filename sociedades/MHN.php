												<h2>Museo de Historia Natural</h2>
												<h3>1964</h3>
											</header>
<p>El 24 de octubre de 1964 se inauguró el Museo de Historia Natural, obra del arquitecto Leónides Guadarrama en la Segunda Sección del Bosque de Chapultepec vecino al panteón Dolores. La mayor parte de las colecciones científicas del antiguo <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/MNHN.php"> Museo Nacional de Historia Natural</a> se fueron a la Universidad Nacional Autónoma de México. La museografía fue responsabilidad de expertos de la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional, dirigidos por el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/DionisioPelaez.php">Mtro. Dionisio Peláez </a>. La adaptación del guión museográfico la hizo el arquitecto Ernesto Valdéz con estudiantes de artes plásticas de la Academia de San Carlos y de la Escuela de la Esmeralda del Instituto Nacional de Bellas Artes. </p>

<b>Directores del Museo</b>
<li>1964-1974 <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/AlfredoBarrera.php">Alfredo Barrera Marín, </a>entomólogo, especialista en pulgas, fundador
<li>1974-1980 <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/GonzaloHalffter.php">Gonzalo Halffter Salas, </a>entomólogo, especialista en biogeografía y conducta de escarabajos peloteros<br><br>
De 1975 a 1988 el Museo compartió el espacio y algunos empleados con el nuevo Instituto de Ecología A.C., creado por Gonzalo Halffter. En 1988 el Instituto se mudó a la Ciudad de Durango, Durango y a Xalapa, Veracruz, a las instalaciones del antiguo Instituto Nacional de Investigaciones sobre Recursos Bióticos (INIREB) del <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/ArturoGomezP.php">Dr. Arturo Gómez Pompa.</a>
<br><br>
<li>1980-1992 Yani Herreman, arquitecta y muséologa. En 1980 el Dr. Gonzalo Halffter invitó a Yani Herreman a tomar la responsabilidad del museo. Ella continuó por 12 años. 
<li>1993-1995 Maya Álvarez Icaza Longoría, Arquitecta. 
<li>1996-1997 María Elena Fernández de Camino

<li>1998-2002 <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/AlfredoBarrera.php">Marco Barrera Bassols, </a>museólogo, museógrafo e historiador 

<br><br>
En 1999, la administración del museo se transfirió a la Secretaría del Medio Ambiente del Gobierno del Distrito Federal encabezado por Cuauhtémoc Cárdenas (1997-1999). El presidente de la república era Ernesto Zedillo (1994-2000).
<p>
<li>2002-2007 Nemecio Chávez Arredondo, biólogo
<li>2008-2013 Eduardo Vázquez Marín, antropolólogo, escritor y poeta  
<li>2013-2016 María Guadalupe Fragoso García, comunicadora
<li>2016-2017 Laura Mercedes Jiménez del Arco, arqueóloga </p>

Seguramente el ícono del museo es el gran dinosaurio <i>Diplodocus carnegii</i> de alrededor de 27 metros de longitud y 4 metros de altura, con un gran cuello y una larga cola. Este gran invitado llegó
al Museo del Chopo en el año de 1931, traído por el zoólogo y paleontólogo americano William Jacob Holland (1848-1932). Como director de los museos Carnegie de su amigo el millonario filántropo americano y escocés Andrew Carnegie, William se ocupó de armar varios dinosaurios que fueron donados a distintos museos en Europa y América. Este dinosaurio fue común en Norte América. Su llegada a México fue gracias a la iniciativa del naturalista <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfonsoLHerrera.php">Alfonso L. Herrera. </a>
<br><br>
El Museo de Historia Natural alberga a la Colección Nacional de Insectos <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/AlfredoBarrera.php">Dr. Alfredo Barrera Marín" </a>con alrededor de 60,000 ejemplares, curados por la Entomóloga María Eugenia Díaz Batres, quien inició su trabajo en el museo desde 1967 y ha estado a cargo de la colección por más de 50 años. En 2012 a la Sala de Entomología del museo se le dio su nombre.  


