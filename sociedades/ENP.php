<p>El Antiguo Colegio de San Ildefonso, ubicado en el Centro de la Ciudad de México fue propiedad de los jesuitas desde su construcción en 1583 hasta que fueron expulsados de la Nueva España en 1767 por decreto del rey Carlos III. Después pasó a ser cuartel de batallón, colegio del gobierno virreinal, Escuela de Jurisprudencia, cuartel de tropas norteamericanas y francesas.</p> 
<p>En 1868 el Presidente Benito Juárez creó la Escuela Nacional Preparatoria con sede en el Antiguo Colegio de San Ildefonso. Su primer director fue el médico poblano Gabino Barreda (1818-1881) y el segundo fue <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXIX/AlfonsoHerrera.php">Alfonso Herrera Fernández </a>(1838-1901). Alfonso Herrera (padre) fue fundador de la <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural </a>y ocupó cargos de presidente, vicepresidente y presidente honorario perpetuo. También apoyó la fundación de la Sociedad Científica <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXVIII/JoseAlzate.php"> Antonio Alzate </a> en 1884 con sede en la Escuela Nacional Preparatoria. Más tarde en 1930 se convertiría en la Academia Nacional de Ciencias “Antonio Alzate” en 1930.</p>  
<p>En 1980 la Escuela Nacional Preparatoria se mudó al Plantel Número 1 “Gabino Barreda” en Tepepan, Xochimilco. Actualmente cuenta con nueve planteles en la Ciudad de México.</p>



