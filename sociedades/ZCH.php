												<h2>Zoológico de Chapultepec</h2>
												<h3>1923</h3>
											</header>
<p>En 1923 <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfonsoLHerrera.php">Alfonso Luis Herrera </a>informó que se construiría un parque zoológico en terrenos de Chapultepec (1.4 ha) donados por la Secretaría de Comunicaciones y Obras Públicas. Esto sucedía durante la presidencia de Álvaro Obregón (1920-1924) época de reconstrucción de instituciones como la Secretaría de Educación Pública (1921) y el impulso de las artes plásticas de artistas como Diego Rivera, José Clemente Orozco y David Alfaro Siqueiros. <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfonsoLHerrera.php">Alfonso Herrera </a>, junto con Aurelio del Río, Marcos E. Becerra y José A. Durán a través de la Sociedad de Estudios Biológicos impulsaron esta iniciativa.</p>
</p>En 1945, durante el gobierno de Manuel Ávila Camacho, el zoológico fue nombrado "Alfonso L. Herrera". Durante la década de los 50´s el zoológico tuvo un resurgimiento con el regente de la ciudad, el sonorense Ernesto Uruchurtu Peralta (1906-1997), quien abarcó tres periodos presidenciales. El zoológico adquirió nuevos animales para su colección y al mismo tiempo adquirió a una de las personas que más influenció el zoológico de Chapultepec y muchos otros de México: el suizo Jean F. Schoch Beachler (1928-2012), mejor conocido como "Johnny". Johnny inició su trabajo en el Zoológico de Chapultepec en 1952 y lo continuó por más de 50 años hasta 2009 cuando falleció. Durante los años 70s fue director de los tres zoológicos de esa época: Chapultepec, San Juan de Aragón y del Parque Peña Pobre. </p>

</p>Iniciando con la llegada del primer Panda "Chia Chia" en 1972, durante el gobierno de Luis Echeverría Álvarez, el zoológico de Chapultepec se distinguió por ser la primer institución fuera de China que pudo reproducir al Panda y de ahí siguió la descendencia de un gran linaje de pandas: Ying Ying, Pe Pe, Xen Li, Tohui, Liang Liang, Xin Hua, Shuan Shuan, Xin Xin. El Panda ha pasado a ser un símbolo de la conservación mundial. Gracias a muchos esfuerzos recientemente pasó de estar En Peligro de Extinción a la categoría de Vulnerable. 

<p>Además del <a href="https://www.naturalista.mx/projects/zoologico-de-chapultepec-alfonso-l-herrera-cdmx">Zoológico de Chapultepec, </a> actualmente los zoológicos de la Ciudad de México están integrados por el <a href="https://www.naturalista.mx/projects/bosque-de-san-juan-de-aragon-cdmx">Zoológico del Bosque de San Juan de Aragón, </a> inaugurado en 1964 en la Delegación Gustavo A. Madero y el <a href="https://www.naturalista.mx/projects/zoologico-los-coyotes-cdmx">Zoológico Los Coyotes </a>en la Delegación Coyoacán que inicia en 1999.

<p>Entre los directores recientes del Zoológico se encuentran:

<li>1982-1997. María Elena Hoyos</li>
<li>1997-2000. Juan Garza Ramos, Médico Veterinario Zootecnista </li>
<li>2000-2010. Fernando Gual Sill, Médico Veterinario Zootecnista </li> 
<li>2010-2015. José Bernal Stoppen, Médico Veterinario Zootecnista </li>
<li>2015-2017. Juan Arturo Rivera Rebolledo, Médico Veterinario Zootecnista	</li>
<li>2017-2018. Claudia Lewy Sánchez Aldana, Médico Veterinario Zootecnista </li>
<li>2019-    . Fernando Gual Sill, Médico Veterinario Zootecnista </li>

</p>
<p>Además de la colección de animales que se encuentra en el <a href="http://www.naturalista.mx/places/zoologico-de-chapultepec"> Zoológico de Chapultepec </a>, este maravilloso espacio alberga una gran variedad de árboles, arbustos, aves y mariposas silvestres.</p>




