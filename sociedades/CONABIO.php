<p>La CONABIO se creó en 1992 un poco antes de la Cumbre de Río, en Brasil. Es una comisión intersecretarial que promuerve y reune información sobre biodiversidad. La CONABIO tiene la misión de promover, coordinar, apoyar y realizar actividades dirigidas al conocimiento de la diversidad biológica, así como a su conservación y uso sustentable para beneficio de la sociedad. Fue concebida como una organización de investigación aplicada, promotora de investigación básica, que compila y genera información sobre biodiversidad, desarrolla capacidades humanas en el área de informática de la biodiversidad y es fuente pública de información y conocimiento accesible para toda la sociedad.</p>
 
<p>Es una institución que genera inteligencia sobre nuestro capital natural; sirve de puente entre la academia, el gobierno y la sociedad; promueve que la conservación y manejo de la biodiversidad se base en acciones realizadas por la población local, la cual debe ser un actor central en ese proceso.</p>
 
<p>Entre las funciones principales de la CONABIO están instrumentar y operar el Sistema Nacional de Información sobre Biodiversidad (SNIB), como establece el artículo 80, fracción V de la Ley General del Equilibrio Ecológico y la Protección al Ambiente (LEGEEPA) para brindar datos, información y asesoría a diversos usuarios así como instrumentar las redes de información nacionales y mundiales sobre biodiversidad; dar cumplimiento a los compromisos internacionales en materia de biodiversidad adquiridos por México que se le asignen, y llevar a cabo acciones orientadas a la conservación y uso sustentable de la biodiversidad de México.</p>
