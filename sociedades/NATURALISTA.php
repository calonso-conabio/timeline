<p>NaturaLista es un espacio para registrar y organizar observaciones de la naturaleza, para conocer a otros entusiastas y para aprender sobre la naturaleza de México y el mundo. Este espacio promueve la participación de una amplia variedad de entusiastas de la naturaleza como excursionistas, observadores de aves, colectores de hongos, guías turísticos, scouts, alumnos, maestros, pescadores, agricultores, biólogos y ecólogos.</p>
<p>El objetivo de es crear conciencia en las comunidades sobre la biodiversidad y promover la exploración de los ambientes locales.</p>
<p>La plataforma iNaturalist se inició como un proyecto de maestría en la Universidad de California en Berkeley en 2008. En 2011 se organizó como sociedad y empieza e expandirse. En 2012 se iniciaron las charlas entre iNaturalist y CONABIO y para finales de ese año se inició la colaboración. CONABIO hizo la traducción de la plataforma, incluyó los catálogos de especies mexicanas, nombres comunes, lista de especies en reisgo, lista de especies exóticas y delimitación de áreas protegidas. También participó en el desarrollo de las applicaciones móviles para IOS y Androide. 

