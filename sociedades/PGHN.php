												<h2>Primer Gabinete de Historia Natural</h2>
												<h3>1790</h3>
											</header>
<p>El primer Gabinete de Historia Natural se inauguró el 27 de abril de 1790 en la calle de los Plateros número 189. Antiguamente conocida como Calle de San Francisco, por el Convento Franciscano, cambió su nombre cuando en 1638 el virrey de Nueva España, ordenó que todos los plateros se ubicaran en esta calle. Actualmente se conoce como avenida Madero. En España se había creado el Real Gabinete de Historia Natural tan solo 20 años antes por orden del rey Carlos III.</p>
<p>El Gabinete era pequeño y los ejemplares de minerales, plantas y animales se organizaron en 24 estantes siguiendo el Sistema Natural del naturalista sueco <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXVIII/CarlVonLinne.php">Carl Von Linné.</a> Algunos ejemplares provenían de España y otros de Nueva España. </a>Aunque el Gabinete fue el resultado de la Real Expedición Botánica a la Nueva España (1787-1803) organizada por el médico aragonés <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXVIII/MartinSesse.php"> Martín de Sessé y Lacasta, </a>con los farmacéuticos Vicente Cervantes (1755-1829), Juan Diego del Castillo y Jaime Senseve (1750-1805) y el cirujano José Longinos Martínez (1756-1802). Sin embargo, hubo muchas diferencias entre Martín Sesse y José Longinos y este último lo organizó aprovechando las ausencias de Martín Sessé quien se ocupaba de las primeras expediciones. </p>
<p>A las expediciones que enriquecieron el Gabinete se incorporaron los jóvenes artistas de la Academia de San Carlos, Atanasio Echeverria y Vicente de la Cerda y el naturalista <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXVIII/JoseMMucino.php">José Mariano Mociño. </a> </p>
<p>Entre los varios viajes de la Real Expedición Botánica de la Nueva España, José Longinos y Jaime Senseve partieron hacia California en 1791 llegando hasta San Francisco y regresando a la Ciudad de México en 1794.  </p>

<p><strong>El Gabinete de Historia Natural </strong></p>
<p>Los 24 estantes tenían el siguiente contenido: 
“El 1 sirve de <strong>biblioteca,</strong> con especiales y costosos libros de Historia Natural, Química, Botánica, Física, Anatomía, Mineralogía, Matemática, etcétera. <br><br>
“El 2, 3, 4 y 5, con <strong>animales,</strong> en donde se podrá manejar y examinar aun los más feroces sin el menor recelo ni repugnancia: allí veremos la volubilidad de las aves reducida al más sosegado reposo para observar de cerca los más preciosos matices.<br><br>

Los pescados que con escamas surcan los mares, causaran admiración vistos de cerca. Y aun aquella república más despreciada por el hombre, los insectos digo, aquellos seres tan pequeños en quienes se necesita el microscopio para admirar más y más la sabiduría del Divino Hacedor se presentarán de manera que el más despreciable embelesará al hombre, obligándole a bendecir al Criador.<br><br>

“El 6, se compone de 20 cajones en figura de libros, que contiene el <strong>herbario o jardín seco </strong>según las 24 clases del sistema sexual de Linneo, y varias hojas y partes de vegetales desecadas para manifestar su organización y estructura interna; se completa este estante con otros 22 cajones en la misma forma según el sistema de Tournefort, todos en folio de marquilla.<br><br>

“El 7 y 8, con <strong>minerales de oro y plata,</strong> en donde se ven muchas piedras de estudio, unas admirables por lo exquisito y precioso, otras por la variedad de sus matices, accidentes y combinaciones, que son ciencia en otras tantas lecciones.<br><br>

“El 9, sigue con iguales piedras de los <strong>minerales de cobre, hierro, estaño, plomo y azogue.</strong><br><br>

“El 10, con <strong>semimetales,</strong> marcasitas, piritas, azufres, mármoles, ágatas, etcétera.<br><br>

“En el 11 y 12 están las <strong>sales, piedras preciosas, </strong>cuarzos, espatos, estalactitas, guijarros jaspeados, etcétera.<br><br>

“En el 13, 14, 15 y 16, lo perteneciente al <strong>reino vegetal, </strong>como, resinas, semillas, gomas, bálsamos, maderas, cortezas, raíces, etcétera.<br><br>

“El 17, con <strong>petrificaciones y osamentas de elefantes </strong>encontrados en varios parajes del reino. Con estos fragmentos, bien examinados, se aclaran las dudas y disputas de los padres Torrubia y Betancourt, que, en el Aparato a la Historia Natural de este reino y Teatro Mexicano, hacen mención de dichas osamentas.<br><br>

“En el 18, producciones de <strong>volcanes.</strong><br><br>

“En el 19, <strong>tierras y antigüedades.</strong><br><br>

“En el 20 y 21 producciones de <strong>mar, </strong>como testáceos, crustáceos, madréporas, lythophytos, zoophytos, corales, coralinas, etcétera.<br><br>

“En el 22, 23 y 24, varias piezas de <strong>anatomía naturales y de cera; </strong>éstas sacadas por los originales de las que mostraron los profesores del gran establecimiento del Real Colegio de Cirujía de Madrid como primer fruto de sus tareas, que vio Su Majestad con agrado y complacencia. Igualmente contiene varias maquinas de Física y Química, como microscopios, óptica, cámara obscura, maquina eléctrica, piedra de imán, prismas, barómetros, termómetros, matraces, recipientes, retortas, etcétera”.
<p>En 1802 el Gabinete fue trasladado al <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENP.php">Antiguo Colegio de San Ildefonso </a>y más tarde a la Universidad Pontificia. 


