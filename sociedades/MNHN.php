<p>José Miguel Ramón Adaucto Fernández y Felix, mejor conocido como el General Guadalupe Victoria (1786-1843), fundó el Museo Nacional el 18 de marzo de 1825 en los salones de la Real y Pontificia Universidad de México. Cuarenta años más tarde, en 1865, el Emperador Maximiliano de Habsburgo (1832-1867) creó el <b>Museo Público de Historia Natural,  Arqueología e Historia </b>en la que fue la antigua Casa Nacional de Moneda en la calle de Moneda. Este edificio se construyó en 1570, sobre "las casas nuevas" o palacio de <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXV_anteriores/Moctezuma.php">Moctezuma Xocoyotzin </a>(1466-1520), noveno Huey Tlatoani mexica. </p>
<p>El Museo se inauguró el 6 de julio de 1866, en el cumpleaños del emperador. El monje cisterciense  y naturalista Dominik Bilimek (Adolf Josef) nacido en el imperio austríaco (1813-1884) estuvo a cargo de la sección de historia natural mientras que el reconocido egiptólogo Leo Simon Reinisch (1832-1919) fue el responsable de la sección de arqueología. El Historiador Manuel Orozco y Berra (1816-1881) fue el responsable del Museo. Ahi se reunieron piezas de la Real Pontifica Universidad de México y del Museo Nacional. El museo se clausuró en 1867 aún con las piezas embaladas.</p>
<p>Con el regreso de Benito Juárez, se reanudan actividades y para 1870, la Antigua Casa de Moneda ya albergaba el Museo de Historia Natural, el Museo de Antigüedades y el Museo de Historia. Fue la sede de las primeras sesiones de la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural</a>, una de las primeras en su tipo en el mundo. </p> 
<p>En 1909 el Museo Nacional fue dividido en dos: los objetos históricos fueron trasladados al <b>Museo Nacional de Antropología </b>en el Castillo de Chapultepec y la colección de historia natural se ubicó en "El Palacio de Cristal" (Museo del Chopo) y se conoció como el <b>Museo Nacional de Historia Natural. </b>Este edificio de hierro, ubicado en la Colonia Santa María la Ribera, fue construido en Düsseldorf, Alemania con el destino de ser cuarto de máquinas de la Mina de la Buena Esperanza en Alemania. El museo se inauguró el 1 de diciembre de 1913. Aquí llegó la reproducción del legendario <i>Diplodocus carnegie </i> donado por la viuda de Andrew Carnegie en 1926. </p>
<p>En 1964 el museo fue cerrado y las colecciones se repartieron entre el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/MHN.php"> Museo de Historia Natural de Chapultepec </a>y el Museo de Geología del Instituto de Geología de la Universidad Nacional Autónoma de México.</p>

