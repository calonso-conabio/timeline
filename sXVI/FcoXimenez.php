												<h2>Francisco Ximenez de Quesada</h2>
												<h3>1550 – 1620</h3>
											</header>
<p>Nací en la Villa de Luna en el Reino de Aragón, en el oriente de España el 23 de noviembre de 1666. Mis padres fueron Martín Ximénez y Ana Espinael. Ingresé a la orden dominicana cuando era bastante joven. En 1688 cuando tenía 22 años llegué a la antigua Capitanía de Guatemala como parte de la comitiva del gobernador Jacinto de Barrios Leal, para completar mi noviciado en el convento de la Antigua. Me inicié como sacerdote en 1691 en el poblado de San Juan Sacatepéquez y en San Pedro de las Huertas en el sur de Guatemala, donde aprendí el cakchiquel y el quiché.</p>
<p>A los 34, en 1690 años, me mudé a la Ciudad Real (Chiapas) a seguir ejerciendo como sacerdote. </p>
<p>Traduje el maravilloso libro conocido como "Popol Vuh" entre 1701 y 1703. Incluí la traducción en el libro "Historia de la provincia de Santo Vicente de Chiapa y Guatemala". Sin embargo, mi traducción estuvo guardada por más de cien años en el Convento de Santo Domingo en Guatemala. Su primera publicación la hizo el explorador austriaco Karl von Scherzer en Viena. 
<p>En 1615 publiqué la obra "Cuatro libros de la naturaleza y virtudes de las plantas y animales de uso medicinal en la Nueva España" para la cual utilicé una copia del resumen del médico napolitano Nardo Antonio Recchi, quien editaba la obra de <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVI/FcoHToledo.php">Francisco Hernández. </a>También añadí mis observaciones personales.  </p>

<p>Escribí también la historia de la provincia de San Vicente de Chiapas y Guatemala que no se publicó hasta 200 años más tarde en 1929. Fallecí en la Antigua, Guatemala en 1620.</p>
                                          
