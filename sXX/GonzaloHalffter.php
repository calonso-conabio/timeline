										  <p>Mi investigación se centró en la taxonomía, biogeografía y comportamiento de los escarabajos rodacacas. Estos pequeños escarabajos, de no más de tres centímetros, también conocidos como peloteros, ruedan los excrementos de los mamíferos convirtiéndolos en pelotitas que entierran en su madriguera, ya sea para alimentarse o para criar a sus pequeños. El macho y la hembra colaboran para crear la pelotita y algunas especies tienen cuidado parental. Meidante su comportamiento alimenticio y reproductivo los escarabajos rodacacas, reciclan nutrientes al enterrar los excrementos.</p>
