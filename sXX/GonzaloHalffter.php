												<h2>Gonzalo Halffter Salas</h2>
												<h3>1932</h3>
											</header>
										  <p>Nací en España el xx de xx de 1932, fui hijo único. Mi padre se llamó Rodolfo Halffter Escriche y mi madre Emilia Salas Viu. En ambos lados de la familia hubo excelentes músicos. Mi padre fue músico compositor. Durante la Guerra Civil Española sufrimos el bombardeo en Madrid. Fui uno de los diez sobrevivientes de mi escuela. En 1939, al terminar la Guerra en España, mis papás se trasladaron a Francia y de ahí a México y nos nacionalizamos al siguiente año. Cuando llegué a México tenía 7 años. Desde los 12 años me gustaba leer sobre insectos y salir al campo a colectarlos.</p>
										  <p>.<br>
										    A los 17 años conocí a Violeta Marcet Ocaña en la Academia Hispano Mexicana. Ella tenía 15 años. Su familia de origen catalán, también llegó a México después de la Guerra Civil. Española, cuando ella tenía a 8 años de edad. Nos casamos en 1955 después de un noviazgo de seis años. Tuvimos tres hijos Ruy, Rodolfo Eugenio y María del Carmen.</p>
										  <p>En la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional (IPN) realicé la licenciatura en Biología (1961) como el doctorado (1966). Ahí me quedé a trabajar primro como profesor ayudante, más adelante como catedrático, Jefe de Departamento y Director de Graduados y de la Investigación Científica de la Escuela.</p>
										  <p>Mi investigación se centró en la taxonomía, biogeografía y comportamiento de los escarabajos rodacacas. Estos pequeños escarabajos, de no más de tres centímetros, también conocidos como peloteros, ruedan los excrementos de los mamíferos convirtiéndolos en pelotitas que entierran en su madriguera, ya sea para alimentarse o para criar a sus pequeños. El macho y la hembra colaboran para crear la pelotita y algunas especies tienen cuidado parental. Meidante su comportamiento alimenticio y reproductivo los escarabajos rodacacas, reciclan nutrientes al enterrar los excrementos.</p>
										  <p>En 1974 fui nombrado Director del Museo de Historia Natural en Chapultepec y en 1975 fundé el Instituto de Ecología, A.C. (INECOL) en el anexo del Museo. Eramos pocos, así que un poco apachurrados ahi cabíamos. Durante la década de los 70s y 80s promoví la creación de las Reservas de la Biosfera en México, empezando con Mapimí y Michilía en Durango y con Montes Azules en Chiapas.</p>
