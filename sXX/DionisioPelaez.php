												<h2>Dionisio Peláez Fernández</h2>
												<h3>1915 - 1998</h3>
											</header>
<p>Dionisio fue un entomólogo español que durante su estancia en México fue pionero en hacer investigación sobre insectos transmisores de enfermedades parasitarias y los mismos parásitos, así como en buscar su erradicación de la población humana.</p>
                                          <p>En Madrid, España, se formó como entomólogo (especialista en insectos); obtuvo la licenciatura en ciencias naturales en la Facultad de Ciencias de la Universidad Central de Madrid (1935) y años más tarde, tomó cursos de doctorado en la misma institución. Salió exiliado de su país natal por participar como soldado del ejército republicano durante la Guerra Civil Española y en 1939 llegó a México junto con otros científicos españoles. </p>
                                          <p>En nuestro país hizo estudios de doctorado en la Escuela Nacional de Ciencias Biológicas del IPN. Entró a trabajar como entomólogo en el Departamento de Salubridad Pública de México, adscrito a la Campaña Nacional contra la malaria y la oncocercosis en el estado de Chiapas. </p>
                                          <p>Años más tarde ingresó al laboratorio de Entomología de la Escuela Nacional de Ciencias Biológicas del IPN; ahí estudió una gran diversidad de grupos de insectos que transmiten la malaria, como chinches y moscas. También incursionó en el estudio de nematodos parásitos (un tipo de gusanos), de los cuales describió nuevas especies. Fue uno de los primeros en reportar los dos primeros casos de gnatostomiasis humana en México y América.</p>
                                          <p>Dionisio también se ocupó en compartir sus conocimientos: impartió numerosos cursos de entomología médica y de parasitología avanzada, también fue coautor de &ldquo;Nociones de Parasitología Médica y Parasitología Tropical&rdquo;, ampliamente utilizado como libro de texto.</p>
                                          <p>Fue coordinador general y director de montaje del Museo de Historia Natural de la Ciudad de México.  </p>
