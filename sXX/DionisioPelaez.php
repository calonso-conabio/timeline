												<h2>Dionisio Peláez Fernández</h2>
												<h3>1915 - 1998</h3>
											</header>
<p>Nací el 6 de julio de 1915 n Madrid, España. Me formé como entomólogo (especialista en insectos); obtuve la licenciatura en Ciencias Naturales en la Facultad de Ciencias de la Universidad Central de Madrid (1935) y años más tarde, tomé cursos de doctorado en la misma institución. Salí exiliado de mi país natal por participar como soldado del ejército republicano durante la Guerra Civil Española y en 1939 llegué a México junto con mis maestros Ignacio y Cándido Bolívar. También me dieron clase en España, Faustino Miranda y Enrique Rioja Lobianco a quienes más tarde reencontraría en México.</p>
<p>En México hice estudios de doctorado en la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional. Entré a trabajar como entomólogo en el Departamento de Salubridad Pública de México, adscrito a la Campaña Nacional contra la malaria y la oncocercosis en el estado de Chiapas.</p>
<p>En México me casé con Josefina Lluch Belda, hermana de <a href="DanielLluch.php">Daniel</a>.<br>
  Años más tarde en 1942 ingresé al laboratorio de Entomología de la Escuela Nacional de Ciencias Biológicas del IPN a trabajar con los doctores Cándido Bolívar y Alfons Dampf (1884-1948). Ahí estudié una gran diversidad de grupos de insectos que transmiten la malaria, como chinches y moscas. También incursioné en el estudio de nematodos parásitos (un tipo de gusanos), de los cuales describí nuevas especies. Fuí uno de los primeros en reportar los dos primeros casos de &quot;gnatostomiasis&quot; humana (enfermedad producida por nemátodos) en México y América.</p>
<p>También me ocupé en compartir mis conocimientos: impartí numerosos cursos de entomología médica y de parasitología avanzada, también fui coautor de “Nociones de Parasitología Médica y Parasitología Tropical”, ampliamente utilizado como libro de texto.</p>
<p>Fui coordinador general y director de montaje del Museo de Historia Natural de la Ciudad de México (1963-1964). Colaboré también con el Museo de Historia Natural de la Ciudad de Puebla. Fallecí el 29 de mayo de 1998.</p>
