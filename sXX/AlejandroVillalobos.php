												<h2><strong>Alejandro Villalobos Figueroa </strong></h2>
												<h3>1918 – 1982</h3>
											</header>
<p>Soy Alejandro Villalobos Figueroa, nací en Pochutla Oaxaca, que significa &ldquo;lugar de la ceiba&rdquo;,  cerca de la costa de Oaxaca del año 1918. Estudié biología en la Ciudad de México en la Casa del Lago en Chapultepec, la cual era el único centro de investigación biológica del país y predecesora del Instituto de Biología de la Universidad Nacional Autónoma de México (UNAM). Ahí me preparé en el área de la hidrobiología, que es el estudio de la vida en el agua. </p>
                                          <p>A los 20 años ingresé a la maestría con el biólogo marino Dr. Enrique Rioja Lo Bianco (1895-1963), uno de los científicos españoles que se refugió en México a causa de la Guerra Civil Española, contratado por Isaac Ochoterena (1885-1950).  Me titulé con el grado de Maestro en Ciencias en 1943, año en que nació el sorprendente volcán Paricutín en el estado de Michoacán. Desde entonces, me incorporé al Instituto de Biología de la UNAM donde trabajé 30 años, participando en el desarrollo del Laboratorio de Hidrobiología y el Departamento de Ciencias del Mar y Limnología. En 1974 se creó la Universidad Autónoma Metropolitana (UAM) para que los estudiantes tuvieran más oportunidades de estudiar. Desde su inicio trabajé con la UAM y desarrollé la carrera de Hidrobiología que se sigue impartiendo hasta la fecha. En esta institución fui jefe del Departamento de Zootecnia y del Área de Ecosistemas Acuáticos.</p>
                                          <p>En el Instituto de Biología de la UNAM está la colección más completa del país sobre cangrejos y camarones (crustáceos) marinos y dulce acuícolas, producto de las colectas que hice durante mis extraordinarias expediciones. En particular estudié a los langostinos de río de México (Cambaridae), también conocidos como camaroncitos reculadores, o acociles, del náhuatl, <em>atl,</em> y<em> cuitzilli, &ldquo;</em>que se retuerce en el agua&rdquo;. En México se han registrado 56 especies, de las cuales 55 son endémicas de México. A este grupo le dediqué mi tesis doctoral. </p>
                                          <p>Además como una contribución para el conocimiento del mundo y orgullo de México, publiqué más de 32 trabajos sobre crustáceos de los que descubrí nuevas especies para nuestro país. Promoví el establecimiento de vedas al camarón, langosta y tortuga marina para que sus poblaciones no fueran drásticamente afectadas y dirigí una investigación para conocer el efecto de la explotación de los pozos petroleros sobre los bancos de las ostras que se encuentran en la laguna de Tamiahua, en el norte de Veracruz.  Mi hijo José Luis Villalobos Hiriart siguió mis pasos y se dedica al estudio de los crustáceos en la UNAM.</p>
                                          <p>Y fue en esa laguna que en el año de 1982 me encontraba realizando mis investigaciones cuando un sorpresivo &ldquo;norte&rdquo; apareció ocasionando fuertes oleajes provocando que mi lancha volteara. A pesar de los esfuerzos de mi hijo, quien me acompañaba, nunca se pudo recuperar mi cuerpo.</p>
