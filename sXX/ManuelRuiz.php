												<h2>Manuel Ruiz Oronoz</h2>
												<h3>1909 - 1978</h3>
											</header>
<p>Nací en la ciudad de Chihuahua, el 12 de abril de 1909, lugar donde realicé mis estudios básicos y di clases de primaria. Más tarde me trasladé a la ciudad de México para ampliar mi formación académica y  especializarla, de manera que obtuve el grado de doctor en Ciencias biológicas otorgado por la Facultad de Ciencias de la UNAM (1942).</p>
<p>A partir de entonces, me dediqué a estudiar los hongos microscópicos responsables de la fermentación del agua miel, que después se convierte en pulque, así como  de la fermentación de varios frutos. Estos estudios motivaron a otros investigadores a seguir trabajando con esta tradicional bebida mexicana.</p>
<p>También me interesaba colectar hongos grandes, actividad que realicé en diferentes regiones del país. En mis salidas al campo, me agradaba platicar con los indígenas para que le contaran sobre cómo los usaban.</p>
<p>Me encantaba transmitir todos estos conocimientos a mis alumnos, así fuera en las clases a diferentes niveles de enseñanza que impartí en instituciones tan diversas como la UNAM, el IPN y la Escuela Normal Superior, o en excursiones al campo que hacía junto con mi gran amigo <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/FaustinoMiranda.php">Faustino Miranda. </a>De esos viajes salieron muchos estudiantes entusiasmados como <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/TeofiloHerrera.php">Teófilo Herrera </a>y <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/ArturoGomezP.php">Arturo Gómez Pompa, </a>Javier Valdés, <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/RamonRibaN.php">Ramon Riba y Nava Esparza </a>y Samuel Mariel. El primero, también se dedicó a los hongos, digo a su estudio. También me dediqué a escribir numerosos libros de texto junto con <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/EnriqueBeltran.php">Enrique Beltrán, </a><a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/TeofiloHerrera.php">Teófilo Herrera </a> y <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/MiguelAUlloa.php">Miguel Ulloa, </a>que fueron referencia obligada durante muchos años.</p>
<p>Trabajé por más de treinta años en el Instituto de Biología de la UNAM y participé en la fundación del Jardín Botánico de dicha institución. Ahi me tocó compartir con colegas como <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/HeliaBravo.php">Helia Bravo, </a><a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/CarlosCHoffmann.php">Carlos Hoffman, <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/IsaacOchoterena.php"> Isaac Ochoterena, </a><a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/RafaelMCampo.php">Rafael Martín del Campo, </a><a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/AmeliaSamano.php">Amelia Sámano, </a>entre otros muchos.</p>
<p>Fuí miembro de varias sociedades científicas, en particular, fui presidente de la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural.</a></p>
<p>Fuí pionero en el campo de la microbiología (la disciplina que estudia a los organismos que no se pueden ver a simple vista) y de los estudios etnomicológicos (los que exploran la relación de los pueblos indígenas con los hongos) en México. Fallecí el 19 de septiembre de 1978.</p>
