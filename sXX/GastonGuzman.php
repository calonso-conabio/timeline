												<h2><strong></strong>Gastón Guzmán Huerta</h2>
												<h3>1932 - 2016</h3>
											</header>
<p>Gastón Guzmán es pionero en el estudio de los hongos mexicanos, en especial de las especies alucinógenas. </p>
                                          <p>Es originario de Jalapa, Veracruz, pero se mudó a la capital del país para estudiar biología y el doctorado en ciencias en la Escuela Nacional de Ciencias Biológicas (ENCB) del IPN. </p>
                                          <p>Varios años de su vida trabajó como botánico explorador de varios laboratorios farmacéuticos, inventariando y colectando hongos alucinógenos y plantas medicinales. Hizo lo mismo para las investigaciones que realizó en distintas partes del país, de manera que hasta ahora ha recolectado y registrado miles de hongos de diferentes tipos y descrito más de 200 nuevas especies. </p>
                                          <p>Esta exhaustiva labor de campo lo llevó a fundar la Colección de Hongos del Herbario de la ENCB (1955), la más grande e importante en América Latina y la del Instituto de Ecología, en Veracruz, donde a la fecha es su curador. Asimismo, colaboró en la organización de las colecciones de varias universidades del país. </p>
                                          <p>Preocupado por difundir el conocimiento científico generado en su campo, participó en la fundación del ya desaparecido <em>Boletín de la Sociedad Mexicana de Micología</em> (1968) y de la Revista Mexicana de Micología (1985), aún vigente. Este interés le ha llevado a escribir poco más de una decena de libros y varios capítulos de libros, todos sobre hongos. </p>
                                          <p>Es cofundador y exdirector de la Sociedad Mexicana de Micología y exdirector de la Asociación Latinoamericana de Micología.</p>
                                          <p>Gastón Guzmán ha promovido el consumo de los hongos a través de sus publicaciones que destacan sus beneficios nutricionales y su gran valor para la industria. </p>
                                          <p>La docencia ha sido otra de sus ocupaciones por lo que se ha convertido en un gran impulsor de la formación de varias generaciones de especialistas en este campo. </p>
