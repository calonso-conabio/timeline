												<h2>Francisco González Medrano</h2>
												<h3>1939 - 2017</h3>
											</header>
<p>Nací en la Ciudad de México el 13 de enero de 1939. En 1960 entré a estudiar biología en la Facultad de Ciencias de la Universidad Nacional Autónoma de México. Mi tésis de licenciatura fue sobre la vegetación de Tamaulipas y desde entonces quedé enamorado de ese estado. Al finalizar la licenciatura empecé a trabajar como técnico en el Jardin Botánico del Instituto de Biología de la UNAM con el maestro Javier Valdés y para 1966 me incié como Investigador de Tiempo Completo en el Instituto.</p>
<p>Me casé con la bióloga Margarita Hernández, a quien conocí en la facultad de ciencias. Ella dedicó gran parte de su trabajo a escribir libros de biología para bachillerato. A nuestra hija la bautizamos con el nombre de Helietta, en honor a un arbolito conocido como Barreta <a href="http://www.enciclovida.mx/especies/6063378"><i>Helietta parviflora.</i> </a> Y a mi esposa le dediqué una nueva especie endémica de Nuevo León, conocida como Flor de Cal <a href="http://www.enciclovida.mx/especies/6028288"><i> Franchenia margaritae. </i></a></p>
<p>Más tarde el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/ArturoGomezP.php">Dr. Arturo Gómez Pompa </a>me invitó a regresar a Tamaulipas para trabajar sobre los impactos de la Presa Las Adjuntas sobre la naturaleza de ese estado, uno de los primeros trabajos sobre impacto ambiental en México.</p>
<p>Fuí presidente de la Sociedad Botánica de México en 1978 y 1979 y asesor de muchas instituciones de gobierno como el Consejo Nacional de Ciencia y Tecnología (CONACYT), la Secretaría del Medio Ambiente y Recursos Naturales (SEMARNAT), y la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/CONABIO.php">Comisión Nacional para el Conocimiento y Uso de la Biodiversidad, </a>entre otras.</p>
<p>También dediqué gran parte de mi vida a impartir cursos, seminarios, talleres, charlas de divulgación en una gran diversidad de instituciones como la Universidad Autónoma Metropolitana, la Universida Nacional Autónoma de México, la Universidad Autónoma del Estado de Morelos. Tuve una gran cantidad de estudiantes que ahora se han convertido en exitosos botánicos. Me gustaba que en los viajes de campo, los estudiantes conocieran realmente a las plantas con todos los sentidos, observándolas, oliéndolas, tocándolas, y hasta probándolas con cuidado. A ellos les gustaba llamarme el "Maestro Pancho Medrano"</p>
<p>En 2003 publiqué "Las Comunidades Vegetales de México" donde sinteticé toda mi experiencia recorriendo el país y colectando alrededor de 22 mil especímenes de plantas. El <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/JerzyRzedowski.php">Dr. Jerzi Rezdowski </a>me dedicó una especie nueva de cuajiote de Hidalgo <a href="http://www.enciclovida.mx/especies/6019089"> <i>(Bursera medranoana)</i> </a>. Durante el último año de vida participe con la Comisión Nacional para el Conocimiento y Uso de la Biodiversidad (CONABIO) como curador de la plataforma <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/NATURALISTA.php">NaturaLista. </a>Fallecí el 12 de enero de 2017</p>
