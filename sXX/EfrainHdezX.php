												<h2>Efraim Hernández Xolocotzi</h2>
												<h3>1913 - 1991</h3>
											</header>
<p>Efraim Hernández Xolocotzi Guzmán fue un estudioso de los sistemas agrícolas de México. Se enfocó sobre todo en el estudio del maíz, y fue un destacado promotor del resurgimiento de la investigación etnobotánica en este país. Nació en Tlaxcala en 1913 y falleció en 1991.</p>
                                          <p>Desde pequeño se mudó con su familia a Estados Unidos donde cursó los estudios de educación básica. Su formación profesional la realizó en el Colegio Estatal de Agricultura N.Y., posteriormente en la Escuela de Agricultura de la Universidad de Cornell N.Y. y la maestría en artes con especialidad  en Biología en la Universidad de Harvard.</p>
                                          <p>A su regreso a México trabajó en el Banco Nacional de Crédito Ejidal en el estado de Tabasco. A partir de ese momento se interesó por los problemas que había en el agro mexicano, a través de sus recorridos a distintos lugares del país y el trato con los campesinos.</p>
                                          <p>A principios de la década de los cuarenta, fue Asesor Técnico para la Administración Económica Extranjera de la Embajada Americana, donde trabajó para aumentar la producción de semillas oleaginosas de México. Por estos años se dedicó además a recolectar las razas de maíz existentes en México. Gran parte de este material fue destinado a la fundación Rockefeller, que fue la principal responsable de formar las colecciones del Centro Internacional de Mejoramiento de Maíz y Trigo (CIMMYT).</p>
                                          <p>Durante más de cuarenta años dedicó su vida a la enseñanza agrícola y etnobotánica, así como a las investigaciones sobre plantas útiles de América Latina y México. Trabajó en el Centro de Botánica del Colegio de Posgraduados, la Escuela Nacional de Agricultura en Chapingo y la Universidad Autónoma de Chapingo (UACH).</p>
                                          <p>Escribió alrededor de 200 artículos científicos y seis libros. Su obra escrita más relevante ha sido compilada en la colección <em>Xolocotzia</em> editada por la UACH.                                            </p>
