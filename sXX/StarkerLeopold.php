												<h2>Starker Leopold</h2>
												<h3>1913 - 1983</h3>
											</header>
											<p>Nací el 22 de octubre de 1913 en Burlington, Iowa, a la orilla del gran río Misisipi. En ese mismo poblado había nacido mi padre. Fuí el primer hijo de los cinco que formamos la familia Leopold. Mi padre fue <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AldoLeopold.php">Aldo Leopold</a>, forestal y conservacionista y mi madre Estella Bergere quien fue maestra. Tuvimos una fuerte influencia de nuestros padres en el interés por la vida silvestre ya que tanto yo como mis hermanos Luna, Carl, Estella y Nina nos dedicamos a las carreras científicas y a contribuir a la conservación de la naturaleza.</p>
											<p>La juventud la pasé en Nuevo México, la tierra de mi madre y su familia desde 1600. Mi padre la conoció ahí mientras trabajaba para el Servicio Forestal de Estados Unidos. Me encantaba salir a excursionar en los paisajes del suroeste y cazar patos en el Río Grande. Cuando cumplí 11 años nos mudamos a Madison, Wisconsin, situada entre los lagos Mendota y Monona. Ahí estudié la licenciatura en agricultura en 1936 y más tarde me fui a la Escuela Forestal de Yale. </p>
											<p>En 1937 acompañé a mi papá Aldo y a mi tio Carl a una gran excursión al Río Gavilán en Chihuahua, México. Era uno de los lugares favoritos de mi padre. Me platicaba que ahí realmente se podía observar un ecosistema saludable. No había gente y aun se conservaban los grandes depredadores como el lobo mexicano y el puma. Regresé una segunda vez, once años más tarde, en el verano de 1948. Esta vez fui acompañado de la gente del Museo de Zoología de Vertebrados de la Universidad de California en Berkeley y pasamos 45 días en la región. Desafortunadamente, esta vez encontramos que el paraíso estaba perdido. Había campamentos madereros, camiones con árboles talados y cientos de vacas pastoreando la región. El río también se había deteriorado. En esa fecha aún había lobos y pumas, pero los osos negros casi habían desaparecido. Los cambios eran muy drásticos. A pesar de la decepción regresé en 1952, y en el área había taladores y ganado por todos lados. Los guajolotes aún eran abundantes, pero los venados ya eran muy escasos. </p>
											<p>En la Universidad de Berkeley, California, mientras estudiaba el doctorado, me casé con la pintora Elizabeth Wiskotten en 1938 y tuvimos un par de hijos, Frederic y Sarah. Recibí el doctorado de la Universidad de California en 1944. Mis asesores fueron los famosos ornitólogos Alden H. Miller y Joseph Grinnell. Más tarde me contrató la Universidad para trabajar en el Museo de Vertebrados, del cual me convertí en director con el tiempo. </p>
											<p>En 1952 junto con el ecólogo y ornitólogo inglés Frank Fraser Darling (1903-1979) de la Universidad de Endimburgo, en Escocia, hicimos una investigación de cuatro meses sobre los impactos del crecimiento económico en Alaska.</p>
											<p>En 1963 escribí un reporte sobre Manejo de Vida Sivestre en Parques Nacionales con recomendaciones sobre el manejo de ecosistemas que se conoce como "El reporte Leopold". El reporte fue muy controversial, pero ahí se especifican los primeros planes para manejar tanto a los ecosistemas como a los visitantes. En 1967 me pasé al Departamento de Manejo de Bosques y Recursos Silvestres de la misma Universidad como Profesor de Zoología y Bosques.</p>
											<p>	A pesar de no ser botánico, escribí una de las primeras descripciones sobre la vegetación de México en 1950. Sin embargo, mi "obra magna" fue <b>"Fauna Silvestre de México: Las aves y mamíferos de caza" </b> publicada en 1959. Este libro fue el resultado de 15 años de trabajo de campo que inicié en 1944. Hice varias expediciones a México, para registrar la ecología e historia natural en 51 localidades desde Sonora hasta Yucatán. El biólogo y artista Charles W. Schwartz (1914-1991) hizo unas magníficas ilustraciones de la fauna. El libro lo tradujo Luis Macías Arellano, Director General Forestal y de Caza de la Secretaría de Agricultura y Ganadería y Ambrosio Gonzalez Cortés, Secretario Perpetuo de la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural </a>en 1965 y el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/EnriqueBeltran.php"> Dr. Enrique Beltrán </a> escribió el prólogo. Ha sido un verdadero clásico entre los biólogos mexicanos.</p>
											<p>Siempre tuve mis binoculares a la mano para poder observar a la naturaleza y la pesca y la caza fueron mis grandes aficiones toda la vida. Al igual que mi padre, fallecí de un ataque cardíaco a los 70 años el 23 de agosto de 1983.</p> 
