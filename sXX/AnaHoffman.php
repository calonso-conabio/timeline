												<h2>Ana Hoffmann Mendizábal</h2>
												<h3>1919 - 2007</h3>
											</header>
<p>Ana Hoffman Mendizábal fue una destacada bióloga, pionera en el estudio de los ácaros y fundadora de la acarología en México, disciplina inexistente en el país hasta 1940. Nació en Puebla en 1919 y murió en la ciudad de México en 2007.</p>
                                          <p>Realizó sus estudios de licenciatura, maestría y doctorado en Biología en la Facultad de Ciencias de la Universidad Nacional Autónoma de México (UNAM). En 1948 realizó estudios de especialización en acarología, en la Universidad de Duke y en el Museo Smithsoniano en Estados Unidos.</p>
                                          <p>Además de dedicarse a la investigación, impartió clases sobre este grupo de pequeños artrópodos, así como de bioespeleología, en el Instituto Politécnico Nacional (IPN) y en la UNAM. Fundó tres laboratorios para el estudio de estos organismos, uno en la Escuela Nacional de Ciencias Biológicas del IPN, el primero en América Latina, otro en la Facultad de Ciencias de la UNAM, y el tercero en el Instituto de Biología, perteneciente a la misma institución. Además, a este último donó su colección de ácaros y su hemeroteca personal, con lo que contribuyó a la creación de la Colección Acarológica Nacional (CNAC), la más importante en el país hasta ahora.</p>
                                          <p>Escribió alrededor de 130 artículos sobre ácaros y fue autora de 10 libros, entre los que se encuentran <em>Manual de Bioespeleología</em> y <em>Los Ácaros</em>.</p>
