												<h2>Ana Hoffmann Mendizábal</h2>
												<h3>1919 - 2007</h3>
											</header>
											<p>Nací en la ciudad de Puebla el 3 de marzo de 1919. Me padre fue <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/CarlosCHoffmann.php">Carlos Cristian Hoffman </a>(1876-1942). Desde pequeña salía con él al campo a colectar insectos. Así que crecí en un mundo de interesados de las plantas y animales. Realicé mis estudios de licenciatura, maestría y doctorado en Biología en la Facultad de Ciencias de la Universidad Nacional Autónoma de México (UNAM). A muchos de mis profesores como <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/RafaelMCampo.php">Rafael Martín del Campo, </a><a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/HeliaBravo.php">Helia Bravo, </a><a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/IsaacOchoterena.php">Isaac Ochoterena </a>los había conocido en las excursiones con papá. Fui parte de la primera generación de estudiantes de la Facultad de Ciencias.</p>
											<p>En 1948 realicé estudios de especialización en acarología, en la Universidad de Duke y en el Museo Smithsoniano en Estados Unidos.</p>
                                          <p>Además de dedicarme a la investigación, impartí clases sobre este grupo de pequeños artrópodos, así como de bioespeleología, en el Instituto Politécnico Nacional (IPN) y en la UNAM. Fundé tres laboratorios para el estudio de estos organismos, uno en la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENCB.php">Escuela Nacional de Ciencias Biológicas </a>del IPN, el primero en América Latina, otro en la Facultad de Ciencias de la UNAM, y el tercero en el Instituto de Biología, perteneciente a la misma institución. Además, a este último doné su colección de ácaros y su hemeroteca personal, con lo que contribuí a la creación de la Colección Acarológica Nacional (CNAC), la más importante en el país hasta ahora.</p>
                                          <p>Escribí alrededor de 130 artículos sobre ácaros y fuí autora de 10 libros, entre los que se encuentran <em>Manual de Bioespeleología</em>, <em>Los Ácaros</em> y <em> El Maravilloso Mundo de los Arácnidos</em> dedicado este último a mi padre y a mi hijo Claudio. Fallecí en la ciudad de México el 11 de octubre de 2007.</p>
