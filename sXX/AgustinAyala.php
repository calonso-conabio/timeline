												<h3>1925 - 2005</h3>
											</header>
											<p>Nací muy cerca del mar, en el puerto de Mazatlán, Sinaloa, y aunque desde muy pequeño nos mudamos a la Ciudad de México mi vida siempre estuvo estrechamente ligada al mar. </p>
                                          <p>Mi pasión por las ciencias del mar comenzó cuando concluí la carrera de biología en la Facultad de Ciencias de la UNAM (1950) pues casi de inmediato fui invitado a trabajar en PEMEX, donde me dediqué a estudiar un grupo de microorganismos llamados foraminíferos, que viven en los sedimentos del fondo marino, muchos de interés petrolero. Mi tésis de licenciatura la dirigió el Dr. Eduardo Caballero y Caballero y fue publicada por la Asociación de Mexicana de Geólogos Petroleros.</p>
                                          <p>Años más tarde me fuí a Estados Unidos para hacer la maestría en Geología y Micropaleontología en la Universidad de Stanford y a tomar cursos especializados en la Universidad de California. En ese periodo, seguí estudiando los foramníferos, pero ahora de varias regiones del mundo. A mi regreso, hice el doctorado en Biología en la Facultad de Ciencias de la UNAM. </p>
                                          <p>En 1957 me casé con Alma Irma López Hernández, maestra normalista, quien fue mi alumna en las clases de Paleontología en la Escuela Norma Superior. Tuvimos tres hijos: Agustín, Alma Irma y Adriana Lilia. </p>
                                          <p>Yo era algo serio, hiperactivo, de ágil memoria, comprometido y tenía una capacidad de organización extraordinaria, características que me llevaron a ocupar distintos cargos administrativos, nacionales como internacionales, desde donde mejoré la infraestructura y las condiciones para la investigación científica en distintas partes del país.</p>
                                          <p>Tuve muchos colegas con los que trabaje como <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/MariaElenaCaso.php">María Elena Caso Muñoz,</a> <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/GloriaAlencaster.php">Gloria Alencaster Ibarra </a> y Eucario López Ochoterena. Me dio clases el <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/EnriqueRioja.php">Dr. Enrique Rioja Lo Bianco.</a> Y mi amigo el <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/JoseAlvarez.php">Dr. Álvarez del Villar,</a> me invitó a trabajar en la Comisión de Piscicultura Rural de la Secretaría de Marina </p>  
                                          <p>En la UNAM fuí director del Instituto de Biología y del Instituto de Ciencias del Mar y Limnología, así como coordinador de la Investigación Científica. En la UNESCO, representé a México en distintos foros internacionales, algunos de los cuales, condujeron a la aprobación de la Convención del Derecho del Mar, un instrumento jurídico de la mayor importancia para la comunidad internacional de los océanos. </p>
                                          <p>Fuí un convencido de que el mar es vital para el desarrollo del país debido a sus muchísimos recursos, por eso me dedique a impulsar mi investigación, a formar profesionales en la materia en instituciones como la SEP, el IPN y la UNAM, así como promover estrategias de administración racional. </p>
