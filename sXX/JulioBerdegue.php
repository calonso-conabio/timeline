												<h2>Julio Berdegué Aznar</h2>
												<h3>1931 - 2007</h3>
											</header>
<p>Nací en Madrid, España el 14 de abril de 1931. Mi padre Julio Berdegué Valdor nos sacó a mi madre, a mi hermana Mercedes y a mi hacia el exilio en México. Llegamos en la bodega del barco Flandre en 1939. En la Ciudad de México, atendí al Colegio Español y a la Academia Hispanoamericana. Entre a la carrera de biología en la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional.</p>
<p>Mis primeras investigaciones fueron sobre los peces de importancia comercial del Pacífico y recorrí ampliamente las costas de Sonora, Baja California y Sinaloa. en 1954 cursé la Maestría en Biología Marina en la Universidad de California.</p>
<p>Conoci a la española Dolores Sacristán Roy, nos casamos y fuimos a trabajar a San Diego, California en la Comisión Internacional del Atún. Tuvimos cuatro varones: Julio, Carlos, Fernando y Mariano. Más adelante regresamos a vivir a Escuinapa de Hidalgo, un pueblito al sur de Sinaloa, cerca de Nayarit. Ahi en Escuinapa aún se practicaba el Juego prehispánico de Ulama. En Escuinapa pasamos seis años y de ahi nos fuimos a vivir a Mazatlán.</p>
<p>En 1972 inauguré El Cid, un campo de golf y un Country Culb en Mazatlán. Más tarde inicié el desarrollo de el Hotel El Cid Granada y seguimos creciendo hasta tener varios hoteles, incluso en el Caribe. En 1996 fui reconocido como &quot;El Empresario Hotelero más Prominente de México&quot;. Tuve excelentes relaciones en Mazatlán con toda clase de fauna silvestre: políticos, empresarios, funcionarios, comerciantes, agricultores, ganaderos, empleados, familiares y amigos. Todos ellos me conocían como &quot;Don Julio&quot;. Fallecí el 21 de abril de 2007 en mi querido Mazatlán.</p>
