												<h2>José Aristeo Sarukhán Kermez</h2>
												<h3>1940</h3>
											</header>
<p>Nací en la Ciudad de México el 15 de julio de 1940. Mis padres Harutiun Sarukhanian y Ángela Kermez llegaron a México en los años 20s de Turquía y Armenia, con miras de establecerse más tarde en Estados Unidos. Descendientes de un país con una larga historia y una rica cultura, emigraron a México como sobrevivientes del gran genocidio ocurrido contra los armenios en Turquía en las primeras décadas del siglo XX. Mi padre murió cuando mi hermana Tere y yo eramos adolescentes.</p> 
<p>Estudié la carrera de Biología en la Facultad de Ciencias de la Universidad Nacional Autónoma de México (UNAM). Mis principales maestros fueron <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXX/ManuelRuiz.php">Manuel Ruiz Oronoz,</a> <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXX/AmeliaSamano.php">Amelia Sámano,</a> <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXX/FaustinoMiranda.php">Faustino Miranda,</a> <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXIX/EnriqueRioja.php">Enrique Rioja,</a> entre muchos otros. Mi tésis la hice sobre el fenómeno ecológico de la sucesión en las selvas de Tuxtepec, en la cuenca del río Papaloapan en Oaxaca.</p>
<p>Durante la licenciatura conocí a Adelaida Casamitjana Vives, hija de refugiados españoles de la Guerra Civil quien también estudiaba biología. Nos casamos en 1962 y tuvimos dos hijos: Arturo y Adelaida.</p> 
<p>Realicé la Maestría en Botánica Agrícola en el Colegio de Posgraduados de Chapingo en 1968 con mi apreciado Maestro <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXX/EfrainHdezX.php">Efraím Hernández Xolocotzi.</a>  Mi tésis de maestría se trató de un análisis de las selvas húmedas. En ese año publiqué junto con Terence Pennington del Jardin Botánico de Kew, el libro "Árboles Tropicales de México", el cual ha tenido varias ediciones. Finalmente obtuvé el Doctorado en Ecología por la Universidad de Gales, Gran Bretaña, en 1972 con el Dr. John L. Harper. En el doctorado me concentré en los estudios de poblaciones de plantas.</p>
<p>A los 39 años fui elegido imprevistamente como Director del Instituto de Biología de la UNAM en donde ejercí el cargo por 8 años (1979-1985), creando hacia el final del periodo el Departamento de Ecología (1985), que evolucionó a convertirse en el Centro de Ecología (1988) y más tarde en el Instituto de Ecología (1996). En 1985 recibí el premio de la Academia de la Investigación Científica y mi segundo periodo a cargo del Instituto fue abreviado por la invitación del Dr. Jorge Carpizo McGregor para ser Coordinador de la Investigación Científica por un par de años (1987-1988), cuando también fui elegido para ser miembro de El Colegio Nacional (1987).</p>
<p>Mi vida laboral ha sido amplia, trabajé como profesor, investigador, divulgador de la ciencia y director de numerosas instituciones académicas y educativas. Mi trabajo científico se enfocó a la ecología de poblaciones de plantas, el estudio y conservación de la biodiversidad y la restauración ecológica. En 1990 recibí el Premio Nacional de Ciencias y Artes en el área de Ciencias Físico-matemáticas y Naturales.</p>
<p>Fui presidente de la Sociedad Botánica de México en 1972 y de la Academia de la Investigación Científica y soy actual miembro de muchas asociaciones nacionales y extranjeras, entre ellas, la U.S. National Academy of Sciences (1993) y la Royal Society of London (2003).</p>
<p>Estuve a cargo de la rectoría de la máxima casa de estudios, la UNAM, por dos periodos, abarcando desde 1989 hasta 1996. Desde 1992 he sido coordinador nacional de la Comisión Nacional para el Conocimiento y Uso de la Biodiversidad (CONABIO) a partir de su creación por decreto presidencial como propuesta de México a la Cumbre de la Tierra en Brasil.</p>
<p>He recibido muchas distinciones por mi contribución a la conservación de la naturaleza, entre la que destacan la medalla “John C. Phillips”, otorgada en 2008 por la Unión Internacional para la Conservación de la Naturaleza y los Recursos Naturales y recientemente el reconocimiento "Campeón de la Tierra" por las Naciones Unidas.</p>
