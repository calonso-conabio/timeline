												<h2>José Sarukhán Kermez</h2>
												<h3>1940</h3>
											</header>
<p>José Sarukhán Kermez es un activo biólogo, botánico y ecólogo que nació en la Ciudad de México en 1940. Estudió la carrera de Biología en la Facultad de Ciencias de la Universidad Nacional Autónoma de México (UNAM); realizó la Maestría en Botánica Agrícola en el Colegio de Posgraduados de Chapingo en 1968 y finalmente obtuvo el Doctorado en Ecología por  la Universidad de Gales, Gran Bretaña, en 1972.</p>
                                          <p>Su vida laboral ha sido muy amplia, desempeñándose como profesor, investigador, divulgador de la ciencia y director de numerosas instituciones académicas y educativas. Su trabajo se ha enfocado sobre la ecología de poblaciones de plantas, el estudio y conservación de la biodiversidad y la restauración ecológica, del cual se han derivado seis libros y alrededor de 180 escritos, entre revistas, boletines, memorias, capítulos de libros y otros tipos de publicaciones tanto nacionales como extranjeras. </p>
                                          <p>Fue presidente de la Sociedad Botánica de México en 1972 y de la Academia de la Investigación Científica, asimismo es actual miembro de muchas asociaciones nacionales y extranjeras. Estuvo a cargo de la rectoría de la UNAM por dos periodos, abarcando desde 1989 hasta 1996. Funge como coordinador nacional de la Comisión Nacional para el Conocimiento y Uso de la Biodiversidad (CONABIO) a partir de la creación en 1992 por decreto presidencial.</p>
                                          <p>Ha recibido decenas de distinciones por su contribución a la conservación de la naturaleza, entre la que destaca la medalla &ldquo;John C. Philips&rdquo;, otorgada en 2008 por la Unión Internacional para la Conservación de la Naturaleza y los Recursos Naturales.</p>
