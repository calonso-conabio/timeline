												<h2>Rafael Martín del Campo</h2>
												<h3>1910 - 1987</h3>
											</header>
<p>Nací el 3 de enero de 1910 en la ciudad de Guadalajara, Jalisco. Cursé el bachillerato en la <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENP.php">Escuela Nacional Preparatoria </a>. Más tarde, inicié la Maestría en Ciencias Biológicas en la Facultad de Filosofía y Letras de la Universidad Nacional Autónoma de México (UNAM) bajo la dirección del maestro <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/IsaacOchoterena.php">Isaac Ochoterena.</a></p>
<p>En el campo zoológico me especialicé como herpetólogo y ornitólogo aunque también hice aportaciones en el campo de la etnobiología, así como en el de la Historia de la Biología. A través de las leyendas y tradiciones contenidas en los códices y escritos de los aztecas en la época de la Conquista, estudié el conocimiento que poseían los antiguos mexicanos sobre la fauna de aquel entonces. </p>
<p>Yo consideraba que "...Es un deber de los mexicanos de hoy revisar y revalorar la civilización y la cultura del antiguo México indígena, tan calumniadas por los españoles que invadieron y sometieron a la fuerza a un país al que no comprendieron ni esttudiaron ni supieron entender..."</p>
<p>Algunos de mis trabajos son Los batracios y reptiles según los códices y relatos de los antiguos mexicanos (1937), Aves en la historia antigua de México (1948) y La Helmintología en el México antiguo (1977).</p>
<p>Trabajé como investigador del Instituto de Biología de la UNAM, en donde además contribuí a engrandecer y sistematizar la Colección Ornitológica que previamente había albergado el <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/MNHN.php">Museo Nacional de Historia Natural. </a>En 1939 fui profesor fundador de la Facultad de Ciencias en dicha casa de estudios, impartiendo los cursos de Zoología IV, Historia de las Ciencias Biológicas y Raíces de lenguas indígenas. Entre mis estudiantes que continuaron con la herpetología están Gustavo Casas Andreu, Oscar Flores-Villela "Archie" y Zeferino Uribe Peña </p>
<p>Publiqué 68 artículos científicos y 19 de divulgación, la mayoría sobre anfibios y reptiles, seguidos de temas etnológicos. Fallecí un 25 de diciembre en la Ciudad de México en 1987.</p>
