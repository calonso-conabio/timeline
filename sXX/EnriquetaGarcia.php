												<h2><strong>Enriqueta García Amaro</strong></h2>
												<h3>1928 - 1999</h3>
											</header>
<p>Enriqueta García Amaro fue una investigadora que dio fuerza al estudio de la relación de la diversidad biológica con los estudios climáticos. Nació en 1928 en la ciudad de México y falleció en 1999. </p>
                                          <p>Comenzó su carrera profesional en 1946 como ayudante de investigador en el Instituto de Geografía de la UNAM. Se dedicó en sus inicios a la representación gráfica de la superficie de la Tierra (topografía) utilizando técnicas como la fotogrametría aérea. Siguiendo esa temática obtuvo el título de Ingeniero Topógrafo e Hidrógrafo en 1948. A partir de entonces le apasionó la elaboración de mapas, algunos de los cuales han sido de gran utilidad para numerosos estudios biológicos.</p>
                                          <p>Uno de sus trabajos más importantes lo publicó en coautoría con su esposo, el botánico Faustino Miranda, titulado &ldquo;<em>Larrea</em> y clima&rdquo; (1961), obra en la que se enfatiza la relación entre el clima y la distribución de la vegetación. </p>
                                          <p>Profundizó en el estudio de esta relación y en 1970 obtuvo la Maestría en Ciencias (Geografía) en la Universidad de Wisconsin, Estados Unidos, con la tesis &ldquo;Modificaciones al Sistema de clasificación de climas de Köppen para adaptarlo a las condiciones de la República Mexicana&rdquo;. </p>
                                          <p>Sus trabajos abrieron un gran campo de investigación en el área de la ecología y la climatología, con lo cual promovió la interacción entre geógrafos, climatólogos y ecólogos. A lo largo de su carrera impartió clases de climatología, geografía y geología en distintos niveles, desde medio hasta posgrados.</p>
                                          <p>Fue miembro de la Academia de la Investigación Científica, de la Sociedad Botánica de México, de la Comisión de Zonas Áridas de la UNESCO y de la Sociedad Meteorológica Americana.</p>
