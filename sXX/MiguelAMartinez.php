											<!-- InstanceBeginEditable name="Biografía" -->
											<p>Nací en la Ciudad de México el 23 de octubre de 1942. Desde niño me interesó naturaleza. Estudié en el Colegio José María Vilaseca Esparza en calzada de Tlalpan y desde entonces me gustó mucho la lectura. Más adelante estuve un tiempo en el seminario, y aunque me interesó la teología, decidí seguir por el camino de la ciencia. Ingresé a la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENP.php"> Escuela Nacional Preparatoria </a>en donde mi profesor de biología fue <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/ManuelRuiz.php">Manuel Ruíz Oronoz.</a></p>
											<p>En los años 60s mi interés por la naturaleza me llevó a la carrera de biología, que cursé en la Facultad de Ciencias de la UNAM (1961-1964). Mi materia favorita fue la botánica impartida por la maestra María Agustina Batalla. Al terminar la carrera me integré a la Comisión Nacional para el Estudio de las Dioscóreas, para realizar mi tesis dirigida por el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/ArturoGomezP.php"> Dr. Arturo Gómez-Pompa </a>en el ejido Sabastopol, Tuxtepec, Oaxaca. A partir de entonces me interesé en entender el binomio hombre-planta o sociedad-naturaleza. </p>
											<p>Fui el primer egresado del Colegio Superior de Agricultura Tropical en Cárdenas, Tabasco. Me titulé como Maestro en Ciencias, bajo la dirección del <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/EfrainHdezX.php">Dr. Efraím Hernández Xolocotzi. </a>Años más tarde (1971) ingresé a la Maestría en Ciencias del Colegio de Posgraduados de la hoy Universidad Autónoma Chapingo. Desde entonces, me preocupaba la necesidad de integrar las ciencias naturales con las sociales para comprender los problemas que tenían las comunidades rurales con las que trabajaba, y por eso procuré que los resultados de esos estudios dieran la pauta para posibles soluciones. </p>
                                            <p>Para incluir una perspectiva antropológica a mi práctica profesional como etnobotánico, decidí hacer una maestría en antropología social, la cual cursé en la Universidad Iberoamericana (1973). Más tarde cursé el doctorado en la Facultad de Ciencias de la UNAM con el tema del uso de las plantas en la Sierra Norte de Puebla.</p>
                                            <p>El conocimiento tradicional de los pueblos indígenas sobre las plantas útiles, especialmente las alimenticias y las medicinales, es muy rico e invaluable. Por ello, varios de los temas desarrollados a lo largo de mi vida laboral fueron la etnobotánica, la historia del uso de las plantas útiles y de la agricultura tradicional.</p>
                                            <p>Impartí innumerables cursos y ofrecí conferencias en distintas instituciones a lo largo de la república mexicana. Entre mis estudiantes que siguieron con la etnobotánica se encuentran Arturo Argueta, Patricia Colunga, Cristina Mapes, Sergio Zamudio, entre otros. También publiqué libros, capítulos de libros y artículos especializados en revistas científicas y de divulgación. </p>
                                            <p>Pertenecí y colaboré con diferentes sociedades científicas nacionales e internacionales como la Sociedad Botánica de México, la International Society of Ethnobiology, el Grupo Etnobotánico Latinoamericano y el Grupo Latinoamericano de Etnobotánica.</p>
                                            <p>Por 27 años viví con mi compañera Abigail Aguilar Contreras, especialista en plantas medicinales, en la Casa Fueguina de Coyoacán.  Fallecí debido a una insuficiencia renal súbita y aun paro cardiaco el 9 de febrero de 2007 en la Ciudad de México. </p>
