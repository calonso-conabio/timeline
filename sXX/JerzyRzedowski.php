												<h2>Jerzy Rzedowski</h2>
												<h3>1926</h3>
											</header>
                                          <p>Nací en 1926 en la ciudad de Lwów, hoy Ucrania. En mi país natal comencé mi formación académica, pero debido a los estragos de la segunda Guerra Mundial me mudó junto con mi padre a México en 1946. </p>
                                          <p>Estudié la carrera de Biología en la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional realizando mi tesis sobre la vegetación del Pedregal de San Ángel bajo la dirección del Dr. Federico Bonet. Durante estos años conocí a la bióloga Graciela Calderón con la que más tarde me casaría. Al terminar la licenciatura ingresé a laborar como investigador en la Universidad de San Luis Potosí. En esos años colaboré con la fundación del Instituto de Investigación de Zonas Desérticas y promoví la creación del primer herbario estatal en dicho estado.</p>
                                          <p>En 1958 realicé una estancia en Francia donde hice estudios de ecología vegetal y aprendí los métodos europeos de investigación en este campo. En 1959 comencé a laborar en el Colegio de Posgraduados de Chapingo, además de obtener el Doctorado en Ciencias por la Facultad de Ciencias de la Universidad Nacional Autónoma de México, bajo la dirección del <a href="FaustinoMiranda.php">Dr. Faustino Miranda</a>.</p>
                                          <p>Soy cofundador, y actual editor, de la revista <em>Acta Botánica Mexicana A. C</em>., una de las publicaciones más importante en este campo a nivel nacional. Asimismo, desarrollé el Centro Regional del Bajío del Instituto de Ecología A.C., y soy coeditor de la serie <em>Flora del Bajío y de Regiones Adyacentes</em>. He publicado alrededor de 120 artículos de investigación, nueve libros y varias decenas de capítulos de libros. Una de mis obras más conocidas <em>La Vegetación de México </em>(1978), es una referencia clásica en la literatura botánica. </p>
