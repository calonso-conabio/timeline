												<h2>Jerzy Rzedowski</h2>
												<h3>1926</h3>
											</header>
<p>Jerzy Rzedowski Rotter es un notable botánico, autoridad en el campo de la florística mexicana, se ha especializado en su ecología, clasificación y distribución.</p>
                                          <p>Nació en 1926 en la ciudad de Lwów, hoy Ucrania. En su país natal comenzó su formación académica, pero debido a los estragos de la segunda Guerra Mundial se mudó junto con su padre a México en 1946. </p>
                                          <p>Estudió la carrera de Biología en la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional realizando su tesis sobre la vegetación del Pedregal de San Ángel bajo la dirección del Dr. Federico Bonet. Durante estos años conoció a la bióloga Graciela Calderón con la que más tarde se casaría. Al terminar su licenciatura ingresó a laborar como investigador en la Universidad de San Luis Potosí. En esos años colaboró con la fundación del Instituto de Investigación de Zonas Desérticas y promovió la creación del primer herbario estatal en dicho estado.</p>
                                          <p>En 1958 realizó una estancia en Francia donde hizo estudios de ecología vegetal y aprendió los métodos europeos de investigación en este campo. En 1959 comenzó a laborar en el Colegio de Posgraduados de Chapingo, además de obtener el Doctorado en Ciencias por la Facultad de Ciencias de la Universidad Nacional Autónoma de México, bajo la dirección del <a href="FaustinoMiranda.php">Dr. Faustino Miranda</a>.</p>
                                          <p>Es cofundador, y actual editor, de la revista <em>Acta Botánica Mexicana A. C</em>., una de las publicaciones más importante en este campo a nivel nacional. Asimismo, creó el Centro Regional del Bajío del Instituto de Ecología A.C., y es coeditor de la serie <em>Flora del Bajío y de Regiones Adyacentes</em>. Ha publicado alrededor de 120 artículos de investigación, nueve libros y varias decenas de capítulos de libros. Una de sus más conocidas obras <em>La Vegetación de México </em>(1978), es una referencia clásica en la literatura botánica. </p>
