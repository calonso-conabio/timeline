												<h2>Amelia Sámano Bishop</h2>
												<h3>1906 - 1998</h3>
											</header>
<p>Nací el 4 de septiembre de 1906 en la región de tierra caliente del estado de Guerrero, en el poblado de San Nicolás del Oro. Mis padres Efrén Sámano Montúfar y Eleuteria “Lutie” Bishop, de ascendencia inglesa, tuvieron siete hijos: yo fui la mayor, seguida de Eva, Gustavo, Fanny, Gracia, Alicia y Alpha.</p>
<p>Me gradué como bachiller en Artes en el Colegio de Arkansas en Estados Unidos. En 1935 estudié la maestría en Ciencias Biológicas en la Facultad de Filosofía y Letras y cuatro años más tarde hice el doctorado en Ciencias Biológicas en la Facultad de Ciencias, ambos en la Universidad Nacional Autónoma de México. Allá por el año de 1949 trabajé en el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/IB.php">Instituto de Biología</a> cuando estaba en la hermosa Casa del Lago de aquel esplendoroso y frondoso Bosque de Chapultepec.</p>
<p>Mi energía, motivación juvenil, disciplina y análisis me llevaron a participar en la fundación de nuevas instituciones educativas. En la UNAM me involucré con la fundación del Instituto de Biología, el Departamento de Embriología de su Facultad de Medicina y el de Biología perteneciente a la Facultad de Ciencias. Conocí al <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/IsaacOchoterena.php">Dr. Isaac Ochoterena </a>quien me aconsejó sobre mi formación universitaria.</p>
<p>En la escuela Médico Militar que fue abierta por Don Venustiano Carranza, promoví la primera clase de embriología humana además de que era la ¡primera escuela militar educando mujeres!</p>
<p>Mi hermana Eva Sámano se casó con Adolfo López Mateos quien fue presidente de México de 1958 a 1964. Ella fue conocida como la “gran protectora de la infancia”, por sus diferentes actividades en bien de los niños mexicanos. Para recaudar fondos organizaba grandes festivales donde intervenían artistas muy famosos, como el cantante y actor estadounidense Frank Sinatra (1915-1998) que dio una presentación en México. La actriz estadounidense Marilyn Monroe (1826-1962) donó dinero para la causa.</p>
<p>Gran parte de mi investigación la realicé sobre las algas de agua dulce de la Cuenca de México. Las algas son un tipo de plantas acuáticas que habitan en el mar así como en los ríos y lagos y desarrollan también fotosíntesis. A menudo utilizaba el microscopio para determinar sus formas, colores y su identidad. Una de las especies más importantes de la Cuenca la espirulina, era conocida como <i>tecuitlatl</i> por los mexicas. El consumo de espirulina estaba asociado con rituales o con los sectores de la población que tenían mayores exigencias en su alimentación como los mensajeros y guerreros. Estas algas verdeazuladas han sido reconocidas recientemente como  cianobacterias. Se considera que las cianobacterias fueron los primeros organismos en el planeta que realizaron la fotosíntesis oxigénica, es decir, productora de oxígeno. De esta forma cambiaron la composición de la atmósfera del planeta. Además son capaces de fijar nitrógeno del aire, función que muy pocos organismos pueden efectuar. Actualmente se ha comercializado un sumplemento dietético conocido como Espirulina que se obtiene a partir de cianobacterias.</p>
<p>Con la Secretaría de Salud realicé cansadísimas expediciones por el país para el estudio de enfermedades de origen tropical como el paludismo, enfermedad provocada por un organismo microscópico transmitido por los mosquitos. Cursé estudios de posdoctorado becada en Alemania, Escocia y Bélgica referentes a enfermedades tropicales y embriología humana.</p>
<p>En el aniversario de la Universidad Nacional de México en 1985, la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural </a>junto con la facultad de Ciencias de la UNAM, organizó un homenaje a los fundadores del Departamento de Biología de la Facultad de Ciencias. Yo fui una de las reconocidas junto a mis admirados colegas <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/RafaelMCampo.php">Rafael Martín del Campo, </a>Margarita Bravo Hollis, Roberto Llamas, María Agustina Batalla Rodríguez, y <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/LeonilaVazquez.php">Leonila Vázquez. </a></p>
<p>Pertenecí a varias sociedades como la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php"> Sociedad Mexicana de Historia Natural, </a>cuando <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/candidoBolivar.php"> el Dr. Cándido Bolívar y Pieltain </a> era el presidente. También pertenecí a la Sociedad Mexicana de Anatomía y fui fundadora de la Academia de la Investigación Científica. Al final de mis días padecí de algunas enfermedades y tuve que dejar el microscopio y las algas para siempre el 14 de junio de 1998 en la Ciudad de México.</p>
