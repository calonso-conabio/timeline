												<h2>Faustino Miranda</h2>
												<h3>1905 - 1965</h3>
											</header>
<p>¡Hola chavales! Soy Faustino Miranda y nací bajo la influencia del clima oceánico de Gijón al norte de España el 19 de febrero de 1905. Mis padres fueron Doña Mercedes González Forcelledo (1874-1915) y Don Hugo Miranda Tuya (1874-1946) quien fue catedrático de matemáticas en el instituto de Jovellanos y autor de una decena de manuales de Aritmética y Geometría. Mi padre fue muy estricto en la educación de sus hijos y alumnos. Siendo yo un crio de diez años mi mamá dejo de existir por culpa del cáncer y mis hermanos Bernardo y José pasamos al cuidado de las tías paternas Bonifacia y Fortunata.</p>
<p>En mi adolescencia y juventud realicé muchas caminatas cuando tenía tiempo libre por los bosques y montañas que están alrededor de mi pueblo, fue así que empecé a observar la diversidad que hay de sus plantas y sus flores. Otros días me la pasaba caminando en el mar al cual llaman “mar cantábrico”. A que no sabéis que este mar fue bautizado así por los romanos cuando estuvieron en la Península Ibérica. Este mar es parte del Océano Atlántico y ahí descubrí por primera vez las algas marinas.</p>
<p>Estudié Ciencias Naturales en la Universidad de Oviedo y continué en la Facultad de Ciencias de la Universidad Central de Madrid (1925). Realicé mi doctorado en el Jardín Botánico de Madrid sobre el estudio de las algas marinas del norte de España y al finalizarlo continué mis estudios en este campo en el Museo de Historia Natural de París. Elegí Francia porque contaba con estudios muy avanzados sobre las algas por ejemplo en el Muséum National d'Histoire Naturelle de París, se encuentran las colecciones más completas de algas de las costas de la Península Ibérica, la Biblioteca del Muséum parisino es una de las más importantes del mundo en temas botánicos y conocer al más importante investigador de algas llamado Camille Sauvageau (1861-1936).</p>
<p>En el año de 1936 teniendo 31 años regresé a mi ciudad para dar clases y seguir estudiando a las algas del Mar Cantábrico. Pero tristemente en julio de ese año mi pueblo fue bombardeado por el ejército del General Francisco Franco Bahamonde (1892-1975) un español que había hecho carrera militar en el norte de África e impuso una dictadura militar en mi país. Imagínense, tuvo como aliados a Adolfo Hitler (1889-1945) y Benito Mussolini (1883-1945) que le proporcionaban armamento y soldados adiestrados. Me trasladé a Barcelona donde di clases en el Instituto Obrero y formé parte de las milicias de la Cataluña en contra de la dictadura. Pero en 1939 las cosas se pusieron horribles ya que el ejército de Franco había triunfado en la Batalla del Ebro por lo que no tardaba mucho en que entrara en Barcelona. Yo decidí caminar y caminar hasta los Pirineos franceses para refugiarme en Francia dónde sufrí mucho ya que estuve en un campo de concentración y en condiciones difíciles de salud.</p>
<p>Con la ayuda de varios colegas franceses pude salir de ahí en el año de 1939 me embarqué hacia México a bordo del buque “Sinaia” con dos mil españoles a bordo y acompañados casis todos de la nostalgia. Mi hermano José también se vino a México y nunca mas volvimos a ver a nuestro padre. Mi hermano Bernardo antes de la guerra había muerto en un accidente automovilístico.</p>
<p>Los primeros meses en México fueron complicadísimos pero después empecé a dar clases en colegios privados y a reencontrarme con algunos paisanos como Enrique Rioja (1895–1963)</p>
<p>Algunas veces me unía a grupos de expedicionarios que recorrían la Ciudad de México y fui descubriendo la belleza de la flora mexicana ya sea en plantas, flores u árboles. Acudí las librerías de viejo a buscar libros de botánica mexicana, así encontré obras de Paul C. Stanley (1884-1963) especialista en flora tropical y a quien tuve el honor de conocer en una visita que hice a la ciudad de Chicago en 1947. En esas librerías también encontré escritos del italiano Conzatti (1862-1951) así que empecé a identificar plantas de los alrededores de Cuernavaca y la tierra caliente de Guerrero empezando a publicar mis descubrimientos en la revista Ciencia fundada por refugiados españoles.</p>
<p>En 1941 el Dr. Isaac Ochoterena (1885-1950) me invitó a formar parte del Instituto de Biología de la UNAM en aquella Casa del lago del Bosque de Chapultepec. Realicé investigaciones sobre taxonomía y ecología vegetal. Descubrí varias especies nuevas en sus numerosas salidas al campo. Y me casé con Enriqueta García Amaro (1928 – 1999) que es geógrafa y con quien publiqué investigaciones sobre las relaciones de la vegetación en relación con el clima. Tuvimos un hijo de nombre Antonio Miranda García, que tiene el doctorado en computación por la Universidad de Texas A&amp;M, lamentablemente le quedé a deber tiempo debido a mi muerte prematura.</p>
<p>Estuve un periodo en Chiapas (1946-1954) donde conocí al Maestro Álvarez del Toro (1917-1996). Ahí estudié la vegetación del estado, de este conocimiento derivó el libro La Vegetación de Chiapas. Además fundé el Instituto Botánico del Estado de Chiapas, actualmente Departamento de Botánica del Instituto de Historia Natural. En Chipas conocí a un joven estudiante llamado Arturo Gómez Pompa (1934-) que junto a otros estudiantes los llevaba a explorar la selva para colectar plantas y hongos. El profesor Enrique Beltrán (1903-1994), director del Instituto Mexicano de Recursos Naturales Renovables (IMERNAR) me invitó a realizar estudios sobre la vegetación de la península de Yucatán, que recorrí entre 1954 y 1955.</p>
<p>A mi regreso a la ciudad de México, contribuí en la fundación del Jardín Botánico de la UNAM con el diseño del mismo y la creación de las primeras colecciones de plantas. Se interesó también en la etnobotánica e historia de la botánica, participó en la interpretación de los escritos del protomédico Francisco Hernández de Toledo (1514-1578) y del Códice Cruz-Badiano.</p>
<p>También di clases de botánica y ecología vegetal en la Facultad de Ciencias de la UNAM, la Escuela Nacional de Graduados, la Escuela Normal Superior y la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional (IPN). En 1958 trabajé para la Secretaría de Marina de México en la valoración biológica de las Islas de Revilagigedo e Isla Socorro junto con el Agrónomo Efraim Hernández (1913-1991).</p>
<p>Un 17 de diciembre de 1964 llegó mi muerte en la Ciudad de México con un infarto. Dejo a vosotros mis esfuerzos enfocados en la botánica mexicana.</p>
