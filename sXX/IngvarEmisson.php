                                          <p>Nací en un pueblo de pescadores en Islandia oriental donde el principal sostén era la pesca. Mis primeros estudios fueron sobre matemáticas en la Universidad de Islandia (1947). Más tarde hice la maestría en la Universidad de Oslo (1951) y el doctorado en la Universidad de Bergen (1953), ambos en Noruega, especializándome en las áreas de geografía física y oceanografía física. En ese periodo participé en numerosas expediciones oceanográficas y pesqueras hacia los mares árticos y  subárticos.</p>
                                          <p>Pasé unos años en Brasil haciendo estudios sobre las aguas y las lagunas costeras sudamericanas, así como dirigiendo cruceros de investigación en el océano Atlántico. Tiempo después, trabajé como asesor para la UNESCO en Cuba, donde estudié las áreas marítimas adyacentes a la isla.</p>
                                          <p>Mi relación con México comenzó en 1970, cuando fui enviado al Instituto de Geofísica de la UNAM. Ahí, realice varias campañas oceanográficas con los buques de la Armada de México, los cuales estaban adaptados para hacer investigación marina. </p>
                                          <p>Luego colaboré en la estructuración y la consolidación del recién transformado Instituto de Ciencias del Mar y Limnología de la UNAM (1982). En esta institución trabajé en el diseño y el equipamiento de los buques &ldquo;El Puma&rdquo; y el &ldquo;Justo Sierra&rdquo;, verdaderos laboratorios de trabajo flotantes, con los cuales se ha estudiado la biodiversidad acuática mexicana.</p>
                                          <p>La docencia también ocupó parte de mi vida, incluso participé en la planeación de algunos posgrados en ciencias marinas. </p>
                                          <p>Soy miembro de la Unión Geofísica Mexicana, la Sociedad Americana de Limnología y Oceanografía y la Unión Geofísica Americana y a la fecha soy coordinador de las Plataformas Oceanográficas de la UNAM, área a cargo de los buques universitarios.</p>
