											</header>
<p>Soy un apasionado de los océanos, de su enorme biodiversidad, de los distintos fenómenos que ocurren en torno a ellos y de los valiosos recursos que contienen. Mi amor por el mundo acuático me ha llevado a ser uno de los principales impulsores de las ciencias del mar en México desde varias trincheras: como investigador, profesor, administrativo y divulgador de la ciencia.</p>
                                          <p>Nací en el Distrito Federal en 1929, donde estudié la licenciatura en Biología y la maestría en Ciencias en la Facultad de Ciencias de la UNAM. Por varios años, me dediqué a la docencia y a la investigación en mi alma máter, hasta que en 1992 me integré al cuerpo docente de la Universidad de Guadalajara, donde laboro actualmente.</p>
                                          <p>He ocupado diversos puestos: Director General del Instituto Nacional de Pesca; Subdirector de Asuntos Biológicos Pesqueros de la Secretaría de Industria y Comercio; presidente, y cofundador, del Colegio de Biólogos de México, entre varios más.</p>
                                          <p>En reconocimiento a mi trayectoria y contribución al desarrollo de la biología pesquera y de las ciencias del mar en México, he recibido el doctorado <em>honoris causa</em> de parte de numerosas universidades del país, incluso internacionales.</p>
                                          <p>Me interesa conservar la riqueza biológica de los mares; por ejemplo, fundé el Programa de Protección y Conservación de la Tortuga Marina en México; así como en defenderlos cuando ha sido necesario o de buscar mejores formas de aprovechamiento de los recursos oceánicos, sobre todo los pesqueros. </p>
                                          <p>Una de mis principales obras de divulgación de la ciencia es &ldquo;El Océano y sus recursos&rdquo;, una serie de doce volúmenes que aborda las investigaciones oceánicas realizadas en México y el mundo desde distintos ángulos.</p>
