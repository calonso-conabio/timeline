												<h2>Arturo Gómez Pompa</h2>
												<h3>1934</h3>
											</header>
<p>Nací en la Ciudad de México el 21 de octubre de 1934. Mi padre fue Arturo Gómez Martínez y mi madre Paula Pompa del Bosque. Mi padre trabajaba como diplomático en el Consulado de Laredo. Crecí cuando la ahora colonia Del Valle, realmente era un valle, había pocas casas y muchos maizales. Estudié en el Centro Universitario México. En la escuela me conocían como "Flaco". Aunque mis padres querían que fuera médico, tome la decisión por la biología cuando visité las oficinas del Departamento de Biología de la UNAM en la avenida Ribera de San Cosme para investigar sobre la carrera y tuve una entrevista con el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/ManuelRuiz.php"> Dr. Ruiz Oronoz.</a> Más tarde él sería mi primer profesor de botánica. Otros de sus discípulos fueron Javier Valdés, <a href="http:/www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/RamonRibaN.php">Ramón Riba y Nava Esparza </a> y Samuel Mariel. </p>
<p>Terminé la carrera en 1956 y gracias al <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/FaustinoMiranda.php">Dr. Faustino Miranda </a>conseguí una entrevista con el Dr. Francisco Giral González, gerente general de Farquinal, una empresa farmacéutica estatal.  El Dr. Giral, amigo de Faustino Miranda y también refugiado español era una enciclopedia en el conocimiento de la química de las plantas. Mi primera tarea fue estudiar el barbasco, una planta de las familia de las Dioscoreaceae. ¡Quedé impresionado con la familia! Es una familia con más de 600 especies en donde sobresale el género <i>Dioscorea.</i> Este grupo de plantas se distribuye en las regiones tropicales de América, Asia y África. Algunas de ellas han sido cultivadas como alimento por sus grandes tubérculo o "camote" y se conocen popularmente como "ñames" o "cabeza de negro". Además muchas de ellas se han utilizado por sus propiedades medicinales. En particular estas plantas eran muy buscadas ya que producían "diosgenina" un sustancia química de la cual se podían producir hormonas animales y en particular la llamada "píldora anticonceptiva". En México viven alrededor de 80 especies de bejucos conocidas como "barbasco" y se utilizan para el tratamiento de reumas o "dolor del cuerpo". </p> 
<p>En 1957 conocí a <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/AlfredoBarrera.php">Alfredo Barrera, </a> quien vino del Instituto Politécnico Nacional a dar una cátedra sobre Protozoológía a la Facultad de Ciencias UNAM. A mi me tocó ser su ayudante en el laboratorio. Yo tenía 24 años y él 36. Desde entonces colaboramos en muchas iniciativas como el Consejo Nacional para la Enseñanza de la Biología (CNEB) en 1967, la Academia de la Investigación Científica como su secretario. De algunos de estos esfuerzos nació el Consejo Nacional de Ciencia y Tecnología (CONACYT) en 1970, donde trabajé con Alfredo en el comité de Biología. Más tarde trabajamos juntos en la Facultad de Ciencias y después en el INIREB.</p> 
<p>Me casé con Norma Barrero en 1958, ambos eramos muy jóvenes. Tuvimos tres hijos: Arturo Eugenio, Eduardo y Gerardo y ahora hasta biznietos tenemos. </p>
<p>A fines de los 50 el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/EnriqueBeltran.php">Dr. Enrique Beltrán, </a>Subsecretario Forestal y de la Fauna en la Secretaría de Agricultura y Ganadería creó la "Comisión de Estudios sobre la Ecología  de Dioscóreas". El conocimiento producido por la comisión sería la base para asignar permisos de colección de estas plantas para la industria farmacéutica. Los principales asesores de la comisión eran el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/FaustinoMiranda.php">Dr. Faustino Miranda </a> y el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/EfrainHdezX.php">Dr. Efraím Hernández-Xolocotzi</a> y a mi me tocó hacerme cargo de la comisión. En época nos empezamos a dar cuenta del valor de las selvas y al mismo tiempo en su inminente destrucción por proyectos como el desplazamiento de poblaciones debido a la construcción de la presa Cerro de Oro en el alto Papaloapan y moverlos a la región de <a href="https://vimeo.com/21363505">Uxpanapa, Veracruz.</a></p>
<p>La comisión fue un catalizador para la formación de muchos investigadores entre los que sobresalieton <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/JoseSarukhan.php">José Sarukhán, </a> Javier Chavelas Polito, <a href="http:/www.biodiversidad.gob.mx//biodiversidad/curiosos/sXX/MarioSousa.php">Mario Sousa Sánchez, </a> <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/MiguelAMartinez.php">Miguel Ángel Martínez Alfaro,</a> Alfredo Perez Jiménez, Jesús Manuel León-Cazares, Jesús Vázquez Soto, Ángel Ramos Sánchez, Fernando Chiang, Nelly Diego, Hermilo Quero, Javier Caballero Nieto y Heriberto Cuanalo de la Cerda. Uno de nuestros grandes maestros en el campo fue Agapito Hernández, natural de Tuxtepec, Oaxaca, quien nos compartió su gran conocimiento adquirido empiricamente.</p>
<p>En 1975 con apoyo del Consejo Nacional de Ciencia y Tecnología (CONACYT) fundé el Instituto Nacional de Investigaciones sobre Recursos Bióticos (INIREB) en la ciudad de Xalapa, Veracruz. Esta fue una de las primeras iniciativas para descentralizar la investigación científica del Distrito Federal. Después de 13 años de investigación fructífera y muchas publicaciones científicas, el INIREB se cerró en 1988 durante el gobierno del Presidente Miguel de la Madrid.</p>
<p>En 1986 inicié actividades en la Universidad de California en Riverside en el Departamento de Botánica y en el Instituto de la Universidad de California para México (UC Mexus). Desde California continué colaborando con instituciones mexicanas.                                                                  
<p>En 1993 junto con un grupo de conservacionistas, mi esposa y mis hijos, fundé la Reserva Ecológica del Edén en las selvas del norte de Quintana Roo. Esta reserva se ha vuelto un centro de investigación y conservación de la biodiversidad.
