												<h2>Laura Huerta Múzquiz</h2>
												<h3>1913 - 2000</h3>
											</header>
				<p> Nací el 17 de octubre de 1913 en Saltillo, Coahuila. Mi padre, Adolfo Huerta Vargas, general maderista fue gobernador interino de Coahuila en 1915, murió cuando to tenía 4 años. Mi madre Electa Múzquiz, hija de José María Múzquiz, también exgobernador interino de Coahuila, emigró con la famila a Estados Unidos, para después regresar en 1934 a la Ciudad de México. Fuimos tres hermanos, Elena (muralista), Adolfo y yo. En la Ciudad de México me incorporé a la recién creada Escuela de Bacteriología, Parasitología y Fermentaciones de la Universidad Gabino Barreda, posteriormente convertida en la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENCB.php">Escuela Nacional de Ciencias Biológicas </a>del Instituto Politécnico Nacional (ENCB). Ahí me titulé como Química-Bacterióloga-Parasitóloga.</p>
                <p>Mis primeros estudios los realicé sobre hormonas y enzimas de plantas y animales en el Laboratorio de Fisiología General Vegetal de dicho instituto, al que me integrué en 1938. Posteriormente realicé una estancia en el Instituto para la Investigación de Hormonas de Plantas en Estados Unidos. Sin embargo, fue a raíz de una expedición oceanográfica al archipiélago de las Islas Revillagigedo, organizada en 1955 por el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/FedericoBonet.php">Dr. Federico Bonet </a>de la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENCB.php">Escuela Nacional de Ciencias Biológicas </a>, que comenzó mi interés por el estudio de las algas marinas. A partir de este momento me dediqué a trabajar sobre la florística y ecología de estos seres, incluyendo investigaciones sobre el fitoplancton y algunos aspectos sobre su aprovechamiento.</p>
                <p>Mi hermana mayor Elena, apodada "La nena Huerta" se dedicó a la pintura y fue parte del movimiento muralista mexicano. Estuvo casada con Leopoldo Arenal, cuñado del gran muralista David Alfaro Siqueiros. De él tomó el nombre de Elena Arenal Bastar. Ella pintó el gran mural "Cuatrocientos años de historia de Saltillo" de 450 metros considerado el más grande de México pintado por una mujer. </p>
                <p>Mi grupo favorito, el de las algas, es muy complejo: algunas son unicelulares, mientras que otras como el kelp, miden decenas de metros. Se han dividido en varios grupos, el más diverso es el de las algas rojas con más de mil especies en México, la mayoría de ellas marinas. Además de su increible biología, las algas me interesaron por su valor alimenticio. Los antiguos mexicanos utilizaron algunas especies de algas del Lago de Texcoco como alimento. Entre ellas se han documentado el amoxtle o cuculin (alga verdeazul), el cocol de agua (<i>Phormidium</i>) y el tecuitate (posiblemente <i>Spirulina geiileri</i>).     
                <p>Publiqué más de 20 artículos científicos sobre las algas del Golfo de México, la Península de Yucatán, las costas de Oaxaca, Colima, Sinaloa y Baja California. Junto con el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/JerzyRzedowski.php">Dr. Jerzi Rzedowski </a>en su libro "La Vegetación de México" publiqué una sintesis de la <a href="http://www.biodiversidad.gob.mx/publicaciones/librosDig/pdf/VegetacionMxC19.pdf">vegetación marina de ambas costas de México</a>. También me dediqué a la docencia, impartí clases de citología vegetal, botánica criptogámica y ecología de algas. Fallecí en el 9 de febrero del 2000.</p>
