												<h2>María Elena Caso Muñoz</h2>
												<h3>1915 – 1991</h3>
											</header>
<p>María Elena Caso Muñoz fue una destacada bióloga que inició una nueva época en el estudio de estrellas de mar, ofiuras, pepinos y erizos de mar (equinodermos) en México. Nació en la ciudad de México en 1915 y falleció en 1991.</p>
                                          <p>Estudió biología en la Facultad de Ciencias de la UNAM donde más tarde obtuvo el grado de Maestra en Ciencias Biológicas con sus tesis &ldquo;Contribución al conocimiento de los Astéridos de México&rdquo; (1943). Posteriormente, en 1961 se tituló doctora en esta misma rama con el trabajo &ldquo;Los equinodermos de México&rdquo;.</p>
                                          <p>Fue cofundadora del Laboratorio de Hidrobiología en el Instituto de Biología de la UNAM, del cual surgió más tarde el Instituto de Ciencias del Mar y Limnología (ICML) de dicha institución.</p>
                                          <p>Su producción científica consiste en alrededor de 60 trabajos que abarcan los principales grupos de equinodermos, lo cual representa la obra más importante y completa que se conoce sobre esta especialidad en nuestro país. Se interesó en la docencia y enseñó zoología de invertebrados, invertebrados marinos, biogeografía marina e hidrobiología marina en la Facultad de Ciencias de la UNAM.</p>
                                          <p>En el periodo de 1950 a 1956, a petición de la Gerencia de Exploración de Petróleos Mexicanos, trabajó como asesora en el Laboratorio de Paleontología. De ahí derivó su trabajo sobre &ldquo;Los equinoideos fósiles del Cenozoico de México&rdquo;.</p>
                                          <p>A lo largo de sus exploraciones por las costas mexicanas del Atlántico y Pacífico, describió numerosas especies no conocidas anteriormente de estos animales. Esta actividad la llevó a formar una vasta colección científica de equinodermos que consta de ejemplares nacionales y extranjeros. Actualmente los ejemplares forman parte de la Colección Nacional de Equinodermos &ldquo;Dra. Ma. Elena Caso Muñoz&rdquo; del ICML de la UNAM.</p>
