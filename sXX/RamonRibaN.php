												<h2>Ramón Riba y Nava Esparza</h2>
												<h3>1934 - 1999</h3>
											</header>
<p>Nácí en la Ciudad de México el 23 de abril de 1934. Estudié biología en la Facultad de Ciencias de la UNAM (1957). Mi gusto por la botánica fue influenciado fuertemente por mi maestro el <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXX/FaustinoMiranda.php">Dr. Faustino Miranda. </a>Ahi tuve como compañeros a los botánicos <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXX/ArturoGomezP.php">Arturo Gómez Pompa</a> y Javier Valdés Gutiérrez. Mis primeras investigaciones se enfocaron en diversos grupos de organismos, como algas, hongos y plantas. Estudié el posgrado en la Universidad de Harvard (1967) con una beca John Simon Guggenheim.</p>
<p>El grupo de las plantas que realmente atrajo mi atención fueron los helechos. Este es un grupo de plantas vasculares, es decir, que tienen un sistema de células especializadas en el transporte, de agua, sales minerales, y nutrientes, pero no tienen semilla. Su reproducción es realmente interesante ya que tienen un ciclo de vida con dos fases o generaciones.  El "esporofito" que es el típico helecho que conocemos con hojas grandes divididas en pequeñas que se van desenrrollando cuando crecen. Estas hojas producen "soros" que son cápsulas en donde se forman las esporas que son dispersadas por el viento. La otra fase, "el gametofito", es muy poco conocida, ya que es minúscula. Se forma a partir de las esporas haploides (con la mitad de los cromosomas) y tiene órganos sexuales masculinos y femeninos. Al unirse las células de ambos sexos producen la nueva generación diploide (con el doble de cromosomas) que es el esporofito.</a> 
<p>Entre mis aportaciones a la botánica de México estuvieron, por un lado, el proyecto de enriquecer con nuevos datos e información al inventario de helechos del estado de Veracruz; y por el otro, mi participación en el proyecto Flora Mesoamericana,  la primera gran publicación  sobre flora regional escrita en español y en la que participaron la UNAM, el Jardín Botánico de Missouri y el Museo de Historia Natural de Londres. También llevé a cabo la traducción al español de varios libros como "Diversificación vegetal" de Theodore Delevoryas, "Las plantas vasculares: forma y función" de Frank Salisbury y Robert Park, y "Las plantas y la civilización" de Herbert Baker.</p>
<p>También me dediqué a la docencia y a las actividades administrativas. Fui cofundador del Jardín Botánico de la UNAM y ayudé a traer plantas de todo el país, junto con otros compañeros. También fuí jefe del Herbario Nacional del Instituto de Biología de la misma institución. Participé en 1974 la creación del Departamento de Biología de la recién creada Universidad Autónoma Metropolitana en la unidad Iztapalapa, junto con el hidrobiólogo <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXX/AlejandroVillalobos.php">Alejandro Villalobos, </a>el mastozoólogo José Ramírez Pulido, el ecofisiólogo Carlos Vázquez-Yanes, y el maestro de anatomía comparada Abraham Kobelkowsky entre otros profesores. Ahi formé su Herbario Metropolitano, el cual hoy lleva mi nombre y resguarda a miles de ejemplares de plantas. Mi alumna la Dra. Blanca Pérez García ha seguido mi labor estudiando detalladamente a los helechos.</p>
<p>  Fui miembro de muchas sociedades como la Sociedad Botánica de México y la Asociación Internacional de Pteridologistas. Fui pionero en México y Latinoamérica en hacer estudios sobre la distribución geográfica y la clasificación de los helechos. Registré lo largo de mi vida muchísimas especies de estas plantas, muchas de ellas  desconocidas para la ciencia. Fallecí el 13 de diciembre de 1999.</p>
