												<h2>Leonila Vázquez García</h2>
												<h3>1914 - 1995</h3>
											</header>
				<p>Nací en Ciudad Jiménez, en el sureste del estado de Chihuahua, el 17 de enero de 1914. Mi papá Enrique Vázquez fue ingeniero en Minas. Mi mamá se llamaba Dolores García. A corta edad me trasladé a la capital del país donde comencé mi vida académica. Cursé el bachillerato en la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENP.php">Escuela Nacional Preparatoria.  </a>. Ahi tuve de maestros a <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/IsaacOchoterena.php">Isaac Ochoterena, </a> <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/CarlosCHoffmann.php">Carlos Hoffmann, </a><a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/HeliaBravo.php">Helia Bravo, </a>entre otros. </p>
				
				<p>Posteriormente hice la licenciatura en Biología en la Universidad Nacional Autónoma de México (UNAM). En 1936 obtuve el grado de Maestra y en 1946 el de Doctora en Ciencias Biológicas por la UNAM. Mis trabajos abordaron el estudio de los insectos, particularmente de las mariposas mexicanas, enfocándose en aspectos de su biología, ecología y clasificación, de las cuales describí varias especies. Este grupo lo trabajé con el Dr. Carlos R. Beutelspacher, especialista en mariposas.</p>

				<p>En 1958 participé en la primera Expedición Científica de la Universidad Nacional Autónoma de México a las Islas Revillagigedo organizada por el Dr. Julián Adem. Ahi estuvimos explorando todo un mes un gran grupo de investigadores entre los que estaban <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/BernardoVilla.php">Bernardo Villa, </a> <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/FaustinoMiranda.php">Faustino Miranda, </a> <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/TeofiloHerrera.php">Teófilo Herrera, </a> <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/AlejandroVillalobos.php">Alejandro Villaobos, </a>Gonzalo Pérez Higareda y Héctor Pérez.</p>

				<p>Una de las especies a las que les dediqué un buen tiempo fue la "cochinilla de la grana" <i><a href="http://www.enciclovida.mx/especies/10053770">(Dactylopius coccus)</a></i>. Este pequeño insecto que crece en los nopales se ha utilizado desde tiempos inmemoriables para producir colorantes. Durante la época colonial tuvo un gran auge y fue muy importante en la economía de algunas regiones el país. </p>


                <p>Además de la investigación, me dediqué también a la docencia, impartiendo clases de zoología, entomología general y ecología de insectos, en la Facultad de Ciencias de la UNAM y en la Escuela Técnica Superior de Posgraduados del Instituto Politécnico Nacional (IPN). Cuando falleció mi maestro el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/CarlosCHoffmann.php">Dr. Carlos Hoffmann, </a>me quedé a cargo de su clase por casi 50 años. Junto con <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/AlejandroVillalobos.php">Alejandro Villalobos </a>escribí un texto para el curso de Artrópodos.</p>
                   <p>Fuí miembro de varias sociedades entomológicas entre las que destaqué como miembro fundador de la Sociedad Mexicana de Entomología y la Sociedad Mexicana de Zoología. También fui miembro de la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural. </a>Publiqué alrededor de 50 trabajos científicos sobre mariposas y escribí la obra &ldquo;Zoología del Phylum Arthropoda&rdquo;, libro de texto fundamental en los cursos de artrópodos. Fallecí en la Ciudad de México el 30 de enero de 1995 a los 81 años.</p>
