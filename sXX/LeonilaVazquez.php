												<h3>1914 - 1995</h3>
											</header>
<p>Leonila Vázquez García fue una notable bióloga que se enfocó en el estudio de las mariposas mexicanas. Nació en Jiménez, estado de Chihuahua, en 1914 y murió en la Ciudad de México en 1995.</p>
                                          <p>A corta edad se trasladó a la capital del país donde comenzó su vida académica. Cursó el bachillerato lo en la Escuela Nacional Preparatoria y posteriormente la licenciatura en Biología en la Universidad Nacional Autónoma de México (UNAM). En 1936 obtuvo el grado de Maestra y en 1946 el de Doctora en Ciencias Biológicas por la UNAM. Sus trabajos abordaron el estudio de los insectos, particularmente de las mariposas mexicanas, enfocándose en aspectos de su biología, ecología y clasificación, de las cuales describió varias especies.</p>
                                          <p>Además de la investigación, se dedicó también a la docencia, impartiendo clases de zoología, entomología general y ecología de insectos, en la Facultad de Ciencias de la UNAM y en la Escuela Técnica Superior de Posgraduados del Instituto Politécnico Nacional (IPN).</p>
                                          <p>Fue miembro de varias sociedades entomológicas entre las que destacó como miembro fundador de la Sociedad Mexicana de Entomología y la Sociedad Mexicana de Zoología. Publicó alrededor de 50 trabajos científicos sobre mariposas y escribió la obra &ldquo;Zoología del Phylum Arthropoda&rdquo;, libro de texto fundamental en los cursos de artrópodos. </p>
