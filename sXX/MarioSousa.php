												<h2>Mario Sousa Sánchez</h2>
												<h3>1940 - 2017</h3>
											</header>
<p>Nací en la Ciudad de México el 19 de febrero de 1940. Mi padre fue abogado y economista, subsecretario de la Secretaría de Economía con el presidente Lázaro Cárdenas y Director del Departamento de Asuntos Agrarios durante el Gobierno de Miguel Alemán Valdés (1946-1954). Al igual que el presidente Alemán, él era veracruzano. Fuimos dos hijos en la familia, Isabel mi hermana y yo.</p>
<p>Estudié la licenciatura en biología en la Facultad de Ciencias de la Universidad Nacional Autónoma de México. Mi tésis de licenciatura la dirigió el <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/FaustinoMiranda.php">Dr. Faustino Miranda </a>y fue sobre la vegetación secundaria de la región de Tuxtepec, Oaxaca. Mi trabajo de campo estaba ligado a la iniciativa de la Comisión para el Estudio Ecológico de las Dioscoreas donde también participaban otros estudiantes como <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/JoseSarukhan.php">José Sarukhán,</a> <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/MiguelAMartinez.php">Miguel Ángel Martínez Alfaro,</a> Javier Chavelas, Alfredo Pérez Jiménez y Fernando Chiang.Un año más tarde en 1964 inicié mi primer trabajo como Técnico Académico del Jardín Botánico de la UNAM.</p>
<p>A mediados de los 60s, junto con el <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/ArturoGomezP.php">Dr. Arturo Gomez Pompa </a>buscamos posibilidades de establecer una Estación Biológica en las selvas tropicales en donde se pudiera llevar investigación a largo plazo. A través de amigos de mi padre nos enteramos que había unos terrenos en la zona de los Tuxtlas que estaban disponbiles. Hicimos los trámites necesarios y la Universidad Nacional Autónoma de México pudo adquirir 600 hectáreas que se convirtieron en la Estación de Biología Tropical "Los Tuxtlas", uno de los sitios en donde más hemos aprendido de las selvas tropicales en México.</p>
<p>Hice la maestría y el doctorado en la Universidad de Harvard, en Cambridge, Massachussets dedicado de lleno a las Leguminosas. Esta familia realmente me llamaba la atención y a ella me dediqué con gran entusiasmo. Al regresar a México ingresé como investigador al Instituto de Biología de la UNAM y ahi fui director del Herbario Nacional, el más grande del país. Durante los 10 años que trabajé como director, triplicamos el número de ejemplares de plantas mexicanas alcanzando 450 mil ejemplares botánicos. </p>
<P>Las leguminosas son una familia de plantas extremadamente diversa. Después de las margaritas (Asteraceae) y de las orquídeas (Orquidaceae) son la familia con más especies. Se conocen alrededor de 20 mil especies y sus hábitos de creciiento  son extremadamente viariados. Pueden ser hierbas, arbustos, enredaderas, lianas y árboles. Sus raíces tienen una simbiosis con bacterias que asimilan el nitrógeno de la atmósfera. Entre mis leguminosas favoritas estaban los árboles del género <a>Lonchocarpus, </a>de los cuales describí muchas especies nuevas. Uno de estos árboles es el balché, del sureste mexicano, del cual los mayas preparaban una exquisita bebida que se utilizaba en las ceremonias sagradas.  
<p>Me encantaba salir a explorar las selvas mexicanas. Recorrí gran parte del sur y sureste y de Centro América, acompañado de fabulosos estudiantes como Esteban Martínez, José Carmen Soto, Arturo Solís, Alberto Reyes, Alfonso Delgado, Lourdes Rico, Rosaura Grether, entre muchos otros. Esas exploraciones me llevaron a describir alrededor de 150 especies de plantas nuevas para la ciencia.</p>
<p>En 1974 inicié la colección de frutos y semillas del Herbario Nacional, que actualmente tiene más 8,300 ejemplares. De 1975 a 1985 regresé como director del Herbario Nacional del Instituto de Biología, el más grande de México. Durante esos años me involucré en el gran proyecto "Flora Mesoamericana" impulsado por el botánico Peter H. Raven, director del Jardín Botánico de Missouri. A la fecha se han publicado cinco volúmenes con alrededor de 8186 especies.  Más adelante volví a ser director del Herbario de 1994 a 2003 y pasamos el millón de ejemplares. </p>
<p>Mis primeras nupcias fueron con Magdalena Peña Muñoz (1939-2012), quien era una gran especialista en orquídeas y discípula de <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/GloriaAlencaster.php">Gloria Alencaster </a>y de <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/EfrainHdezX.php">Efraím Hernández Xolocotzi. </a>Con ella tuvimos tres hijos varones: Mario, Raúl y Leonardo. Más tarde me casé en segundas nupcias con la botánica Gloria Andrade Murguía (1949- ) y tuvimos dos hijas: Gloria y Maria Luisa.
<p>Recibí el Doctorado Honoris Causa de la Universidad Michoacana de San Nicolás de Hidalgo en 2011. En 2015 cumplí 50 años de Trayectoria Académica en el Instituto de Biología de la UNAM. Fallecí en el lugar donde nací, la Ciudad de México, el 17 de enero de 2017.</p>
