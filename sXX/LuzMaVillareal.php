												<h2>Luz María Villarreal de Puga</h2>
												<h3>1913 - 2013</h3>
											</header>
									<p>Nací el 8 de marzo de 1913 en la ciudad de México. Estudié en el Colegio de San Ignacio de Loyola Vizcaínas, un edificio muy antiguo en el Centro Histórico de la Ciudad de México. Ahí estudiaron también Josefa Ortiz de Domínguez, la "corregidora" y la famosa actriz Sara García, pero un poquito antes que yo. Ahí me gradué como profesora normalista. Desde niña me atrajo la naturaleza. Más adelante de joven visité el Herbario Nacional de México y me hice amiga del gran botánico <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/MaximinoM.php">Maximino Martínez.</a></p>
                                    <p>Mi vida profesional comenzó como secretaria para el gobierno. En 1942, me casé con Felipe González Ordorica, y tuvimos a nuestra hija Luz María González Villarreal quien siguió mis pasos de bióloga. Unos años más tarde nuestro matrimonio terminaría. En 1959 me volví a casar con el Ingeniero Luis Puga y Robles Gil y nos mudamos la ciudad de Guadalajara. Ahi cursé una especialización en Biología en la Escuela Normal Superior &ldquo;Nueva Galicia&rdquo; de Guadalajara y más tarde, ahí comencé a impartir clases de biología, zoología y botánica. En Guadalajara y más tarde en todo México, fui conocida como "la Maestra Puga"</p>
                                    <p>También impartí las cátedras de sistemática botánica, botánica general y ciencias biológicas en escuelas preparatorias de la Universidad de Guadalajara, en la Escuela de Agricultura de la misma universidad y en la Normal Superior del Estado. En 1960 me integré al Instituto de Botánica de la Universidad de Guadalajara y en 1980 fui nombrada su directora. Durante mi gestión impulsé los estudios florísticos y de vegetación, consolidando a la institución en este campo de investigación.</p>
                                     <p>Otra de mis actividades fue la de promover la conservación de la naturaleza del estado de Jalisco. Participé en el descubrimiento del maíz perenne <em>Zea diploperennis</em> y para protegerlo, ayudé a gestionar la creación de la Reserva de la Biosfera de la Sierra en Manantlán en 1987.</p>
                                     <p>En mis excursiones al campo hice numerosas colectas de la flora tanto del estado de Jalisco como de otros estados, con las cuales aumenté el acervo del herbario del Instituto de Botánica, que a la fecha alberga la colección de plantas más importante del occidente de México. </p>
                                     <p>Fui miembro de varias sociedades científicas dedicadas al desarrollo y difusión de las ciencias biológicas, así como por la conservación de la naturaleza, como la Sociedad Botánica de México, Sociedad Botánica del Estado de Jalisco y la International Asociación Internacional de Taxonomía de Plantas.</p>
                                     <p>El 11 de noviembre del año 2013, ocho meses después de cumplir 100 años, me despedí del planeta. 
