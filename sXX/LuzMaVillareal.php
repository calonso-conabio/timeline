												<h3>1913 - 2013</h3>
											</header>
<p>Luz María Villarreal fue una destacada botánica que nació en 1913 en la ciudad de México. Estudió en el Colegio de las Vizcaínas, en el cual se graduó como profesora normalista.</p>
                                          <p>Su vida profesional comenzó como secretaria para el gobierno, pero en 1959 se mudó a la ciudad de Guadalajara donde cursó una especialización en Biología en la Escuela Normal Superior &ldquo;Nueva Galicia&rdquo; de Guadalajara y más tarde, ahí comenzó a impartir clases de biología, zoología y botánica. </p>
                                          <p>También impartió las cátedras de sistemática botánica, botánica general y ciencias biológicas en escuelas preparatorias de la Universidad de Guadalajara, en la Escuela de Agricultura de la misma universidad y en la Normal Superior del Estado. En 1960 se integró al Instituto de Botánica de la Universidad de Guadalajara y en 1980 fue nombrada su directora. Durante su gestión, impulsó los estudios florísticos y de vegetación, consolidando a la institución en este campo de investigación.</p>
                                          <p>Otra de sus actividades fue la de promover la conservación de la naturaleza del estado de Jalisco. Descubrió el maíz perenne <em>Zea diploperennis</em> y gracias a sus gestiones para su protección, promovió la formación de la Reserva de la Biosfera de la Sierra en Manantlán en 1987.</p>
                                          <p>En sus excursiones al campo hizo numerosas colectas de la flora tanto del estado de Jalisco como de otros estados, con las cuales incrementó el acervo del herbario del Instituto de Botánica, que a la fecha alberga la colección de plantas más importante del occidente de México. </p>
                                          <p>Fue miembro de varias sociedades científicas dedicadas al desarrollo y difusión de las ciencias biológicas, así como por la conservación de la naturaleza, como la Sociedad Botánica de México, Sociedad Botánica del Estado de Jalisco y la International Asociación Internacional de Taxonomía de Plantas.</p>
