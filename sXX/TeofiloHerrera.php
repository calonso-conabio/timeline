												<h2>Teófilo Herrera Suárez</h2>
												<h3>1924 </h3>
											</header>
<p>Nací en la ciudad de México el 24 de febrero de 1924. Mi padre fue Teófilo Herrera Ostos y mi madre Victoria Suárez Robaina. Fuí el menor de cuatro hermanos y el único varón, Catalina, María Elena, Victoria. Ambas familias, la de mi madre y la de mi padre venían de Tantoyuca, Veracruz, &quot;la Perla de las Huastecas&quot;.</p>
<p>Mi interés por la naturaleza me llevó a cursar dos carreras simultáneamente. Estudié biología en la Facultad de Ciencias de la Universidad Nacional Autónoma de México (1945) y casi a la par realicé la carrera de Químico Biólogo Parasitólogo en la <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENCB.php">Escuela Nacional de Ciencias Biológicas </a>del Instituto Politécnico Nacional. En 1954 hice la maestría en ciencias en la Universidad de Wisconsin en la especialidad de bioquímica y microbiología y en 1964 obtuvé el grado de doctor en la Facultad de Ciencias de la UNAM. Uno de mis grandes maestros fue el <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/ManuelRuiz.php">Dr. Manuel Ruiz Oronoz, </a>a quien acompañé a una gran excursión a Cuicatlán, Oaxaca, donde colecté al <a href="http://www.enciclovida.mx/especies/3005410">hongo soldadito <i>(Podaxis pistillaris)</i></a>.</p>
<p>En 1955 conocí a <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/GastonGuzman.php">Gastón Guzmán</a> gracias a la presentación que nos hizo el <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/AlfredoBarrera.php">Dr. Alfredo Barrera </a>¡eramos los dos únicos estudiosos de los hongos en el país! Con él inicié la Sociedad Mexicana de Micología de la cual fuí el primer presidente. Tuvimos un gran apoyo de las Doctoras Celia Dubovoy y Martha Zenteno.<<p>
<p> He explorado gran parte del país, por lo que adquirí un vasto conocimiento sobre la vegetación del país. De hecho, colaboré con la elaboración de 1,000 fichas en doce tomos sobre la flora de México. En 1967 junto con mi discípulo el <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/MiguelAUlloa.php">Dr. Miguel Ulloa </a>conocí a María Sabina en  Huautla de Jiménez, Oaxaca y participamos en una ceremonia con hongos alucinógenos. La experiencia la publiqué en un artículo científico.</p>
<p>En revistas científicas nacionales e internacionales he publicado alrededor de 118 artículos derivados de su labor de investigación, la mayoría tratan de hongos y el resto sobre bacterias u otros temas. También me ha dedicado a la docencia en distintos niveles educativos. Me inspiró el <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/ManuelRuiz.php">Dr. Ruiz Oronoz </a>y he impartido las asignaturas de microbiología, bacteriología y micología, entre otras. En el campo de la divulgación he escrito varios libros de textos sobre estos temas.</p>
 <p>Fuí cofundador de la sección de macromicetos en el Herbario Nacional y de la Sociedad Latinoamericana de Microbiología y del Jardín Botánico “Faustino Miranda” de la UNAM. Me adentré en el conocimiento sobre la microbiología de alimentos y bebidas fermentadas indígenas como el pulque, tepache y tesgüino.</p>
