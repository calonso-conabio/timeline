												<h2>José Álvarez del Villar</h2>
												<h3>1908-1989</h3>
											</header>
<p>Nací en Michoacán y ahí pasó mi juventud. Tuve una formación académica muy nutrida que me llevó a mudarme varias veces. Primero viajé a la Universidad de California, Estados Unidos, donde me diplomó como técnico agrónomo. Más tarde me mudé a la ciudad de México para cursar la carrera de químico-biólogo-parasitólogo en la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional (IPN), y luego la carrera de biología en la misma institución (1938).</p>
<p>A partir de entonces, me interesé estudiar la diversidad de peces y su cultivo para fines comerciales, así que regresé a Estados Unidos donde hice una especialidad en piscicultura en el Instituto Politécnico de Alabama y  la Maestría en Ciencias en la Universidad de Míchigan. De regreso a tierras mexicanas, hice el doctorado en Ciencias de nuevo en el IPN.</p>
<p>Mis investigaciones se enfocaron principalmente a estudiar la composición y la distribución de los peces de diferentes cuerpos de agua dulce del país, en particular de mi estado natal; incluso realice estudios sobre peces fósiles. También me interesé hacer estudios sobre la piscicultura como fuente de alimentación rural.</p>
<p>A partir de toda esta la información escribí el “Catálogo de los Peces de las Aguas continentales mexicanas” y un par de manuales para la identificación y la clasificación de los peces. </p>
<p>Fuí fundador y miembro de la Sociedad Mexicana de Hidrobiología. Tuve varios cargos administrativos: fui consultor de la Secretaría de Marina, asesor del Museo de Historia Natural de la Ciudad de México, y jefe de la Comisión para el Fomento de la Piscicultura Rural.</p>
<p>Gran parte de mi vida también la dediqué a la docencia, incluso escribí dos libros de texto que por muchos años fueron ampliamente consultados. Mi hijo Ticul Álvarez Solorzano (1935-2001) y mi nieto Ticul Álvarez Castañeda (1964 - ) se dedicaron también a estudiar la naturaleza.</p>
