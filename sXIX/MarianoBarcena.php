												<h2>Mariano de la Bárcena y Ramos</h2>
												<h3>1842 - 1899</h3>
											</header>
<p>Nací con el nombre de Mariano Santiago de Jesús de la Bárcena Ramos en Ameca, Jalisco el 22 de julio de 1842. Mi padre fue José María de la Bárcena y Villaseñor y mi madre Candelaria Ramos Celis.</p>
<p>En 1864 inicié estudios en la Real Academia de San Carlos y más tarde continué en la Escuela Nacional Preparatoria. Ahi tuve como maestros a Gabino Barreda y a Leopoldo Río de la Loza. Me encantaba la pintura y la música. Posteriormente pasé a ser estudiante de Ingeniería en la Escuela de Minas. Fui miembro de la Sociedad Filomatemática Mexicana, de la <strong><a href="SMHN.php">Sociedad Mexicana de Historia Natural</a></strong>&quot; y socio de la Sociedad Mexicana de Geografía y Estadística.</p>
<p>En el campo de las ciencias naturales describí y clasifiqué especies vegetales y di nombre a dos especies fósiles. Mi maestro <a href="AntonioCastillo.php"><strong>Antonio del Castillo</strong></a> me contagió su pasión por el estudio de los minerales. Junto con él descubrí al &quot;Hombre del Peñón&quot; en el Cerro de Tepetzinco o Peñon de los Baños en el valle de México, cerca del actual aeropuerto, al cual le asignamos más de 8 mil años.</p>
<p>Además, por mi trabajo en la Secretaría de Fomento, participé en diferentes comisiones de exploración a lo largo del territorio mexicano. De la información recabada derivaron mis tratados de geología, paleontología y litología: Noticia geológica del estado de Aguascalientes, Las obsidianas de México, Datos para el estudio de las rocas mesozoicas de México y sus fósiles y Noticia acerca del hallazgo de restos humanos prehistóricos en el valle de México.</p>
<p>En 1877 se creó el Observatorio Meteorológico Central, precursor del Servicio Meteorológico Nacional, del cual fuí director hasta mi muerte. Ahí coordiné las investigaciones sobre el clima y el tiempo; creía que la observación minuciosa de éstos durante años, nos daría elementos para establecer reglas sobre los fenómenos periódicos de la vegetación y se podrían establecer “calendarios botánicos”.</p>
<p>En 1882 me casé en Guadalajara con Soledad de los Ríos y Arias y tuvimos tres hijas: Ana María, Rosa y Cristina.</p>
<p>También desempeñé cargos en la política, fuí nombrado secretario de gobierno de Jalisco así como gobernador de ese estado por un breve periodo en 1889. Fallecí el 10 de abril de 1899 en mi mismo estado.</p>
