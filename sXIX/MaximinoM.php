												<h2>Maximino Martínez</h2>
												<h3>1888 - 1964</h3>
											</header>
<p>Nací en 1888 en San Miguel Regla, estado de Hidalgo. Cuando tenía 3 años murió mi padre, así que mi mamá se encargó totalmente de mi educación. Estudié en Hidalgo y ahí obtuve el título de Profesor de Instrucción Primaria. En 1908 me mudé a la ciudad de México para estudiar en la Escuela Normal donde me recibí y me convertí en profesor.</p>
<p>En 1907, a los 19 años empecé mi vida con las actividades de enseñanza que mantuve durante 38 años. Di clases de geografía, matemáticas, biología y botánica. Además, de 1914 a 1929 estuve trabajando como Encargado de la Sección de Botánica del Museo Nacional de Historia Natural. El director era mi maestro Alfonso Luis Herrera, de quien tomé clases de biología en la Escuela Normal de Maestros. Más adelante en 1915 trabajé también con él en la Dirección de Estudios Biológicos de la Secretaría de Fomento que coordinaba las labores de investigación. Ahí mi puesto era de Jefe de la sección de Botánica</p>
<p>La botánica era mi fascinación y aprendí esa ciencia por mi propio esfuerzo. Mi principal interés fueron los grupos forestales de los pinos, piceas, romerillos y encinos, de los que describí  varias especies nuevas e hice estudios sobre su distribución geográfica.</p>
<p>En 1941 junto con un grupo de investigadores fundé la Sociedad Botánica de México, fui su director por varios años y editor del boletín de la sociedad durante 14 años. También fuí investigador al Instituto de Biología de la Universidad Nacional Autónoma de México e impartí clases en la Escuela Nacional Forestal y en la Escuela Nacional de Ciencias Biológicas del Politécnico Nacional, donde fundé un Herbario.</p>
<p>Entre mis trabajos se encuentra el libro Catálogo de nombres vulgares y científicos de plantas mexicanas (1923), donde incluí los nombres de las plantas en náhuatl y que tuvo posteriores ediciones aumentadas: Las plantas más útiles que existen en la República Mexicana (1928) y Las plantas medicinales de México (1934), obra considerada de gran importancia por la vasta información que contiene sobre la herbolaria mexicana.</p>
<p>Recibí el grado de Doctor Honoris Causa de la Universidad Latinoamericana de la Habana, Cuba en 1956. Fallecí en la Ciudad de México en 1964.</p>
