												<h2>Maximino Martínez y Martínez</h2>
												<h3>1888 - 1964</h3>
											</header>
<p>Nací el 30 de mayo de 1888 en San Miguel Regla, estado de Hidalgo. Cuando tenía 3 años murió mi padre, así que mi mamá se encargó totalmente de mi educación. Estudié en Hidalgo y ahí obtuve el título de Profesor de Instrucción Primaria. En 1908 me mudé a la ciudad de México para estudiar en la Escuela Normal donde me recibí y me convertí en profesor.</p>
<p>En 1907, a los 19 años empecé mi vida con las actividades de enseñanza que mantuve durante 38 años. Di clases de geografía, matemáticas, biología y botánica. Además, de 1914 a 1929 estuve trabajando como Encargado de la Sección de Botánica del <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/MNHN.php">Museo Nacional de Historia Natural. </a>El director era mi maestro <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfonsoLHerrera.php">Alfonso Luis Herrera, </a>de quien tomé clases de biología en la Escuela Normal de Maestros. Más adelante en 1915 trabajé también con él en la Dirección de Estudios Biológicos de la Secretaría de Fomento que coordinaba las labores de investigación. Ahí mi puesto era de Jefe de la sección de Botánica</p>
<p>La botánica era mi fascinación y aprendí esa ciencia por mi propio esfuerzo. Mi principal interés fueron los grupos forestales de los pinos, piceas, romerillos y encinos, de los que describí varias especies nuevas e hice estudios sobre su distribución geográfica. En el mundo se han descrito alrededor de 100 especies de pinos que habitan principalmente en el Hemisferio Norte. México posee la mitad de estas especies, de los cuales 17 especies son endémicas de nuestro país. </p>
<p>En 1941 junto con un grupo de investigadores fundé la <a href="http://www.socbot.mx/">Sociedad Botánica de México, </a>fui su director por varios años y editor del boletín de la sociedad durante 14 años. También fuí investigador al Instituto de Biología de la Universidad Nacional Autónoma de México e impartí clases en la Escuela Nacional Forestal y en la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENCB.php">Escuela Nacional de Ciencias Biológicas </a>del Politécnico Nacional, donde fundé su herbario en 1943.</p>
<p>Entre mis trabajos se encuentra el libro Catálogo de nombres vulgares y científicos de plantas mexicanas (1923), donde incluí los nombres de las plantas en náhuatl y que tuvo posteriores ediciones aumentadas: Las plantas más útiles que existen en la República Mexicana (1928) y Las plantas medicinales de México (1934), obra considerada de gran importancia por la vasta información que contiene sobre la herbolaria mexicana.</p>
<p>Recibí el grado de Doctor Honoris Causa de la Universidad Latinoamericana de la Habana, Cuba en 1956. Fallecí el 2 de junio de 1964 en la Ciudad de México. Precisamente ese año, el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/JerzyRzedowski.php">Dr. Jerzi Rzedowski </a>describió una hermosa especies endémica de pino, con una piña gigantesca, de la Sierra de Morones en Zacatecas a la que llamó <a href="http://www.enciclovida.mx/especies/6019349"><i>Pinus maximartinezii, </i> </a>en mi honor, ahora conocido como pino azul.</p>
