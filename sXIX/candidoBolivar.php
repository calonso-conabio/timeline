												<h2>Cándido Bolívar y Pieltain</h2>
												<h3>1897 - 1976</h3>
											</header>
<p>Nací el 15 de abril de 1897 en Madrid, España. Desde temprana edad, mi padre Ignacio Bolivar y Urrutía (1850-1944) me inició en el estudio de la naturaleza y particularmente en la investigación entomológica. El era naturalista y entomólogo, investigador de la Universidad Central de Madrid y director del Museo de Ciencias Naturales.</p>
<p>Obtuvé la licenciatura (1917) y el doctorado (1920) en Ciencias por la Universidad de Madrid. Fuí jefe de la sección de entomología y secretario del Museo de Ciencias Naturales. También me dediqué a la docencia en la Universidad de Madrid y junto con mi padre fundó la revista Eos.</p>
<p>El 2 de enero de 1924 me casé con la también madrileña Amelia Goyanes Echegoyen, hija del ilustre cirujano Dr. José Goyanes. Tuvimos seis hijos, cuatro nacieron en España (José Ignacio, Amelia, María Luisa y Ana María) y dos nacieron en México (Simón y Antonio). José Ignacio, el mayor se dedicó a la química, fundó la Sociedad Química de México y la Revista de la Sociedad (1924-1982). Antonio, el más pequeño, fundó en 1974 el taller tipográfico Redacta y por más de 40 años ha contribuido a la edición y publicación de una inmensa variedad de textos científicos. </p>
<p>Llegué a México en 1939 contratado por la Secretaría de Salubridad, con el objetivo de participar en la investigación de algunas enfermedades transmitidas por insectos, como el paludismo y la oncocercosis en Chiapas. La oncocercosis es una enfermedad causada por un gusano nemátodo que es transmitida por varias especies de "moscas negras" conocidas como simúlidos. Estas pequeñas moscas de 2 a 5 mm de tamaño, viven cerca de los ríos. Algunas especies son hematófagas y muerden a las personas y animales para obtener sangre. Al morder inyectan un anestésico, un vasodilatador y un anticoagulante que les permite alimentarse sin preocupación. Durante su alimentación, pueden contaminar la sangre con el gusano parásito, el cual puede invadir los ojos y causar ceguera. Afortunadamente, gracias a la investigación y al control esta enfermedad ha ido disminuyendo y se ha erradicado de distintas regiones.</p>
<p>Junto con mi padre, fui cofundador en 1940 de la revista <b><a href="http://www.edaddeplata.org/tierrafirme_jae/revistaciencia/index.html">Ciencia, </b> </a>revista hispano-americana de ciencias puras y aplicadas, y fui uno de sus redactores principales. La revista se publicó desde 1940 hasta 1975. También me dediqué a la docencia en la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENCB.php">Escuela Nacional de Ciencias Biológicas</a> del Instituto Politécnico Nacional, donde ingresé en 1941 y organicé el laboratorio de Entomología en el cual dejé importantes colecciones de insectos.</p>
<p>Me interesó el estudio de la fauna cavernícola y de las altas cumbres de los volcanes mexicanos, por lo cual exploré numerosas grutas del país junto con mi amigo <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/FedericoBonet.php">Federico Bonet</a> como las grutas de Villa García, Palmito y Cueva del Carrizal en Nuevo León y las grutas de Cacahuamilpa y la Boca del Diablo en Guerrero. Publiqué una serie de trabajos científicos sobre insectos de México. Formé parte de la <a href"=http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural,</a> y fuí su presidente en 1947. También fundé el Grupo Espeleológico Mexicano y la Asociación Mexicana de Espeleología.</p>
<p>Desarrollé toda una escuela de entomólogos mexicanos y españoles y recibí el título de Doctor honoris causa de la Universidad Nacional Autónoma de México. Fallecí en la ciudad de México el 22 de noviembre de 1976. </p>
