												<h3>1859 - 1929</h3>
											</header>
<p>Nicolás León Calderón fue un estudioso muy completo, médico de profesión, combinó su labor con intereses humanísticos, fue arqueólogo, etnólogo, antropólogo, historiador de la medicina, lingüista, bibliógrafo y además botánico. Nació en 1859 en Quiroga, estado de Michoacán y murió en Oaxaca en 1929.</p>
                                          <p>Su vida académica transcurrió en su estado natal, donde se recibió de médico cirujano en el Colegio de San Nicolás de Hidalgo en 1883. Comenzó a laborar como profesor de botánica y latín, y para 1886 fundó el Museo Michoacano, del que fue director y editor de los <em>Anales</em> por algunos años.<strong> </strong> </p>
                                          <p>Después de vivir una temporada en Oaxaca donde reorganizó el Museo Oaxaqueño, en 1892 cambió su residencia a la Ciudad de México y para 1899 se incorporó como investigador al Museo Nacional Mexicano, hoy Museo Nacional de Antropología. También fue miembro de la Academia Nacional de Medicina y su director en 1921.</p>
                                          <p>Una de sus grandes obras fue <em>Biblioteca botánico-mexicana: Catálogo bibliográfico, biográfico y crítico de actores y escritos referentes a vegetales de México y sus aplicaciones desde la Conquista</em> (1895), obra en la cual hace un compendio de las publicaciones de un gran número de científicos, desde el siglo XVI hasta XIX, con temas relacionados principalmente a la farmacología vegetal. Escribió alrededor de 500 trabajos, entre libros, artículos, traducciones, reimpresiones de otros autores y estudios inéditos.</p>
