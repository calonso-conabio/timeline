												<h2>Nicolás León</h2>
												<h3>1859 - 1929</h3>
											</header>
<p>¡Soy un orgulloso mexicano nacido en Villa de Cocupao, hoy Quiroga, en el bello estado de Michoacán! que en náhuatl significa tierra de pescadores. Mi padre se llamó Nicolás León y ZárateDesde niño fui siempre muy observador de la naturaleza particularmente de la flora y de las culturas que vivían en mi estado. Por cierto, cuenta la leyenda “que la princesa purépecha Eréndira dio batalla a los españoles quitándoles un caballo para después montarlo y defender a su pueblo con una espada española”.</p>
<p>Estudié en la ciudad de Pátzcuaro y después en Morelia en la Escuela de Medicina, de donde me gradué en 1883. Trabajé en el Hospital Civil de Morelia y di clases de Patología en mi alma mater.</p>
<p>Así, enamorado de mí país, sus culturas y sus distintas plantas además de ejercer mi profesión como médico practiqué la arqueología, antropología. Fui maestro, historiador, botánico y etnólogo que es la ciencia que se dedica al estudio de las distintas culturas que existen. Cuentan por ahí que la Universidad de Princeton en Estados Unidos se interesó por saber mi capacidad intelectual.</p>
<p>Producto de mis excursiones por los caminos y veredas de Michoacán, en el año de 1886 fui nombrado director del Museo Michoacano exhibiendo pequeñas colecciones de arqueología, plantas y animales. Actualmente en este museo se puede conocer el escenario ecológico del estado como su clima, tipo de suelo, flora y fauna, los aspectos más importantes de su historia, desde la época prehispánica hasta la modernidad. En diciembre de ese año nacería en Guanajuato un gran muralista mexicano reconocido a nivel mundial de nombre Diego Rivera Barrientos. A quien le gustará plasmar en sus murales motivos de contenido social.</p>
<p>“La Biblioteca botánico-mexicana: Catálogo bibliográfico, biográfico y crítico de actores y escritos referentes a vegetales de México y sus aplicaciones desde la Conquista (1895)”, es una de mis grandes obras ya que es un compendio de las publicaciones de distintos científicos, que desde el siglo XVI hasta XIX, escribieron sobre temas relacionados principalmente a la farmacología vegetal.</p>
<p>Después de vivir una temporada en Oaxaca, reorganicé el Museo Oaxaqueño. Por el año de 1899 y me incorporé como investigador al Museo Nacional Mexicano, que hoy es el majestuoso Museo Nacional de Antropología.</p>
<p>En 1929 durante una visita que hacía a la ciudad de Oaxaca me dio una fuerte pulmonía y ya no me recuperé.</p>
