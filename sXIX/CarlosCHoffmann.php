												<h2>Carlos Cristian Hoffmann</h2>
												<h3>1876 - 1942</h3>
											</header>
							<p>Me encantaban los insectos y los arácnidos y también los parásitos. Nací en la ciudad de Frankfurt en Alemania en 1876 donde estudié. Mi papá fue Jorge Felipe Hoffmann y mi mamá Anna Margaretha Nuss. En 1899 hice un inolvidable viaje para explorar los Alpes en el norte de Italia con algunos de mis profesores. </p>
							<p>A los 25 años llegué a México junto con el siglo XX en 1901. En 1907 me casé con Ida Iversen Sartorius, hija del escritor alemán Carl Christian Wilhelm Sartorius (1796-1872). Tristemente, Ida ´falleció dos años más tarde poco después del nacimiento de nuestra hija. Pasaron nueve años y volví a casarme con Esperanza Mendizábal Benítez de Puebla. De este matrimonio nació <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/AnaHoffman.php">Anita, </a>quien desde pequeña tuvo gran interés por los insectos y la naturaleza en general. </p>
							<p>Años más tarde (1923) ingresé a trabajar como profesor en la Universidad Nacional Autónoma de México. En esta institución, en 1929 fui miembro fundador del Instituto de Biología y en 1939 participé en la fundación de la Facultad de Ciencias, donde inicié la asignatura de artrópodos.</p>
                             <p>Realicé estudios sobre el hábitat y distribución geográfica de algunos artrópodos transmisores de agentes patógenos, como los moscos que transmiten la oncocercosis, malaria y la fiebre amarilla, incluso describé especies no conocidas anteriormente. La oncocercosis es una enfermedad producida por un gusano nemátodo <i>(Onchocerca volvulus)</i> que es es transmitida por las moscas negras. El médico guatemalteco Rodolfo Robles Valverde la descubrió en 1915. La enfermedad se conoce también como ceguera de los ríos, ya que en ocasiones los nemátodos pueden migrar a la córnea de los ojos produciendo ceguera. En este campo trabaje para la Secretaría de Salubridad Pública en la Sección de Parasitología del Instituto de Higiene, principal centro de investigación médico-biológica del país. </p>
                             <p>Me fascinaban las mariposas y a lo largo de mis viajes por todo el país formé una gran colección de estos insectos. Publiqué numerosos trabajos siendo el más importante el <em>Catálogo sistemático y zoogeográfico de los lepidópteros mexicanos</em>, que se volvió en obra imprescindible de consulta para los especialistas.</p>
                             <p>También realicé un trabajo monográfico sobre los alacranes del país y su grado de toxicidad, titulado <em>Escorpiones de México</em>. Morí en en la ciudad de México el 26 de noviembre de 1942. Fuí considerado el iniciador de la moderna entomología médica en México. . </p>
