												<h2>Carlos Cristian Hoffmann</h2>
												<h3>1876 - 1942</h3>
											</header>
<p>Me encantaban los insectos y los arácnidos y también los parásitos. Nací en la ciudad de Frankfurt en Alemania en 1876 donde estudié. A los 25 años llegue a México junto con el siglo XX en 1901. Años más tarde (1923) ingresé a trabajar como profesor en la Universidad Nacional Autónoma de México. En esta institución, en 1929 fui miembro fundador del Instituto de Biología y en 1939 participé en la fundación de la Facultad de Ciencias, donde inicié la asignatura de artrópodos.</p>
                                          <p>Realicé estudios sobre el hábitat y distribución geográfica de algunos artrópodos transmisores de agentes patógenos, como los moscos que transmiten la oncocercosis, malaria y la fiebre amarilla, incluso describé especies no conocidas anteriormente. En este campo trabaje para la Secretaría de Salubridad Pública en la Sección de Parasitología del Instituto de Higiene, principal centro de investigación médico-biológica del país. </p>
                                          <p>Me fascinaban las mariposas y a lo largo de mis viajes por todo el país formé una gran colección de estos insectos. Publiqué numerosos trabajos siendo el más importante el <em>Catálogo sistemático y zoogeográfico de los lepidópteros mexicanos</em>, que se volvió en obra imprescindible de consulta para los especialistas.</p>
                                          <p>También realicé un trabajo monográfico sobre los alacranes del país y su grado de toxicidad, titulado <em>Escorpiones de México</em>. Morí en en la ciudad de México en 1942. Fuí considerado el iniciador de la moderna entomología médica en México. . </p>
