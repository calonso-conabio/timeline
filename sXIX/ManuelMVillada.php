												<h3>1840 - 1912</h3>
											</header>
<p>Manuel María Villada fue un sabio naturalista que nació en la ciudad de México en 1841 y falleció en 1924.</p>
                                          <p>Estudió &ldquo;Latinidad y Filosofía&rdquo;  en el Colegio de San Juan de Letrán y posteriormente física, química e historia natural en la Escuela Nacional de Medicina, donde se recibió en 1863. En 1864 participó en la Comisión Científica de Pachuca, proyecto del gobierno y de los intelectuales de la época para conocer las poblaciones humanas,  la fauna y flora de la región del hoy estado de Hidalgo. </p>
                                          <p>Fue miembro fundador de la Sociedad Mexicana de Historia Natural, creada en 1868, la cual fue una de las asociaciones científicas más importantes del siglo XIX. <em>La Naturaleza</em>, órgano informativo de la sociedad, fue dirigida y editada por Villada durante todos los años que ésta existió (1890-1914) y fue uno de sus autores más prolíficos.</p>
                                          <p>También formó parte de otros centros educativos y de investigación científica como la Escuela Nacional de Agricultura, donde ingresó en 1868 y miembro de la sección de historia natural de la Academia Nacional de Medicina en 1886. Dirigió en 1881 el Instituto Científico Literario de Toluca y promovió en éste, la creación del primer Observatorio Meteorológico del estado de México.</p>
                                          <p>Posteriormente, en 1886,  ingresó al Museo Nacional donde se desempeñó como investigador y profesor de mineralogía, botánica, geología, paleontología y botánica. Además ahí aumentó la colección de fósiles y elaboró el primer catálogo sobre éstos.</p>
                                          <p>La diversidad temática de sus trabajos fue muy amplia, y fueron dados a conocer en las publicaciones propias de cada institución de la que fue miembro. En especial se interesó por las propiedades medicinales de algunas plantas y por el estudio de las aves.</p>
