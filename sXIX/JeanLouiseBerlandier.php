												<h2>Jean Louis Berlandier</h2>
												<h3>1805 - 1851</h3>
											</header>
<p>Nací en un pueblo de Francia cercano a Suiza llamado Fort l´Ecluse alrededor de 1805. Estudié botánica en Ginebra. Aunque me inicié como estudiante de farmacia, poco a poco me fui interesando en el conocimiento de la historia natural de las plantas. El gran botánico suizo Augustin Pyramus de Candolle (1778-1841) me recomendó con su estudiante el político y naturalista Lucas Alamán y Escalada (1792-1853) para que me aceptaran en una expedición como biólogo con rumbo a México en el barco "Le Havre de Grace". Con la expedición llegué al puerto de Pánuco, en lo que hoy es el estado de Veracruz en diciembre de 1826, a los 24 años siguiendo los pasos de mi ídolo <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/AlexanderHumboldt.php">Alexander Von Humboldt.</a> </p>
<p>Mi maestro Candolle, tuvo el honor de conocer a <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/JoseMMucino.php"> José Mariano Mociño </a>en 1816 en Francia. Cuando Mociño le enseñó las ilustraciones de la Real Expedición a la Nueva España, Candolle contrató al talentoso artista Jean Christophe Heyland, para que reprodujera las ilustraciones. Yo tuve la suerte de aprender el arte de la ilustración de este gran artista.</p>				
<p>En 1826 inicié mis trabajos de campo en México, como botánico y zoológo. Un par de años más tarde ya andaba por Texas, cerca de San Antonio, en un viaje de cacería acompañando al coronel José Francisco Ruiz, junto con varios indios comanches. Además de mi curiosidad por las plantas, aquí se inició mi interés por las costumbres de los indígenas. A partir de esta experiencia escribí el libro "Indigenes nomades des Etats Internes d´Orient et d´Occident des territoires du Nouveaeu Mexique et des deux Californies".</p>
<p>Participe con la Comisión de Límites Mexicana que se formó en 1848 después de la guerra con Estados Unidos, donde México perdió los estados de Alta California, Nuevo México y parte de los estdos de Tamaulipas, Coahuila y Sonora, reduciendo su territorio a menos de la mitad. La frontera se trazó entre 1849 y 1857. </p>
<p>Me establecí en Matamoros, Tamaulipas donde trabajé como médico y farmacéutico. Aprovechaba mis excursiones para colectar plantas medicinales además de todas las otras especies de plantas y animales que me interesaban. También me encantaba la ilustración y dibujé muchas de las especies que colecté. Me casé con una mexicana y tuvimos varios hijos, nuestra casa en Matamoros era un museo lleno de colecciones de insectos, invertebrados, plantas, minerales,  pinturas y manuscritos. Todo eso fue comprado por el teniente Darius Nash Couch por la suma de $500 dólares y llevado parcialmente con el herpetólogo Spencer Fullerton Baird (1823-1887) al Instituto Smithsonian, y con el botánico Asa Gray a la Universidad de Harvard.  </p>
<p>Fallecí en 1851 a la escasa edad de 46 años al cruzar el río San Fernando a caballo. Por la amplitud de mis intereses, fui considerado antropólogo, geógrafo, historiador, meteorólogo y naturalista y uno de los primeros científicos exploradores en el noreste de México y en el sur de Texas. Muchas especies me han sido dedicadas y llevan el nombre de <i>berlandieri </i></p></section>

