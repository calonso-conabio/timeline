												<h2>Fernando Altamirano Carbajal</h2>
												<h3>1848-1908</h3>
											</header>
											<p>Nací en el poblado de Aculco den el norte del Estado de México el 7 de julio de 1848. Mi padre fue Manuel Altamirano y Téllez y mi madre Micaela Carbajal Castillo. De ese matrimonio fuimos tres, Federico, Alberto y yo, pero tuve siete medios hermanos, ya que mi padre estuvo casado anteriormente con María Guadalupe Monteverde y Belaunzarán y despúes de que falleció mi madre contrajo nupcias con Guadalupe Ruiz.</p>
											<p> A los dos años nos mudamos a San Juan del Río y más tarde a la Ciudad de Querétaro donde crecí. A los trece años perdí a mi padre y a mi madre y quedé a cargo de mi querido abuelo Manuel. </p> 
											<p>A los 20 años me fui a la Ciudad de México a estudiar en la Escuela Nacional Preparatoria y de ahi pasé a la Escuela Nacional de Medicina. Desde pequeño, en los recorridos con mi abuelo de la Hacienda la Nopalera cerca de  Cadereyta de Montes, en el estado de Querétaro, me había interesado la botánica y las plantas medicinales. A los 25 años me casé con Luisa González Mancera en Querétaro y formamos una gran famiia de 10 hijos, siete hombres y tres mujeres.</p>
											<p>Fui miembro de la Academia Nacional de Medicina de México y de la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural. </a>Con ésta última reuní el catálogo de la colección de productos naturales indígenas que se presentó en la Exposición Universal de Filadelfia en 1876. </p>
											<p>Fui el primer director del Instituto Médico Nacional donde permanecí hasta el final de mi vida. Ahi realicé investigaciones sobre fisiología, botánica y zoología. Descubrí que las semillas rojas del colorín <a href="http://www.enciclovida.mx/especies/6050297"><i>(Erythrina coralloides) </i> </a>contenían un alcaloide que llamé erythroidina. Su actividad era colinérgica, es decir, que esta sustancia podía modificar la acción del neurotransmisor  acetilcolina y tener efectos sobre éste. </p>
											<p>En el área de la botánica, junto con mi gran amigo <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/JoseRamirez.php">José Ramírez, </a>escribí el libro "Lista de los nombres vulgares y botánicos de árboles y arbustos propios para repoblar los bosques de la República, acompañados de la indicación de los climas en que vegetan y de la manera de propagarlos".</p>
											<p>En la Sierra de las Cruces, que divide la Cuenca de México y la Cuenca de Lerma descubrí a un ajolote. Inmediatamente envié un especimen al zoólogo francés <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfredoDuges.php">Alfredo Dugés, </a>quien lo describió como una nueva especie y me lo dedicó. Ahora se conoce como ajolote de Zempoala y su nombre científico es <a href="http://www.enciclovida.mx/especies/8012770"><i> Ambystoma altamirani.</i></a>
											<p>También me interesé por los conocimientos de los pueblos antiguos y publiqué "Historia Natural Aplicada a los Antiguos Mexicanos". Traduje del latín la obra de <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVI/FcoHToledo.php">Francisco Hernández de Toledo. </a> En 1898 consiguí en Europa una copia de la colección de los manuscritos del ilustre naturalista <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/JoseMMucino.php">José Mariano Mociño. </a>

											<p>Fallecí el 7 de octubre de 1897 debido a una hemorragia interna.  


