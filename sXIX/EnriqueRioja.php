												<h2>Enrique Rioja Lo-Bianco</h2>
												<h3>1895 - 1963</h3>
											</header>
<p>¡Que tal amigos!, soy Enrique Rioja Lo-Bianco, español que nació en Santander el 16 de febrero de 1895, hijo del matrimonio entre el zoólogo José Rioja Marín y Ninfa Lo-Bianco de origen italiano. Mi inclinación por la biología marina se debe a la formación de mi papá y a su trabajo en la Estación Marina de Santander donde empecé a recolectar con él a manera de juego algunos organismos marinos cuando lo acompañaba a su trabajo, después en mis clases en la secundaria reforcé más mi interés y finalmente cuando conocí a mi tío Salvatore Lo-Bianco que trabajaba como jefe del acuario del laboratorio en Nápoles, Italia. Y con quien después intercambié información sobre los invertebrados marinos.</p>
<p>En el año de 1917 empecé a publicar mis primeros trabajos que trataban sobre gusanos marinos que los científicos llaman poliquetos así como otros invertebrados marinos, con el paso de los años y después de mucho trabajo y esfuerzo en el área académica fui miembro de la Junta Directiva de la Sociedad Española de Historia Natural. Me distinguí no solo por haber descubierto nuevas especies sino porque fui autor de libros enfocados a la enseñanza. Me propuse que desde los niños y hasta las personas que estudian el posgrado de las Ciencias Naturales tuvieran libros adecuados para una mejor comprensión del tema. Estuve rodeado de muchas personas de renombre como mi querido maestro y amigo el entomólogo Ignacio Bolivar. Con quien compartí incluso el exilio a México producto de la Guerra Civil Española.</p>
<p>Esta Guerra inició principalmente porque los españoles votamos porque el Rey Alfonso XIII abdicara y tuviéramos un gobierno republicano, en otras palabras democracia. Pero entre las personas que querían la república existían intereses políticos distintos además de los grupos que no la querían o bien de aquellos que se proclamaron nacionalistas. El resultado fue una guerra muy cruel entre españoles. En 1939 llegué a este país por la solidaridad que dio el presidente mexicano General Lázaro Cárdenas del Río (1895-1970 a intelectuales, científicos, pintores, escritores y niños huérfanos llegados de España.</p>
<p>Gracias al Dr. Isaac Ochoterena (1885-1950) fui aceptado como investigador en el Instituto de Biología de la UNAM. Ocupé la jefatura del Departamento de Hidrobiología donde inicié el Laboratorio de Hidrobiología para consolidarlo como centro de investigación de importancia para México.</p>
<p>Participé junto con mi querido compatriota Faustino Miranda (1905-1964) en la estructuración de los planes de estudio de la Facultad de Ciencias de la UNAM que consistió principalmente en integrar materias como las matemáticas, física, química y climatología a la carrera de Biología. Teníamos la convicción de dejar huella con nuestro trabajo. También me involucré en la preparación de la revista llamada los Anales del Instituto de Biología y en los planes para la creación del Museo de Historia Natural.</p>
<p>Realicé varias exploraciones a lo largo de las bellas costas del Pacífico mexicano y encontré más de 300 especies de poliquetos, algunas de ellas ni se conocían y de las cuales más de 43 son válidas aún en la actualidad.</p>
<p>Escribí más de 200 trabajos de investigación sobre mis adorados poliquetos, crustáceos, esponjas de agua dulce y temas variados de hidrobiología. Varios de estos trabajos contienen ilustraciones realizadas por mí. También di clases en la UNAM como en el Instituto Politécnico Nacional, y escribí libros de texto y de divulgación como el Tratado elemental de zoología, La vida en el mar, Zoología hispanoamericana (invertebrados) y El mar, acuario del mundo, entre otros.</p>
<p>En el año de 1960 participé en las reuniones de biología marina que organizó el Centro de Cooperación Científica de la UNESCO y me estaba interesando y entusiasmando un proyecto sobre la enseñanza de las Ciencias Marinas y Biogeográficas a nivel Latinoamérica, desafortunadamente un cáncer en mi hígado lo prohibió definitivamente y un 20 de septiembre de 1963 dejé de existir.</p>
<p>Mi compañero Faustino Miranda (1905-1964) fue de las personas que resintió mi ausencia pero les dejo mis obras dedicadas a México lindo y querido.</p>
