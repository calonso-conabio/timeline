												<h2>Alfonso Luis Herrera </h2>
												<h3>1868 - 1942</h3>
											</header>
<p>La avenida Paseo de la Reforma que en tiempos del emperador Maximiliano de Habsburgo (1864-1867) se llamó Paseo de la Emperatriz y que fue diseñado por ingeniero austriaco-francés Luis Bolland Kuhmackl, solo era transitable para los aristócratas. Sin embargo en el año de 1868 esto cambió y se abrió a todo el público. Al año siguiente, el 3 de julio de 1860 mi madre Adela López Hernández me dio a luz en esta bella Ciudad de México. Fui el segundo de tres hermanos: Román Carlos María Agustín, el primero, y María Adelaida Braulia Ángela, la tercera. Es esa época los nombres salían baratos. Mi padre fue <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfonsoHerrera.php">Alfonso Herrera Fernández de San Salvador </a>(1838-1901) a él debo mucho mi acercamiento hacia la biología ya que crecí viendo todas las innovaciones de estudio que realizó como farmacéutico, naturalista y director en la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENP.php">Escuela Nacional Preparatoria.</a></p>
<p>Obtuve el grado de farmacéutico en la Escuela Nacional de Medicina y al ser ayudante en el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/MNHN.php">Museo Nacional de Historia Natural </a>en 1889 me interesé aún más en la biología. Ahí realicé una serie de catálogos de diferentes colecciones de vertebrados existentes en el museo. En este trabajo me ayudó mucho haber sido alumno y amigo del notable naturalista francés <a href"http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfredoDuges.php">Alfredo Dugès </a>(1826-1910). A la edad de 29 años y siendo el año de 1897 el amor llegó a mi vida y después de enamorarla me casé con la señorita María Estrada Delgado. Tuvimos dos hijos: Rafael Faustino Juan Herrera Estrada y Lucía Melesia Herrera Estrada.</p>
<p>En 1902 en medio de una gran algarabía el presidente Porfirio Díaz (1830-1915) con un grupo de allegados colocó la primera piedra de la Columna de la Independencia para conmemorar los festejos del centenario de nuestra independencia. Yo con mis ilusiones instalé la primera cátedra de Biología en el país dentro de las aulas de la Escuela Normal Superior para Maestros. Con esta acción México pasaría de tener naturalistas a formar biólogos que se encargaran de entender y explicar las diferentes formas de la vida en el planeta. La herramienta escolar que me ayudó fue un libro hecho por mí y que titulé “Nociones de Biología” de 1904. Es por demás decir que fue el primer libro de texto de biología en México. En esta obra doy a conocer mis ideas evolucionistas y las de mi colega inglés <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/DarwinWallace.php">Charles Darwin </a>(1809-1882). A mi proyecto central de investigación lo llamé la “Plasmogenia” y se trataba del estudio, el origen y la evolución de la vida.</p>
<p>En 1906 mi cátedra de Biología fue suprimida por ser peligrosa para la juventud y las creencias sociales de aquella época. Mis perspectivas de la vida, su origen y su evolución no encajaban muy bien. Sin embargo siempre seguí luchando porque se reconociera a la Biología como actividad científica.</p>
<p>En 1915 al ser nombrado director del <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/MNHN.php">Museo Nacional de Historia Natural </a> diseñé la creación de la Dirección de Estudios Biológicos un dos de octubre de ese año. Su edificio se encuentra actualmente en las calles de Balderas y Ayuntamiento en el Centro Histórico. Gracias a esta dirección se establecieron el Jardín Botánico y el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ZCH.php">Parque Zoológico </a> en el Bosque de Chapultepec.</p>
<p>En la Dirección de Estudios Biológicos trabajé con el italiano <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/CasianoConzatti.php">Cassiano Conzatti </a>(1862-1951), el alemán Karl Friederich Reiche (1860-1929) y el mexicano <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/CasianoConzatti.php"> Maximino Martínez </a>(1888-1964), todos ellos botánicos de primera.</p>
<p>Unos años más tarde mi Dirección de Estudios Biológicos se convertiría en el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/IB.php">Instituto de Biología </a>de la Universidad Nacional Autónoma de México, pero por tener enfoques distintos a los del poblano <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/IsaacOchoterena.php">Isaac Ochoterena </a>(1885-1950) ya no fui parte de esta importante institución.</p>
<p>En 1922 di clases de Ciencias Naturales en la Escuela Nacional de Altos Estudios y de los dos alumnos que se inscribieron solo uno terminó la carrera siendo el primer biólogo. Su nombre es <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/EnriqueBeltran.php">Enrique Beltrán Castillo </a>(1903-1994) quién también fue un amigo cercano.</p>

<p>Finalmente el 17 de septiembre de 1942 con todos los condimentos para el desarrollo de la Segunda Guerra Mundial tuve mi último respiro, pero me llena de gran alegría haber contribuido al nacimiento de la Biología mexicana y puedo afirmar contundentemente que fue mi gran pasión realizarlo.</p>

<p>En algún momento de inspiración escribí este breve poemas:</p>

<p>"Quisiera ser el soñador ardiente</br>
de inmenso afán y poderoso vuelo</br>
ceñido con estrellas en la frente,</br>
y empuñando la lámpara del cielo...</br>
Y así volar, fluctuando entre los mundos</br>
y el abismo sin astros indescrito</br>
y ante el destino de la vida adverso</br>
donde su barco sin piloto avanza</br>
mitigar el dolor del Universo</br>
con los cantos de amor y de esperanza."</p>

<p>Alfonso L. Herrera
