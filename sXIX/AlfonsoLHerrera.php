												<h2>Alfonso Luis Herrera </h2>
												<h3>1868 - 1942</h3>
											</header>
<p>Alfonso Luis Herrera López fue un destacado científico, considerado pilar de la biología moderna en México, y uno de los primeros investigadores de este país en estudiar sistemáticamente el origen de la vida. Hijo de <strong>Alfonso Herrera Fernández</strong> de San Salvador y Adela López Hernández. Nació en la ciudad de México en 1868(9) y falleció en 1942.  </p>
                                          <p>Estudió farmacia en la Escuela Nacional de Medicina, pero a partir de su ingreso como ayudante al Museo Nacional de Historia Natural (MNHN) en 1889 se interesó en la biología. Ahí realizó una serie de catálogos que sistematizaron las diferentes colecciones de vertebrados existentes en el museo. En 1897 se casó con María Estrada Delgado.</p>
                                          <p>Entre otros quehaceres se dedicó a la docencia, fundando en 1902 la primera cátedra de Biología general en la Escuela Normal para Maestros. Como herramienta para el curso escribió &ldquo;Nociones de Biología&rdquo;: primer libro de texto de biología en el país. En éste, expuso sus ideas evolucionistas e introdujo al país las del naturalista inglés Charles Darwin. Ahí además propone la &ldquo;<strong>Plasmogenia</strong>&rdquo;, ciencia que estudia el origen y evolución de la vida. </p>
                                          <p>En 1906 su cátedra de biología fue suprimida por ser peligrosa para la juventud y las creencias.</p>
                                          <p>En 1915 fue nombrado director del MNHN y como parte de su proyecto, creó la Dirección de Estudios Biológicos (DEB), y que posteriormente, se convirtió en el Instituto de Biología de la Universidad Nacional Autónoma de México.</p>
                                          <p>Uno de los logros de la DEB fue el establecimiento en 1922 del Jardín Botánico del Bosque de Chapultepec y un año después, la creación del Parque Zoológico de Chapultepec, que actualmente lleva su nombre.</p>
                                          <p>Sus trabajos fueron dados a conocer en publicaciones como <em>Nueva Farmacopea Mexicana</em>, <em>Memorias de la Sociedad Científica Antonio Alzate, Anales del Museo Nacional, La Naturaleza, Science</em>, entre muchas otras.</p>
