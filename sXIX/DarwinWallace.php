<p>El 1 de julio de 1858 presentamos juntos a la Sociedad Linneana de Londres el tema "Sobre la tendencia de las especies a formar variedades; y sobre la perpetuación de variedades y especies por medios naturales de selección". La presentación consistió en un artículo de Alfred titulado "Sobre la tendencia de las variedades de apartarse indefinidamente del tipo original" y un extracto de mi trabajo no publicado sobre las especies de 1844, con una carta que le envíe al botánico estadounidense Asa Gray. Alfred tenía 35 años y yo 49. La presentación se publicó el 20 de agosto de 1858.</p>
<p>La Sociedad Linneana de Londres se fundó en 1788 con el nombre en honor al botánico sueco <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/CarlVonLinne.php">Carl Linnaeus.</a> Actualmente se reconoce como la sociedad biológica activa más antigua del mundo. Desde 1854 la Sociedad se aloja en la Casa Burlington construida en 1664 en Picadilly, Londres.</p> 
<p>En 1856 Charles Lyell leyó un artículo de Alfred Wallace titulado "Sobre la ley que ha regulado la introducción de nuevas especies" que le pareció tenía mucha similitud con mis pensamientos y me motivo a publicar mis ideas tan pronto como fuera posible. Yo no estaba tan preocupado pero el 18 de junio de 1858 recibí un manuscrito de Alfred Wallace en donde describía la "selección natural" tal y como yo la había pensado. Le envié el manuscrito a Lyell y él junto con Hooker me sugirieron que hicieramos una presentación juntos. </p>
<p>Sir Charles Lyell (1797-1875), el famoso geólogo escocés junto con el botánico inglés Joseph Dalton Hooker (1817-1911), ambos grandes amigos, prepararon la sesión con la Sociedad para que ambos artículos se presentaran juntos. Ellos conocían mi trayectoria sobre el tema de la evolución y sabían que había estado escribiendo sobre esto desde mi regreso del gran viaje en el Beagle en 1836. Un año más tarde el 22 de noviembre de 1859 publiqué "Sobre el origen de las especies" y el tiraje de 1250 ejemplares se vendió ese mismo día.</p>
