												<h2>Alfredo Dugès</h2>
												<h3>1826 - 1910</h3>
											</header>
<p>Nací en 1826 en la ciudad de Montpellier en la costa sur francesa. En esta ciudad se creó el primer Jardín Botánico francés en 1593. Su facultad de medicina también es una de las más antiguas del mundo y en ella estudió el famoso astrólogo Nostradamus. Mi papá Antonio Louis Dugès (1797-1838) fue médico y a él le debo el interés fascinante por la naturaleza. Publicó un estudio relacionado a la anatomía de las ranas.</p>
<p>En mi juventud descubrí que pintando acuarelas podía retratar con todo detalle a los animales y plantas que veía. Así que antes de graduarme como médico ya había publicado algunos trabajos sobre mis descubrimientos a través de mis ilustraciones. A los 20 años obtuve el título de Bachiller en Ciencias y Letras y al igual que mi querido papá, también me convertí en médico, ya que es esos tiempos era lo más cercano a mi verdadero interés por la historia natural.</p>
<p>Para el año 1853, Napoleón III (1808-1873) sobrino de Napoleón Bonaparte, subió al poder en Francia con sus ideas expansionistas y haciendo la guerra por todos lados así que platiqué con mi esposa Marie Louise Frey (1816-1885) le propuse cambiar de ambiente y decidimos partir a México. En este país practiqué mi profesión en ciudades como Veracruz, Silao y Guadalajara pero lo más interesante es que incursioné en dar clases sobre plantas y animales en la ciudad de Guanajuato. Debo decirles que fue algo que me gustó muchísimo. En esta ciudad me sentí muy a gusto y decidí quedarme a vivir y trabajar desde aquí. Después de algunas cartas con mi hermano Eugenio Dugès (1826-1895), él también decidió venir a México y se encargó del Departamento de Historia Natural del Museo Michoacano. Su especialidad eran los insectos.</p>
<p>Mi vida y mi trabajo como médico y como naturalista estuvieron dedicados a los anfibios y reptiles de México. Por eso me han nombrado el “padre de la herpetología mexicana”. Aunque debo confesarles que también hice trabajos con ilustraciones sobre mamíferos, insectos y aves, los cuales son verdaderas obras de arte. ¡Y ahora que me acuerdo!, también escribí algo de botánica y paleontología.</p>
<p>En mis investigaciones recurrí a los puntos de vista de otros naturalistas en el mundo, así como de importantes mexicanos como Alfonso L. Herrera (1868-1942), Manuel M. Villada (1841-1922) y el paisajista José María Velasco (1840-1912).</p>
<p>Quiero aclararles que no fui el primero en describir la riqueza biológica de este país, pero gracias a mis ilustraciones muchos mexicanos se interesaron ya sea por la práctica del dibujo científico o por el estudio de los reptiles. La revista científica de la Universidad de Guadalajara, el Museo de Historia Natural de la Universidad de Guanajuato y el premio que otorga el congreso del estado de Guanajuato llevan mi nombre como una manera de reconocimiento a mi labor en este maravilloso país.</p>
<p>Lamentablemente mi esposa Marie murió a los 69 años y nunca tuvimos hijos y yo en el año de 1910 tuve mi último respiro en mi estado, como un mexicano por naturalización y orgulloso de contribuir al conocimiento de la naturaleza mexicana.</p>
