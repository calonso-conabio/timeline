												<h2>Alfredo Dugès</h2>
												<h3>1826 - 1910</h3>
											</header>
<p>Nací el 16 de abril de 1826 en la ciudad de Montpellier en la costa sur de Francia. En esta ciudad se creó el primer Jardín Botánico francés en 1593. Su facultad de medicina también es una de las más antiguas del mundo y en ella estudió el famoso astrólogo Nostradamus (1503-1566). Mi papá Antonio Louis Dugès (1797-1838) fue médico y a él le debo el interés fascinante por la naturaleza. Publicó un estudio relacionado a la anatomía de las ranas. Fue discípulo de Étienne Geoffroy Saint-Hilaire (1772-1844), profesor del Museo de Historia Natural y colaborador de los naturalistas Henri Milne Edwards (1800-1885) y Georges Cuvier (1769-1832). Además, mi bisabuela y una de las hermanas de mi abuela fueron parteras muy reconocidas. Mi abuelo también fue médico cirujano. Mi madre se llamó Rene Euphrosine Vanard y fui el mayor de cuatro hijos: Marie Louise, Antoinette-Eugénie y Eugenio. Mi padre falleció cuando yo tenía 12 años. </p>

<p>En mi juventud descubrí que pintando acuarelas podía retratar con todo detalle a los animales y plantas que veía. Así que antes de graduarme como médico ya había publicado algunos trabajos sobre mis descubrimientos a través de mis ilustraciones. A los 20 años obtuve el título de Bachiller en Ciencias y Letras y al igual que mi querido papá, también me convertí en médico, ya que es esos tiempos era lo más cercano a mi verdadero interés por la historia natural. Después de obtener el título de médico a los 26 años me casé con Marie Louis Frey (1816-1885) en Inglaterra. Ella era ocho años mayor que yo y esa situación causó un gran desacuerdo con mi madre. </p>
<p>Para el año 1853, Napoleón III (1808-1873) sobrino de Napoleón Bonaparte, subió al poder en Francia con sus ideas expansionistas y haciendo la guerra por todos lados así que le propuse a mi esposa cambiar de ambiente y decidimos partir a México. En este país practiqué mi profesión en ciudades como Veracruz, Silao y Guadalajara pero lo más interesante es que incursioné en dar clases sobre plantas y animales en la ciudad de Guanajuato. Debo decirles que fue algo que me gustó muchísimo. En esta ciudad me sentí muy a gusto y decidí quedarme a vivir y trabajar desde aquí. Después de algunas cartas con mi hermano <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/EugenioDuges.php"> Eugenio Dugès </a>(1826-1895), él también decidió venir a México y se encargó del Departamento de Historia Natural del Museo Michoacano. Su especialidad eran los insectos.</p>
<p>Mi vida y mi trabajo como médico y como naturalista estuvieron dedicados a los anfibios y reptiles de México. Por eso me han nombrado el “padre de la herpetología mexicana”. Aunque debo confesarles que también hice trabajos con ilustraciones sobre mamíferos, insectos y aves, las cuales son verdaderas obras de arte. ¡Y ahora que me acuerdo!, también escribí algo de botánica y paleontología.</p>
<p>En mis investigaciones recurrí a los puntos de vista de otros naturalistas en el mundo, así como de importantes mexicanos como <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfonsoLHerrera.php">Alfonso L. Herrera </a>(1868-1942), <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/ManuelMVillada.php"> Manuel M. Villada </a>(1841-1922) y el paisajista <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/JoseMVelasco.php"> José María Velasco </a>(1840-1912). Con Alfonso Herrera sostuve un gran intercambio de cartas por varios años y nos convertimos en muy buenos amigos. </p>
<p>En 1869 publiqué la obra "Reptiles y batracios de los Estados Unidos Mexicanos" donde describí la herpetofauna (como se conoce a anfibios y reptiles juntos) investigada hasta el momento.  Reuní información sobre 182 reptiles y 37 anfibios. En esos días, no sabíamos que México llegaría a ser el segundo país con más especies de reptiles, solamente sobrepasado por Australia. ¡Actualmente se conocen 864 especies de reptiles y 376 de anfibios! </p>

<p>En 1878, en la sesión del décimo aniversario de la <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural, </a>presidida por Porfirio Díaz Presidente de México, recibimos premios como socios destacados yo y mi hermano, <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/JoseMVelasco.php"> José María Velasco </a>, <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfonsoLHerrera.php">Alfonso L. Herrera </a> y <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/FernandoAltamirano.php"> Fernando Altamirano </a>, <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AntonioCastillo.php">Antonio del Castillo, </a>y <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/MarianoBarcena.php">Mariano Bárcena.</a></p>
<p>Quiero aclararles que no fui el primero en describir la riqueza biológica de este país, pero gracias a mis ilustraciones muchos mexicanos se interesaron ya sea por la práctica del dibujo científico o por el estudio de los reptiles. La revista científica de la Universidad de Guadalajara, el Museo de Historia Natural de la Universidad de Guanajuato y el premio que otorga el congreso del estado de Guanajuato llevan mi nombre como una manera de reconocimiento a mi labor en este maravilloso país.</p>
<p>Lamentablemente mi esposa Marie murió a los 69 años y aunque nunca tuvimos hijos adoptamos a las hijas de un amigo viudo: María y Luisa Reynoso Ruiz. El 10 de abril de 1910 tuve mi último respiro debido a la arterioesclerosis. Morí en mi estado Guanajuato, como un mexicano por naturalización y orgulloso de contribuir al conocimiento de la naturaleza mexicana.</p>
