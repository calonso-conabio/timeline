												<h2>Alfredo Dugès</h2>
												<h3>1826 - 1910</h3>
											</header>
<p>Alfredo Augusto Delsescautz Dugès fue un naturalista considerado el más destacado zoólogo entre los siglos XIX y XX en México y padre de la herpetología en este país. Nació en 1826 en Montpellier, Francia, y murió en Guanajuato, México en 1910. Hijo del médico y naturalista Antoine Louis Dugès. </p>
                                          <p>Recién casado con Marie Louise Frey emigró a  México en 1853, médico de profesión, ejerció en varias ciudades del país, como Veracruz, Silao y Guadalajara. No hubo hijos de su matrimonio. Impartió las cátedras de Botánica y Zoología del gabinete de historia natural y el jardín botánico en el Colegio del Estado de Guanajuato, hoy Universidad del Guanajuato. Su hermano Eugenio Dugés, interesado en insectos, también se vino a México y se encargo del Departamento de Historia Natural del Museo Michoacano.<br>
                                                                                                                                                               <br>
                                            Se interesó en el estudio de la fauna mexicana. Aunque su interés principal eran los reptiles y anfibios, también realizó trabajos sobre mamíferos, insectos y aves, e inclusive hizo trabajos aislados sobre botánica y paleontología. Realizó un gran número de dibujos y acuarelas de las aves de México.</p>
                                          <p>Publicó los libros <em>Programa para un curso de zoología</em>, <em>Elementos de zoología </em>y <em>La flora y fauna del Estado de Guanajuato</em>, así como 168 artículos en publicaciones científicas de la époc. Muchas de sus publicaciones aparecen en la revista La Naturaleza de la Sociedad Mexicana de Historia Natural fundada en 1868  y en la revista de la Sociedad Científica Antonio Alzate. En la primera Sociedad participa junto con Alfonso L. Herrera, Luis M. Villada y con José María Velasco.</p>
                                          <p>Tenía una colección personal de varios grupos de animales, especialmente de anfibios y reptiles, pero también tenía ejemplares de mamíferos que posteriormente pasaron a la colección de Mamíferos del Museo de Historia Natural &ldquo;Alfredo Dugès&rdquo; de la Universidad de Guanajuato. La revista científica <em>Dugesiana</em> de la Universidad de Guadalajara se nombró en su honor. </p>
