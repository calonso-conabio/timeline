												<h2>Alfonso Herrera Fernández</h2>
												<h3>1838 – 1901</h3>
											</header>
<p>Nací en 1838 en la ciudad de México y me llamo Alfonso Herrera Fernández. Mi papá se llamaba Francisco Herrera y mi mamá Rosario Fernández San Salvador. Estudié la carrera de farmacéutico que está relacionada con la fabricación de medicamentos. Los años que me tocaron vivir como mexicano fueron en un ambiente de invasiones extranjeras por parte de los ejércitos norteamericanos y franceses que se establecían en nuestras costas del Golfo de México y en la capital. Las guerras eran algo común en nuestras vidas. Cuando tenía nueve años, México había perdido la mitad de su territorio y Santa Ana había salido del poder para siempre.</p>
<p>En febrero de 1868 por indicaciones del presidente Benito Juárez se inauguró la Escuela Nacional Preparatoria. Me uní a su primer director Don Gabino Barreda y su equipo de trabajo. Cuando él dejó la dirección yo fui el responsable de esta joven escuela la cual se encontraba en el Antiguo Colegio de San Ildefonso, que se localiza a dos cuadras del Palacio Nacional en la ciudad de México. y me propuse hacer algo verdaderamente importante por mi país.</p>
<p>No quiero parecer presumido pero no había en América una escuela tan interesante como mi preparatoria, ya que se me ocurrió instalar laboratorios y después un observatorio. Para el área de las ciencias naturales se me ocurrió tener un invernadero con su museo de botánica y en el patio central un jardín botánico, además un pequeño pero interesante zoológico con algunos reptiles, aves y changos. Todo esto para provocar el estudio por las ciencias en los jóvenes incluyendo a mi pequeño hijo Alfonso Luis. Herrera (1868-1942).</p>
<p>Entregué mi vida para promover y difundir el estudio por las ciencias naturales. Fui fundador de la <strong><a href="SMHN.php">Sociedad Mexicana de Historia Natural</a></strong>&quot; en 1869, de la cual fui también presidente, vicepresidente y presidente honorario perpetuo. Apoyé también la creación de la Sociedad Científica “Antonio Alzate” con el objetivo de dar a conocer al mundo lo que hacíamos los científicos mexicanos y relacionarnos con los de otros países para intercambiar opiniones sobre nuestros estudios. Esta sociedad en la época porfirista gozaría de gran prestigio y credibilidad. Pocos saben que fuimos los médicos y los naturalistas los abuelos de la biología en México.</p>
<p>Actualmente me parece increíble que de las nueve preparatorias que tiene la UNAM, ninguna lleve mi nombre pues hice mucho por la consolidación científica y universitaria del país. Fui designado miembro honorario de la Academia Mexicana de la Lengua en 1877.</p>
<p>Después de haber dejado de trabajar en la preparatoria seguía colaborando con distintas sociedades científicas y dando clases a diferentes alumnos, pero una enfermedad en las vías respiratorias me afectaría seriamente. Mantuve correspondencia con Don Porfirio Díaz solicitando se aprobara una jubilación justa y proponiendo que mi hijo Alfonso Luis Herrera se quedara con mi plaza de maestro. Mantuve una relación importante con él porque le dio un importante apoyo a la educación, la ciencia, la cultura y las artes. Finalmente la muerte me alcanzó en la ciudad de Cuautla en 1901. Me voy alegre con la idea de haber dejado un México mejor del que viví ya que siempre creí que a través de las ciencias, México sería más grande y más feliz.</p>
