											<!-- InstanceBeginEditable name="Biografía" -->
											<p>Nací en Temascalcingo, Estado de México en 1840. Mi padre fue Felipe Velasco y mi madre María ANtonia Gomez-Obregón. Mis padres me dieron el largo nombre de José María Tranquilino Francisco de Jesús Velasco Gómez-Obregón de Velasco</p>
                                            <p>Realice mis estudios primarios en la ciudad de México, donde llegué a los 9 años. Mi padre murió de una epidemia de cólera a los pocos meses de nuestra mudanza, así que empece a trabajar muy temprano.</p> 
                                            <p> A los 15 años en 1855 ingresé a la Academia de San Carlos. Ahí fui alumno del pintor italiano Eugenio Landesio (1810-1879) quien acababa de llegar a México y me inició en los paisajes. Durante las clases me di cuenta de la necesidad de conocer profundamente aquello que dibujaba y pintaba así que para complementar mis obra artística ingresé a la Academia de Medicina, donde cursé botánica, zoología, anatomía, arquitectura, geografía, física y matemáticas. Comencé a dar clases de pintura a los 18 años y en 1860 gané un concurso de pintura y con el premio pude aliviar mis problemas económicos.</p>
                                            <p>En 1868 comencé a colaborar en &ldquo;La Flora del Valle de México<em>&rdquo;</em>, una obra de botánica por entregas en la cual hacía dibujos para ilustrar los fascículos. Ese mismo año me casé con Luz Sánchez Armas Galindo y tuvimos una pequeñita a la que llamamos  Mercedes. En 1889 representé al Gobierno de México en la Exposición Universal de Paris para la cual se había construido la maravillosa Torre Eiffel. Fui miembro de la Sociedad Mexicana de Historia Natural (SMHN) donde realicé muchas láminas que servían de apoyo gráfico para los artículos de otros autores que trataban temas de botánica, zoología, geología y paleontología. En esta sociedad fui secretario, vicepresidente y presidente interino. </p>
                                            <p>La SMHN tenía la publicación <em>La Naturaleza</em>, en la cual publiqué mis investigaciones. Mi primer aporte como hombre de ciencia fue un estudio botánico sobre las cactáceas de México. Mi obra biológica más importante fue &ldquo;Descripción, metamorfosis y costumbres de una especie nueva del género <em>Siredon</em>&rdquo;, con ésta di a conocer datos precisos y hasta entonces desconocidos de la metamorfosis del famoso ajolote mexicano.</p>
                                            <p>Por más de 35 años, Velasco trabajé en el Museo Nacional como investigador. Uno de los productos de mi estancia en el museo fue la realización de varios lienzos en los que representé la evolución de la vida y las eras geológicas. En 1905 después de un ataque al corazón me retiré a pintar Villa de Guadalupe Hidalgo donde morí en 1912</p>
