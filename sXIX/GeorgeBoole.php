												<h2><strong>George Boole Hinton</strong></h2>
												<h3>1882 - 1943</h3>
											</header>
<p>Hello! My name is George! Nací en Londres en 1882, hijo de Charles Howard Hinton y Mary Everest Boole, ambos matemáticos y escritores. A la edad de siete años me mudé con la familia a Japón donde mi papá fue contratao como profesor de la Universidad de Tokio. Varios años más tarde nos mudamos a Estados Unidos. Debido a mis problemas de la vista, los primeros años de educación fueron en casa y después estudié en la Escuela de Minas de la Universidad de Columbia en en Minesota, en la Escuela de Minas de Arizona y en la Universidad de California en Berkeley. Pagaba mis estudios trabajando como ensayador en las minas de México durante el verano. El país me gustó tanto que decidí convertirlo en mi hogar junto con mi esposa Emily Percival Wattley en 1911.</p>
<p>Ya en territorio nacional, me desempeñé en diversas profesiones: ingeniero civil, ensayista, ingeniero metalúrgico, arquitecto, e industrial. En 1931, a los 50 años, empecé con el hobby de la botánica. Cinco años más tarde me jubilé  y finalmente me dediqué a ser colector botánico de tiempo completo. El director del Real Jardín Botánico de Kew, el botánico Sir Arthur Hill (1875-1941), me propuso recolectar especímenes de plantas durante mi trabajo en la mina El Rincón en Temascaltepec, en el estado de México.</p>
<p>A mis cincuenta años, me dediqué tiempo completo a la botánica. Hice numerosas expediciones por el territorio nacional acompañado de Jaime Hinton (1915-2006), el más pequeño de mis tres hijos, quién también se convirtió en prominente botánico y escritor. Juntos y con excepcional condición física, recorrimos las selvas secas de las sierras de Guerrero y Michoacán viajando en mula, atravesando zonas que eran prácticamente desconocidas para la botánica debido a lo accidentado del terreno y a la inseguridad de los caminos. También recorrimos Coahuila, Nuevo León, Tamaulipas, el Estado de México y Oaxaca. Muchos de los ejemplares de mi hijo Jaime fueron identificados por el gran botánico <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/JerzyRzedowski.php">Jerzy Rzedowski. </a>Descubrí seis géneros y 360 especies de plantas nuevas para la ciencia. Indeed!</p>
<p>Las expediciones que organizaba eran grandes aventuras. Me ganaba la confianza de los lugareños, llevándoles regalos y medicinas. Muchas veces me internaba en regiones que ni la misma autoridad querían visitar. En 1937 contraje malaria y tuve una hemorragia interna que tuve que controlar comiendo hielo. Unos años más tarde me rompí la cadera en un descarrilamiento del tren, pero la fractura solo me detuvo por seis semanas, antes de volver a salir al campo.</p> 
<p>Del total de cada colecta, una parte la preparaba para venderla a herbarios, jardines botánicos e instituciones de diferentes países, como Inglaterra, Suiza y Estados Unidos. Siempre procuré que algunas de mis colectas se quedaran en el Herbario Nacional del Instituto de Biología de la Universidad Nacional Autónoma de México y en el Herbario de la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional. Otra parte de la colecta la guardaba para mi propio herbario “G.B. Hinton”, en Nuevo Léon, que contiene más de 20 mil ejemplares de plantas, ahora al cuidado de mi nieto George Sebastián Hinton (1949-), la tercera generación de colectores botánicos. Fallecí en 1943 de una tromobosis coronaria después de un accidente en automóvil.</p>
