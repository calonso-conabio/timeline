<p>Hello! My name is George! Nací en Londres, hijo de Charles Howard Hinton y Mary Everest Boole, ambos matemáticos y escritores. A la edad de siete años me mudé con la familia a Japón y después a Estados Unidos, donde estudié minería en varias universidades. En ese país trabajabé como ensayador y visitaba minas en México durante el verano. El país me gustó tanto que decidí convertirlo en mi hogar junto con mi esposa Emily Percival Wattley en 1911.</p>
<p>Ya en territorio nacional, me desempeñó en diversas profesiones: ingeniero civil, ensayista, ingeniero metalúrgico, arquitecto, industrial y finalmente colector botánico. El director del Real Jardín Botánico de Kew, el botánico Sir Arthur Hill (1875-1941), me propuso recolectar especímenes de plantas durante mi trabajo en la mina El Rincón en Temascaltepec, en el estado de México.</p>
<p>A mis cincuenta años, me dediqué tiempo completo a la botánica. Hice numerosas expediciones por el territorio nacional acompañado de James Hinton (1915-2006), el más pequeño de mis tres hijos, quién también se convirtió en prominente botánico y escritor. Juntos y con excepcional condición física, recorrimos las selvas secas de las sierras de Guerrero y Michoacán viajando en mula, atravesando zonas que eran prácticamente desconocidas para la botánica debido a lo accidentado del terreno y a la inseguridad de los caminos. También recorrimos Coahuila, Nuevo León, Tamaulipas, el Estado de México y Oaxaca. Descubrí seis géneros y 360 especies de plantas nuevas para la ciencia. Indeed!</p>
<p>Del total de cada colecta, una parte la preparaba para venderla a herbarios, jardines botánicos e instituciones de diferentes países, como Inglaterra, Suiza y Estados Unidos.  Algunas de mis colectas se encuentran en el Herbario Nacional del Instituto de Biología de la Universidad Nacional Autónoma de México y en el Herbario de la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional. Otra parte de la colecta la guardaba para mi propio herbario “G.B. Hinton”, en Nuevo Léon, que contiene más de 20 mil ejemplares de plantas, ahora al cuidado de mi nieto George Sebastián Hinton (1949-), la tercera generación de colectores botánicos. Fallecí en 1943.</p>
