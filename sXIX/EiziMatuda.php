												<h2>Eizi Matuda</h2>
												<h3>1894 - 1978</h3>
											</header>
<p>Eizi Matuda fue un destacado estudioso y colector de la vegetación mexicana. Nació en 1894 en Nagasaki, Japón, y murió en 1978 en Lima, Perú. Se nacionalizó mexicano en 1928.</p>
                                          <p>Realizó sus estudios primarios, secundarios y de bachillerato en Nagasaki.  Obtuvo el título de Maestro en Ciencias Biológicas por la Universidad Nacional de Taihoku, Japón, en 1914 y varios años después (1962) el doctorado en la Universidad de Tokio. Hasta 1921 fue profesor e investigador de diferentes centros académicos en Japón realizando estudios sobre la flora de algunas regiones de diferentes países orientales como: Hong Kong, Singapur, Japón e Indonesia. </p>
                                          <p>Se mudó a México en 1922, instalándose en una colonia agrícola japonesa del Estado de Chiapas. Ahí comenzó la larga serie de exploraciones y colectas biológicas que haría a lo largo de su vida, recorriendo también otros estados de la república como Oaxaca, Veracruz, Tabasco y Campeche.</p>
                                          <p>A partir de 1950 se integró como investigador al Instituto de Biología de la Universidad Nacional Autónoma de México (UNAM), y al Instituto de Investigaciones Forestales de la Secretaría de Agricultura y Ganadería.</p>
                                          <p>Colaboró en la Comisión Botánica Exploradora del gobierno del Estado de México, de la cual derivaron numerosos trabajos sobre la flora del estado entre las que destacan &ldquo;Las plantas mexicanas del género <em>Yucca</em>&rdquo;, en coautoría &ldquo;Las leguminosas del Estado de México&rdquo; y &ldquo;Las orquídeas del Estado de México&rdquo;.</p>
                                          <p>Describió decenas de nuevas especies de plantas. En 1932 fundó el Herbario Matuda en  la Universidad de Ciencias y Artes de Chiapas. Gran parte del material de sus colectas se encuentra bajo resguardo del Herbario Nacional (MEXU) del Instituto de Biología de la UNAM.</p>
