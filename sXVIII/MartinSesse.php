												<h2>Martín Sesse y Lacasta</h2>
												<h3>1751 - 1808</h3>
											</header>
<p>Nací en 1751 en un poblado muy pequeño llamado Baraguás, en Aragón, España muy cerca de Francia, cruzando tan solo los montes Pirineos. Estudié medicina en la Universidad de Zaragoza, una de las universidades más antiguas de España fundada en 1542. </p>
<p>En 1785 mi amigo Casimiro Gómez Ortega director del Real Jardín Botánico de Madrid, me comisionó para dirigir el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/RJBPVNE.php">Real Jardín Botánico de la Nueva España </a>y para crear la cátedra de botánica en la Real y Pontificia Universidad de México fundada desde 1551 por el rey Carlos V. </p>
<p>El rey Carlos III autorizó la Real Expedición Botánica a Nueva España para continuar la obra de <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVI/FcoHToledo.php">Francisco Hernández de Toledo, </a>médico de Felipe II. Ésta fue una de las expediciones más extensas en el nuevo continente. 
La expedición duró varios años e incluyó recorridos hacia el sur hasta Centro América y hacia el norte hasta Canadá. En la expedición participó el naturalista, anatomista y cirujano José Longinos Martínez, el botánico Vicente Cervantes, y los farmacéuticos Jaime Senseve y Juan del Castillo. Sin embargo, tuve problemas con el primero y siguió independientemente sus trabajos. Más tarde en la Nueva España se incorporaron <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/JoseMMucino.php">José Mariano Mociño </a> José Maldonado y los jóvenes artistas Vicente de la Cerda y Atanasio Echeverría y Godoy, quienes elaboraron más de 2000 ilustraciones científicas de plantas y animales.  </p>
<p>La expedición fundó el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/RJBPVNE.php">Real Jardín Botánico de la Nueva España </a> y la cátedra para la enseñanza de la botánica impartida por el botánico español Vicente Cervantes, discípulo predilecto de Casimiro Gómez Ortega, director del Jardin Botánico de Madrid. Cervantes se quedó en México y tuvo relación con <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/JoseAlzate.php">José Antonio Alzate </a>y la visita en 1803 del naturalista <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/AlexanderHumboldt.php">Alexander von Humboldt </a>y el botánico francés<a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/AimeBonpland.php"> Aimé Bonpland. </a>Su mejor estudiante fue <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/JoseMMucino.php">José Mariano Mociño </a>. Yo regresé a Madrid en 1803 donde fallecí el 4 de octubre de 1808.</p>
