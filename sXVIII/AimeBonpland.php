												<h2>Aimé Alexandre Bonpland</h2>
												<h3>1773 - 1858</h3>
											</header>
<p>¡Bonjour bon ami! Nací el 28 de agosto de 1773 en La Rochelle, ciudad portuaria antigua en la costa Atlántica francesa. Mis padres me bautizaron como Aimé Jaques Alexandre Goujaud, pero más tarde cambié mi nombre simplemente a Aimpe Bonpland, que significa "buena planta". Mi padre Jacques Simon Goujad era médico farmaceútico y mi madre Marguerite Olive de la Coste. Tuve tres hermanos: Michel Simon, Elisabeth Olive y Constance.</p>
<p>En 1790 me mudé a París a estudiar medicina junto con mi hermano Michel. En el Museo Nacional de Historia Natural de París tuve como maestro al naturalista Jean-Baptiste Lamarck (1744-1829), quien propuso una de las primeras teorías de la evolución biológica.  </p>
<p>Conocí a <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/AlexanderHumboldt.php">Alexander von Humboldt </a>en 1798, él era 4 años mayor que yo. Nos hicimos grandes amigos y nos embarcamos en un increible viaje a Sudamérica, el Caribe, México y Estados Unidos que duró de 1799 a 1804. Durante el viaje colecté 60,000 plantas y clasifiqué 6,000 especies de plantas que eran desconocidas en Europa, además de una notable colección de insectos. Mi gran amigo Alexander decía que yo era "como el vino de Francia, una combinación de alegre vitalidad con una profunda corriene interior de riqueza y fuerza". Junto con él escribí <strong>"Narrativa personal de los viajes a las regiones equinocciales del Nuevo Continente durante los años 1799-1804"</strong> y <strong>"Ensayo sobre la Geografía de las Plantas".</strong></p> 
<p>Regresando a Francia trabajé como intendente por 9 años (1805 a 1814), en "el Chateau de Malmaison", residencia de campo de Josefina Bonaparte, primera esposa de Napoleón Bonaparte. El castillo se encontraba cerca del río Sena a 15 kilómetros al oeste de París. El jardin del castillo era de los más bellos de Europa. Ahí cultivaba más de 200 especies de plantas. Tan solo de rosas cultivé alrededor de 250 variedades. Además había canguros, cisnes negros, ovejas, gacelas, avestruces, antilopes, llamas y hasta una foca. Durante mi estancia en Francia, conocí junto con Humboldt al gran militar y político venezolano Simón Bolívar.</p> 
<p>Al morir la Emperatriz, decidí regresar a América. Viajé a Buenos Aires junto con mi esposa Adelaide y nuestra hija Emma y me establecí como profesor en la facultad de Medicina y en el Museo de Historia Natural. ¡Ahí era yo Amado Bonpland! De ahi me mudé a Corrientes, pero mi esposa se regresó a Franica. Santa Ana era un territorio disputado entre Argentina y Paraguay donde establecí una colonia para cultivar y elaborar yerba mate. Sin embargo, mis ideas empresariales no le gustaron al dictador de Paraguay José Gaspar Rodríguez de Francia (1766-1840) quien tenía el monopolio de la yerba mate. Mi colonia fue destruida y yo fui condenado a no salir de Santa María (hoy en el departamento de Misiones) durante diez años. Mis amigos Humboldt, Bolívar y varios más, intentaron liberarme y finalmente fue posible en 1829 después de 9 años. En Corrientes me enamoré de María, hija de un cacique de Paraguay y tuvimos dos hijos. Aprendí mucho de la sabiduría de los indígenas guaraníes con quienes tuve una gran relación.
<p> Me casé por tercera vez con Victoriana Cristaldo, originaria de Paso de los Libres y tuvimos tres hijos: Carmen, Amado y Anastacio. Seguí viajando por Argentina, Uruguay y Brasil, siempre regresando a Corrientes a mi estancia "El Recreo" a la orilla del río Uruguay.</p>
<p>Fallecí el 11 de mayo de 1858 en Paso de los Libres, Argentina a los 84 años, y fui enterrado en el Cementerio de la Santa Cruz. Después de mi muerte el poblado Santa Ana fue denominado Bonpland. A la fecha mis descendientes viven en Corrientes, Argentina. ¡Adieu! </p>
