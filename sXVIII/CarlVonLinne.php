												<h2>Carl Von Linné</h2>
												<h3>1707 - 1778</h3>
											</header>
<p>¡Hej! Crecí en Råshult, un pequeño poblado en una región de poco relieve pero llena de lagos y bosques en el sur de Suecia. A pesar de ser el sur del país, es muy al norte del continente Europeo, en la región conocida como Escandinavia que incluye a Dinamarca, Noruega y Suecia. Fui el mayor de los hijos de Nils Ingemarsson Linnaeus y Christina Brodersonia. Mi padre fue pastor luterano (Cristiano protestante), rector de la Universidad del pueblo de Stenbrohult y tenía como afición la botánica y la jardinería. Mi madre era hija del rector de la Universidad antes que lo sucediera mi padre. Tuve tres hermanas y un hermano. Lo que más me entretenía era jugar en el jardín de la casa junto con mi papá.</p>
<p>Mas tarde fui conociendo a varios médicos interesados en la botánica y decidí estudiar medicina en la Universidad de Lund en Upsala, al sur de Suecia. Sin embargo mi pasión, más que la medicina, realmente eran las plantas y las materias que más me gustaban tenían que ver con las plantas medicinales. Desde que era estudiante empecé a dar clases sobre botánica y a hacer expediciones para conocer las plantas de la región.</p>
<p>A los 25 años en 1732 organicé una expedición a Laponia, cerca del círculo polar ártico, con apoyo de la Real Sociedad de Ciencias de Upsala. Durante varios meses anduve a pie y a caballo recorriendo el norte de la península escandinava. Esta región es conocida porque la antigua etnia Sami, también conocidos como Lapones. Ellos tienen más de 5,000 años viviendo en la región, son la etnia más norteña de Europa y han domesticado a los renos <i>(Rangifer tarandus), </i>que son la misma especie que los caribous silvestres de Norte América. A mi me interesaba registrar la naturaleza de la región, y en esa expedición descubrí más de 100 plantas. Al regresar escribí el libro titulado "Flora Lapponica".</p>
<p>Más adelante en 1735 recibí mi grado de médico desarrollando una tésis sobre las fiebres intermitentes. En Holanda conocí al botánico Johan Ffredeik Gronovius quien se entusiasmó con mis propuestas para crear una nueva clasificación de las plantas. En general muchas especies de plantas tenían nombres largos y complicados y yo tenía una mejor idea. Mi primera edición de "Systema Naturae" (El sistema de la naturaleza) de 1737 tenía tan solo 12 páginas y presentaba la clasificación jerárquica de los tres reinos de la naturaleza, minerales, vegetales y animales. A estos reinos los dividí en clases, órdenes, géneros, especies y variedades. Gran parte de mi clasificación de las plantas se basó en sus sistemas reproductivos. En 1737 publique "Genera Plantarum" en donde una clasificación basada en características naturales de la forma de flores y frutos. En esos tiempos pensabamos que las especies similares eran derivadas de un mismo padre creado por Dios. Sin embargo, en 1859, el naturalista inglés <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/DarwinWallace.php">Charles Darwin, </a>aclaró el proceso de formación de especies a través de la selección natural.</p>
<p>De regreso a Suecia en 1739 me casé con Sara Elisabeth Moraeus, hija del médico Johan Moraeus, con la que tuve siete pequeños. Unos años más tarde pude enseñar medicina y botánica en la Universidad de Uppsala. Continué trabajando en mi "Systema Naturae" del cual completé 12 ediciones y alcanzó 2400 páginas. Actualmente soy recordado por crear el sistema binomial de nomenclatura, que le da dos nombres a cada especie: su género y su especie. </p>
<p>Fui uno de los fundadores de la Real Academia Sueca de las Ciencias en Estocolmo. Muchos de mis estudiantes pudieron se fueron en expediciones a colectar plantas en todo el mundo. Entre ellos Daniel Solander acompañó al explorador inglés James Cook (1728-1779) en su primer viaje de circunnavegación. Contribuí a mejorar la escala de temperatura Celsius, ya que mi compatriota Anders Celsius había puesto el punto de ebullición como 0 grados y el de congelación como 100 grado. Me pareció que sería más lógico ponerlos al revés.</p>
<p> Fui el primero en colocar al ser humano en la familia de los primates. No lo incluí ahi por razones evolutivas sino por las similaridades que identifiqué con este grupo. Fallecí en 1778 debilitado por un infarto que sufrí tres años antes. Mis colecciones y manuscritos fueron venidos a Sir James Edward Smith fundador de la Sociedad Lineo de Londres.</p>

