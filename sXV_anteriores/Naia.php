												<h2>Naia</h2>
												<h3>12000 BCE - 11985 BCE</h3>
											</header>
<p>Mi nombre es Ma, pero recientemente me han nombrado “Naia”, en honor a las náyades, ninfas de la mitología griega que habitaban en el agua. Nací en el año 12,800 antes del presente. Crecí en el oriente de la península de Yucatán, en el actual estado de Quintana Roo. Fui parte del “Clan del Tigre Dientes de Sable”. Nuestro grupo, de unos 40 integrantes, todos parientes, era reconocido con ese nombre porque nuestro guía y bisabuelo portaba un gran diente de sable de 15 cm en su gargantilla.  Pocos depredadores eran tan respetados entonces como el poderoso tigre dientes de sable, al que con su gran tamaño y su estrategia de acecho en grupo, rara vez se le escapaban sus presas. </p> 
										  <p> En esas épocas el clima era seco y frío y todo estaba cubierto por sabanas donde predominaban los pastos y las hierbas y uno que otro enebro. En ellas habitaban y pastaban grupos de animales de gran tamaño como los gonfoterios, un pariente del elefante de 2.7 m de altura al hombro que podía superar las cinco toneladas. Se alimentaban de hojas y ramas de arbustos y árboles. Para poder derribarlos, los hombres y mujeres del clan estudiaban sus movimientos y planeaban la estrategia. También había perezosos y armadillos gigantes, camellos, caballos y el temerario tigre dientes de sable. Todos estos magníficos y sabrosos animales desaparecieron hace alrededor de 10,500 años a.p.</p> 
										  <p> Me encantaba observar a los grandes animales, ver sus distintos comportamientos y seguir sus gigantescas huellas. También era aficionada a observar aves y otros animales pequeños. Junto con mi mamá, colectábamos distintas plantas para hacer sabrosos y aromáticos brebajes que nos ayudaban cuando recaíamos con alguna enfermedad.</p> 
										  <p>El mar quedaba como a 15 kilómetros de distancia de nuestro territorio habitual. En ocasiones hacíamos salidas para pescar en la orillas del gran acantilado y alimentarnos de exquisitos pescados y caracoles. Me encantaban esas excursiones porque podía recolectar conchas marinas para mi colección. </p> 
										  <p>Cuando hacía más frío en la superficie, podíamos vivir dentro de las cavernas. Ahí teníamos fogatas y antorchas para alumbrar el interior. Además, desde dentro teníamos acceso al agua, en algunos de los cenotes más profundos. Todos los miembros del clan éramos parientes cercanos, hermanos, primos, sobrinos, tíos y abuelos. Los tíos y hermanos mayores cuidaban a los más pequeños.</p>
										  <p>Me encontraron a 600 metros de la entrada y a 42 metros de profundidad en un pozo subterráneo inundado. El pozo Hoyo Negro con una profundidad de 60 metros es parte del sistema Sac Actun, que significa “Cueva Blanca” en maya. Actualmente es considerado el río subterráneo más largo de México y el segundo en el mundo con una longitud de 320 kilómetros. Sin embargo, hace 12 mil años era una gran caverna seca. El nivel del agua era de 50 a 100 metros más profundo que el actual. En Hoyo Negro también se encontraron restos de 27 especies de grandes mamíferos.</p>
										  <p>En la misma región del hoy estado de Quintana Roo se han encontrado a varios de nuestros descendientes dentro de las cavernas inundadas: la mujer de Naharon que murió a los 25 o 30 años (11,670 años), la Mujer de las Palmas, de 50 años (8,050 años), el hombre del Templo y el joven de Chan Hol, ambos vivieron hace 10,000 años. ¡Seguramente fueron descendientes del exitoso “Clan del Tigre dientes de sable”!</p>
