											<p>Me llamo Gakil. Mi nombre significa <a href="http://www.enciclovida.mx/especies/6008004">"garambullo" </a>una de mis frutas preferidas. El nombre me lo dio el wama, nuestro chamán, un hombre viejo y muy sabio. Pertenezco a un grupo que vivió en lo que ahora se conoce como península de Baja California. A nuestra lengua se le ha dado el nombre de laymón o cochimí laymón. Pertenece a la familia yumano-cochimí, que incluye al cochimí, yumano, kumiai, cucapá, kiliwa, paipai, entre otras. </p>
											<p>Crecí en las en las montañas que ahora se conocen como Sierra de San Francisco. Nuestros vecinos del sur de la península fueron los guaycuras y pericúes. Al norte vivían los kiliwa, paipai, y cucapá. Desde lo alto de las montañas veíamos el mar de los dos lados de la península. Nuestra gran familia se integraba por hermanos, tíos, primos, sobrinos. En total éramos como 50 en la familia. Los hombres aprendíamos a cazar desde pequeños, mientras que las mujeres se dedicaban a recolectar plantas comestibles. Nos encantaban las <a href="http://www.enciclovida.mx/especies/6008095">pitahayas dulces</a> y <a href="http://www.enciclovida.mx/especies/6008086">agrias, </a>las <a href="http://www.enciclovida.mx/especies/6009322">tunas </a>y por supuesto los <a href="http://www.enciclovida.mx/especies/6008004">garambullos. </a>En las montañas había mucho "tajé" o <a href="http://www.enciclovida.mx/especies/8011324">borrego cimarrón</a> y en las partes bajas cazabamos al "ammo-gokio" o <a href="http://www.enciclovida.mx/especies/8011132">berrendo.</a> </p>
											<p>A finales del verano y principios del otoño nos reuníamos a celebrar y a pintar las cuevas de la Sierra. Ahí plasmamos a muchos de los animales con los que convivimos como el <a href="http://www.enciclovida.mx/especies/8013249">cóndor, </a>el <a href="http://www.enciclovida.mx/especies/8011664">venado bura, </a>el <i>amogoquió </i> o <a href="http://www.enciclovida.mx/especies/8011324">borrego cimarrón </a>y el <a href="http://www.enciclovida.mx/especies/8011132">berrendo. </a>También pintamos a las <a href="http://www.enciclovida.mx/especies/8012267">ballenas grises </a>que llegaban por miles a finales del otoño a las lagunas costeras y desaparecían en la primavera.</p>
											<p>Actualmente en nuestra Sierra se han descubierto alrededor de 300 sitios con pinturas rupestres y petroglifos. Entre los principales están la cueva de Batequí, la cueva La Pintada, la cueva de la Natividad, la cueva de las Flechas y las grutas del Brinco. Nuestro arte fue reportado por el jesuita <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/FcoClavijero.php">Francisco Javier Clavijero </a>en su libro "Historia de la Antigua o Baja California" publicado en 1789. Ahí describe algunas de las especies de animales y sus nombres cochimíes, como el <i>chimbi </i>o <a href="http://www.enciclovida.mx/especies/8012284">gato montés </a>y el <i>chimbicá </i>o <a href="http://www.enciclovida.mx/especies/8011410">puma.</a> 
