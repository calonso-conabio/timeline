												<h2>Garra de Jaguar</h2>
												<h3>649  - 698</h3>
											</header>

<p> Mi nombre real fue <i>Yuknoom Yich’aak K’ahk’ </i> o Zarpazo de Fuego. Nací el 9 de octubre de 649, así quedó registrado en la estela 9 de la gran ciudad de Calakmul, a la cual llamábamos <i>Uxte´Tuum </i>que significa "Tres piedras". Esta fue la época dorada del reino de Kaan o reino de la serpiente. Mi padre fue <i>Yuknoom Ch'een II </i>“el Grande”. Calakmul era el centro del poder. Durante este siglo sucedió la caída de Teotihuacán en el centro de México. </p>
<p> Crecí en las selvas tropicales de chicle, ramón, pukté, machiche, bayo,  entre otros muchos árboles. Junto al centro ceremonial se extendía el gran bajo de Calakmul, una zona inundable  donde se reunían muchísimos animales en la época de secas. Todos los días podía observar a los monos aulladores en la copa de los árboles comiendo chicozapote, y admirar a los majestuosos tapires bañándose en el bajo. Había pavos ocelados por todos lados y grandes parvadas de pericos que pintaban el cielo de color jade. <p> 
<p>Fui contemporáneo de K'inich Janaab' Pakal, el gobernante maya mejor conocido. Pakal “el Grande” gobernó el reino de B´aakal ("Lugar del Hueso") desde Lakam Ha´ hoy conocida como "Palenque" desde el año 615 hasta el 683. Su magnífica tumba ha sido celebrada desde su descubrimiento en 1952. </p>
<p>Desde que tenía uso de razón, hubo grandes rivalidades entre nuestra ciudad y la ciudad de Tikal al sur, que hoy se encuentra en Guatemala. Mi padre gobernó el reino por 50 años, desde 636 hasta 686. La última batalla de la que salimos triunfantes fue en el año 679. </p>
<p>El 6 de abril de 686, a los 36 años fui declarado como <i>K’uhul ajaw </i> (Señor divino) del reino de <i>Kaan,</i> fecha que fue conmemorada en muchos lugares del reino. Para ese entonces, mi padre tenía 80 años y yo le había ayudado bastante en las grandes decisiones del reino.</p>
<p>El 5 de agosto de 695 el rey de Tikal <i>Jasaw Chan K´awaiil I, </i> conocido como "Ah Cacao" finalmente nos derrotó. Fallecí el 31 de marzo de 698 y fui sepultado en la estructura más alta de Calakmul, conocida ahora como "Estructura II".</p>
